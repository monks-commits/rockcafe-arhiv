<b>Alice Cooper</b> (born Vincent Damon Furnier, February 4, 1948), is a hard rock singer and musician. "Alice Cooper" was originally the name of Furnier's band; he legally changed his own name to Alice Cooper for a successful solo career. He is often referred to as the founder of shock rock due to his gory, theatrical performances.
<p>
Cooper's music influenced many later musicians and helped shape the sound of punk rock and early heavy metal.

