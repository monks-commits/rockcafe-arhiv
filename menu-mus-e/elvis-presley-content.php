<div align=justify>
Пресли был первой суперзвездой рока и одной из немногих звезд музыкального мира, которых можно сравнить с полубогами Голливуда великой эры.
<p>
<b>Элвис Пресли</b> (Elvis Aron Presley) родился 8 января 1935 в городе Тупело, штат Миссисипи.
В надежде на лучшую жизнь семья переехала в Мемфис, штат Теннеси. 
Молодой человек, обладающий музыкальный слухом, имел здесь возможность услышать буквально все, что угодно — от непристойнейшего блюза до самой слюнявой сельской баллады. А у Элвиса был слух. Он внимательно слушал и впитывал все.
Петь он начал в церкви. Там он исполнял нечто вроде белого госпела. 
<br>
В 18-летнем возрасте шофер грузовика Элвис Пресли захотел записать на пластинку две песни и подарить ее матери на день рождения. Он пришел в маленькую студию в Мемфисе. Поборов робость, он толкнул дверь и оказался лицом к лицу с Марион Кайскер, секретаршей. Она позвонила шефу, и через несколько минут Элвис Пресли и Сэм Филлипс стояли рядом в студии записи — в первый, но далеко не последний раз. 
<br>
Открытие уникального "звука Пресли" произошло в точности так, как это изображено в его многочисленных низкопробных фильмах. 
Далее историю Короля рок'н'ролла мы опустим, она слишком известна.
<br>
В начале 1956 Элвис Пресли был #1 в американском хит-параде с песней 'Heartbreak Hotel' ("Отель Разбитых Сердец"). Это было началом самой фантастической и самой успешной сольной карьеры в современной поп-музыке. И это было началом эры рока. 
С того момента Элвиса было невозможно остановить, несмотря на то, что родители, проповедники, представители власти, критики, старые звезды и боссы масс-медиа ненавидели его. А возможно, и благодаря этому. Они злопыхательствовали, бранили его на чем свет стоит, сжигали его чучела и пластинки — но остановить не могли. 
Это было бессмысленно. Пытаться остановить Элвиса Пресли было так же бесполезно, как и рок-н-ролл. 
