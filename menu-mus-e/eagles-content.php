<div align=justify>

К середине 1971-го в Калифорнии появился квартет по имени "Eagles".
Рождением группа обязана Лос-Анжелесу. Может, "Eagles" стали главным его парадоксом: никто из группы, лучше всех воспевших Калифорнию, не был калифорнийцем.
Хотя петь умели все, фронтменом выступил неутомимый Фрей. Его песни и принесли начальный успех - в частности, "Take it easy". Песня вошла в дебютный альбом "The Eagles".
Несмотря на мощную поддержку, пластинка потерпела коммерческую неудачу. Слушатели сходились на том, что группа лучше смотрится на концертах. Критики же единодушно назвали квартет "ещё одной типичной кантри-бандой".
Сложился авторский тандем Фрея - Хенли. Новый альбом, "On the border", стал поворотным в их биографии.
Новое звучание влилось в старое, выкристаллизовав столь необходимую индивидуальность. Пластинка принесла первое "золото" и три хита №1 в чартах "Billboard" – "James Dean", "Best of my love" и "One of these night".
Альбом "One of these nights" собрал "платину". Не будь "Hotel California", он бы остался венцом "Eagles". Песня "Lyin' eyes" получила "Грэмми", а "Jorney of sorcerer" стала заставкой популярного телесериала "Автостопом по Галактике".
<br>"Hotel California" (1976) записывался более полугода в нескольких студиях. 
Хитами стали практически все песни. Но совместное творение Фрэя - Фелдера - Хенли затмевало все. 
В течение всего года песня "Hotel California" лидировала во всех мыслимых чартах, и не было мгновения на земле, когда бы она не звучала где-нибудь в эфире.
"Eagles" официально объявили о распаде в мае 1982-го. В 1994 году квинтет собрался в составе 1978-го.
