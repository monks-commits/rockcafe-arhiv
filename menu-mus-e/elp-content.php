1970, Великобритания
<br>Исходный состав: Грег Лэйк (10.11.1948) - вокал, бас, гитара; Кейт Эмерсон (2.11.1944)- клавишные; Карл Палмер (20.03.1947) - ударные
<br>
<br>Трио ELP можно считать настоящей супергруппой: Лэйк вышел из рядов KING CRIMSON (ранее THE GODS), Эмерсон - THE NICE (ранее GARRY FARR AND THE T-BONES), а Палмер - ATOMIC ROOSTER (ранее CRAZY WORLD OF ARTHUR BROWN). Сценический дебют ELP состоялся 25 августа 1970 г. в Плимуте, а в декабре первый одноименный альбом трио достиг 4-й позиции британского хит-парада (в США в марте 1971 г. пластинка заняла 18-е место). Успех ELP не был случаен - в эпоху расцвета прогрессивного рока высокотехничный симфо-арт-джаз-рок музыкантов пришелся как нельзя кстати. Немаловажно и то, что "текстовиком" ELP стал Пит Синфилд, известный по своей работе в KING CRIMSON.
<br>На протяжении следующих четырех лет ELP работали уверенно и качественно, а лучшими творениями трио стали альбомы "Tarkus" (UK-1/US-9), "Pictures An Exhibition" (UK-3/US-10) и "Brain Salad Surgery" (UK-2/ US-11). Главным идейным вдохновителем ELP был Кейт Эмерсон, приверженец классической музыки, в совершенстве владеющий клавишными инструментами.
<br>В конце 1977 г. ELP почувствовали значительное охлаждение слушателей к своему творчеству. Альбом 1978 г. "Love Beach" не попал даже в британский Топ-45, ввиду чего Эмерсон, Лэйк и Палмер решили прекратить совместную деятельность. Дальнейшая карьера музыкантов сложилась достаточно удачно: Лэйк и Палмер попали в состав ASIA (Палмер также был замечен в проекте P.M. и реанимированных ATOMIC ROOSTER), Эмерсон много работал над саундтрэками к кинофильмам. Однако в 1985 г. Лэйк и Эмерсон сумели договориться о возрождении ELP. Палмер возвратиться не захотел, его за барабанной установкой заменил Кози Пауэлл (RAINBOW), начальная буква фамилии которого помогла сохранить аббревиатуру ELP.
<br>Новый альбом трио "Emerson, Lake And Powell" получился на удивление интересным и цельным: в США диск занял 23-е место, а на родине музыкантов - 35-е. После турне по Америке Пауэлл вышел из состава ELP. Новые переговоры с Палмером не дали результатов, и в 1988 г. Эмерсон и Лэйк объединились с ударником группы HUSH Робертом Берри, организовав проект 3. После неудачного альбома "The Power Of Three" проект развалился. Лишь через четыре года Палмер дал согласие на воссоединение ELP в классическом составе. Новый диск трио "Black Moon" не снискал особой популярности у слушателей (только 78-е место в США), однако музыканты все же отправились в турне. В 1994 г. появился второй альбом возрожденных ELP, сделанный в стилистике AOR и прошедший незамеченным.

