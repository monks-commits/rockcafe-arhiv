<div align=justify>
<img src="zz-top.jpg" alt="<? echo $nam; ?>" width=200 height=230 hspace=10 vspace=3 border=0 align=left>
Техасский гитарист Билли Гиббонс в конце 60-х организовал блюзовую группу "Движущиеся тротуары", но с ней у него ничего не получилось. 
Билли пригласил барабанщика Фрэнка Бирда и басиста Дасти Хилла. 
Так образовался бессменный состав участников команды "ZZ Top". 
Взяв за основу стиля близкий им ритм-энд-блюз, музыканты решительно привнесли в него жесткое хардроковое звучание. 
<br>Первый диск группы вызвал интерес, правда, только у земляков-техассцев, но уже второй альбом обеспечил "ZZ Top" фанами по всем Соединенным Штатам. 
В третьей пластинке оказался первый международный хит группы "Ла Гранж", сама программа спустя некоторое время получила платиновый статус. 
<br>В 1983 году выходит альбом "Eliminator", который вернул "ZZ Top" международную славу и стал лучшим в дискографии группы. В нем содержалось много международных хитов, таких как "Gimme all your lovin'", "Legs" и "Sharp dressed man". 
Историю хард-рока невозможно представить без колоритной троицы "ZZ Top". Они создали себе классный имидж (бороды, стетсоновские шляпы и темные очки). 
