Клавишник Род Арджент, гитарист Пол Аткинсон и барабанщик Хью Гранди начали свои музыкальные эксперименты в 1961 году, будучи еще школьниками. Ребята пригласили к себе басиста Пола Арнольда, который привел за собой своего друга Колина Блунстона, взявшего на себя обязанности вокалиста. Образовавшаяся группа начала свою концертную деятельность, исполняя композиции в духе 50-х годов, обычные для ансамблей того времени. Полу, однако, вскоре музыкальная деятельность надоела, он подался в медицину, а на смену ему пришел Крис Уайт. В этом составе группа и играла в 1962 году, а в 1963-м некоторые члены стали подумывать о роспуске команды, в связи с предстоящим поступлением в университет. Род и Крис решили поучаствовать в местном конкурсе молодых талантов "The Herts Beat Contest", призом в котором являлся контракт с записывающей фирмой "Decca". Они считали, что победа в нем удержит группу от развала. 
<br>Еще до окончания конкурса "Zombies" уже получили от "Decca" предложение о сотрудничестве. Так команда перешла в профессиональную стадию. Продюсер Кен Джонс был без ума от песни Арджента "She's Not There", поэтому первый сингл включал ее вместе с крисовской "You Make Me Feel Good". "She's Not There" стала транснациональным хитом 1964 года. В Америке она заняла самую верхнюю строчку в хит-парадах. 
<br>Следующий сингл с "Leave Me Be" Уайта тоже был неплохим, хотя и не имел такого успеха в чартах, как его предшественник. Третий сингл "Tell Her No", был прохладно встречен в родной Англии, зато попал в американский Топ 10. Тогда "Zombies" отправились в Америку, где они выступали вместе с "Bluebells" и Чаком Джексоном. Группа играла на больших площадках, где на волне "британского вторжения" встречалась с диким восторгом местных фанов. "Zombies" также очень хорошо принимали в Японии, на Филиппинах и в других странах. Вернувшись в Англию "Zombies" обнаружили, что "Decca", выпустив один альбом группы, уже начала забывать о ее существовании. Парни снова взялись за работу и стали издавать сингл за синглом, ни один из которых, правда, не смог повторить успех "She's Not There". Группа также записала саундтрек к фильму "Bunny Lake is Missing" и даже немного появилась в нем. 
<br><img src="zombies3.jpg" alt="<? echo $nam; ?>" width=200 height=150 hspace=10 vspace=3 border=0 align=right>
Следующий сингл, "Whenever You're Ready", также постигла неудача. Разочарованию музыкантов противодействовал лишь энтузиазм Рода и Криса. Имелись также проблемы с продюсером Джонсом, пытавшимся вернуть "Zombies" к их раннему саунду и, в конечном счете, группа рассталась с ним. Музыкальная карьера почти не приносила членам команды никакого дохода, несмотря на то что им удалось подписать контракт с "CBS records". Поэтому было принято решение о роспуске группы, но не раньше, чем они выпустят заключительный альбом. 
<br>Запись "Odessey and Oracle" была закончена в студии "Abbey Road" в конце 1967 года. "CBS/Columbia" не хотела выпускать альбом уже распущенной группы, однако один из менеджеров компании настоял на этом и диск увидел свет в июле 1968-го. Альбом содержал прекрасные вещи типа "Odessy", "Care of Cell 44" и "Time of the Season". Последнюю из этих песен долго гоняла одна американская радиостанция. В конце концов, неожиданно она попала в штатовский Топ 5 в конце 1968-го и в начале 1969-го. "CBS" заказала дополнительный тираж диска, промоутеры начали предлагать огромные суммы денег за воссоединение группы. Однако музыканты не принимали эти предложения. Род Арджент и Крис уже начали работу в проекте "Argent", а Колин Блунстон занялся сольной карьерой. 
<br>В конечном счете, экс-"Zombies" произвели большее число хитов в течение 70-х чем они имели в группе в 60-х. На композицию "She's Not There" было сделано множество кавер-версий такими артистами как Сантана, "The UK Subs" и другими. "Zombies" воссоединялись в 1991 году для записи альбома "New World". Несмотря на мощное влияние группы на коллективы типа "The Kinks", "The Left Banke", "Procol Harum" и других, она так и осталась одной из наиболее недооцененных команд в истории рока. 
