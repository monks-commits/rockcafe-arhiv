        году автобиографии, его с детства завораживали монотонные звуки электробритвы 
        отца и конвейера на местном заводе. Прозвище «Игги» ему дали Майкл Эрлевайн 
        и прочие товарищи по группе «Prime Movers» за то, что Остерберг прежде 
        играл в команде The Iguanas.</font></p>
      <p><font size="2">Эти блюз-коллективы не устраивали <strong>Игги</strong>, 
