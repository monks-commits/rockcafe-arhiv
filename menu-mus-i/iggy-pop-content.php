        о записи новых альбомов пришлось оставить. Игги Поп предпринимал неоднократные 
        попытки возродить The Stooges, и большую помощь ему в этом оказывал (с 
        1971) Дэвид Боуи, который убедил его на какое-то время перебраться в Англию. 
