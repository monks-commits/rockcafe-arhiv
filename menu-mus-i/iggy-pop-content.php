        раскрепощенную, основанную на энергичном повторении мощных аккордов. Вдохновлённый 
        примерами Лу Рида и Джима Моррисона, в 1967 г. он создаёт команду The 
        Stooges, звучание которой было вызывающе примитивным. На сцене в <strong>Игги 
        Попа</strong> словно вселялся легион бесов: выступая с обнажённым торсом 
        и расстёгнутой ширинкой, он всячески бесчинствовал, «нырял» в толпу фанатов 
        (первым в истории), оказываясь к концу шоу вымазанным в собственной крови 
        от царапин.</font></p>
      <p><font size="2">К началу 1970-х участники <strong>The Stooges</strong> 
