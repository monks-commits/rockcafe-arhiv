<p>
Iron Maiden провели тур совместно с Kiss, посетив США, Канаду и Японию, и выпустили второй альбом, Killers, продолжавший стиль первого. Начиная с этого альбома, продюсером группы стал Мартин Берч. В это время большинство музыкантов были недовольны работой вокалиста ди Анно: он много выпивал, употреблял наркотики и часто не мог петь на необходимом уровне. Решением группы Пол был уволен, его место занял Брюс Диккинсон, ставший позднее самым известным «голосом» Iron Maiden. Ди Анно продолжил карьеру, основав одноимённую его последнему альбому с «девой» группу Killers. в силу того, что Пол всегда был поклонником панка Мэйдэн врядли бы смогли достичь коммерческого успеха как металлическая группа при его участии.

      <p><strong><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="iron_maiden_03.jpg" alt="<? echo $nam; ?> photo" width=400 height=400 hspace=10 vspace=3 border=0 align=left></a>«Классический» 
        период</strong></p>
      <p> В 1982 году вышел The Number of the Beast (Число Зверя): третий альбом 
        группы и первый с участием Диккинсона. Брюс, хотя исполнил все вокальные 
        партии на альбоме, даже не значился в титрах при первом издании, так как 
        имел контракт с другой студией, угрожавшей ему иском. Альбом сопровождался 
        целым рядом синглов — на песни Run to the Hills, The Number of the Beast, 
        Hallowed Be Thy Name — и стал прорывом для группы, заняв первое место 
        в чартах продаж в Британии, попав в десятку лучших в США, Канаде и Австралии. 
        В поддержку альбома группа провела тур «Beast on the Road», завершившийся 
        аншлаговым концертом в Нью-Йоркском палладиуме. 
      <p>
В следующие два года вышли альбомы Piece of Mind и Powerslave. Оба альбома были записан в студии в Насау, на Багамских островах. Iron Maiden провели туры в поддержку альбомов, в ходе которых впервые посетили социалистическую Польшу. Во время этих туров техники группы усложнили сценические представления, добавив элементы светомузыки. Эдди теперь управлялся не человеком, сидящим внутри, а сложным механизмом.
<p>
На этих альбомах Iron Maiden стали значительно меньше внимания уделять шокирующе-мистической тематике, за которую подвергались критике со стороны религиозных организаций. Преобладать в лирике стали темы фантастики и истории. Iron Maiden написали песню To Tame a Land по мотивам «Дюны» Фрэнка Херберта, Trooper о Крымской войне, Where Eagles Dare и Aces High о летчиках Второй Мировой Войны, песни по мотивам греческой и египетской мифологии.
<p>
В конце 80-х Iron Maiden отошли от агрессивного звука к более экспериментальному на альбомах Somewhere in Time и Seventh Son of a Seventh Son. Seventh Son… стал полу-концептуальным альбомом. Хотя в отличие от аналогичных работ Blind Guardian и Manowar альбом не содержал подробного сценария и текстовых интерлюдий, тексты песен этого альбома складываются в рассказ о жизни ребенка, наделенного провидческими способностями и страдающего от этого. Альбом основан на книгах фантаста Орсона Скотта Карда.
<br clear="all"><br>

      <p><strong><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="iron_maiden_06.jpg" alt="<? echo $nam; ?> photo" width=400 height=396 hspace=10 vspace=3 border=0 align=left></a>Смены 
        состава в 90-е</strong></p>
<p>
Адриан Смит скептически относился к экспериментам Харриса, и в 1990 году покинул группу из-за стилистических разногласий. Его заменил Янник Герс - экс гитарист Gillan и White Spirit. В это же время Брюс Диккинсон записал свой первый сольный альбом Tattooed Millionaire, а в конце того же года вышел альбом No Prayer for the Dying, отличавшийся более тяжёлым звуком. Песня Bring Your Daughter… to the Slaughter вышла отдельным синглом и вошла в саундтрек к фильму «Кошмар на Улице Вязов-5». Следом был записан Fear of the Dark, один из самых известных альбомов группы, вновь попавший на верхние строчки хит-парадов. Эдди претерпел значительные изменения, так как впервые художником-оформителем был не Дерек Риггс, а Мелвин Грант. После выхода альбома группа провела мировой тур, запись которого была выпущена на концертном альбоме The Real Live Dead One. В последнем концерте принимал участие иллюзионист Саймон Дрейк, который демонстрировал свои фокусы-ужасы в перерывах между выступлениями группы.
<p>
Ещё до начала тура стало известно, что Брюс Диккинсон, «голос» группы, покидает Iron Maiden. Брюс был недоволен последними работами группы и хотел в своей сольной карьере более свободно экспериментировать с разными стилями. Место вокалиста занял Блэйз Бейли из Wolfsbane.
<p>
В новом составе группа выпустила альбом The X Factor в 1995 и Virtual XI в 1996. Альбомы были практически полностью написаны Стивом Харрисом, который начал экспериментировать с клавишными и электронной обработкой звука. Однако отсутствие популярного Диккинсона сказалось негативно на популярности группы и продажах альбомов, которые уступали предыдущим показателям.
<p>
Тур в поддержку альбомов, намеченный на следующий год, оказался сорван по вине Блэйза. Вокалист страдал от приступов аллергии и не смог принять участие в живых выступлениях.
<br clear="all"><br>




      <p><strong><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="iron_maiden_04.jpg" alt="<? echo $nam; ?> photo" width=400 height=400 hspace=10 vspace=3 border=0 align=left></a>Воссоединение</strong></p>
<p>
Iron Maiden провели тур, озаглавленный Ed Hunter только в 1999 году, и для участия в нём были приглашены бывшие музыканты группы: Брюс Диккинсон и Адриан Смит. Группа воссоединилась и был записан альбом Brave New World (2000), после чего состоялся тур в его поддержку. Тур был столь удачен, что выступление воссоединившейся группы в Рио-де-Жанейро вышло отдельным концертным DVD Rock in Rio.
<p>
Диккинсон и Смит приняли решение остаться с группой на постоянной основе. При этом ни Яник Герс, ни Дэйв Мюррей не покинули ее состава, таким образом, в Iron Maiden оказалось сразу три гитариста и шесть постоянных музыкантов в общей сложности. Это заметно сказалось на стиле их следующих альбомов: Brave New World (2000), Dance of Death (2003) и a Matter of Live and Death (2006). На этих альбомах группа продемонстрировала элементы прогрессивного метала: записывала песни большой длины — до тринадцати минут, с многоминутными соло, зачастую исполняемыми всеми тремя гитаристами по очереди.
<p>
Популярность группы снова пошла в рост. Каждый тур Iron Maiden был успешным, видеоклипы транслировались по телевидению.
<br clear="all"><br>


      <p><strong><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="iron_maiden_05.jpg" alt="<? echo $nam; ?> photo" width=400 height=400 hspace=10 vspace=3 border=0 align=left></a>Инцидент 
        на Оззфесте</strong></p>
<p>
Во время своего выступления на Ozzfestе Iron Maiden оказались вовлечены в неприятный инцидент, который в равной степени оставил у многих поклонников как неприятные воспоминания, так и восхищение группой. Происшествие случилось во время выступления 20 августа 2005 года в Сан-Бернардино: Iron Maiden были забросаны яйцами и всевозможным концертным мусором, а кто-то из толпы даже плюнул в Брюса Диккинсона. Более того, им несколько раз отключали звук, задержали появление на сцене Эдди и скандировали в громкоговорители «Оззи», когда легендарные британцы появились на сцене. Причины подобной ненависти к Iron Maiden организаторов шоу остаётся загадкой. Из возможных предположений наиболее популярные: 1) Диккинсон разболтал секрет Оззи Озборна, использующего на концертах «телепромтер» (фактически видеосуфлёр); 2) Диккинсон высказывался о реалити-шоу «Семейка Озборнов» как о недостойных рок-исполнителей. Сами «Мэйдэн» на протяжении концерта показывали удивительную стойкость, часто смешанную с отборной руганью Диккинсона в адрес организаторов концерта. Вместо укороченного концерта группа решила отыграть свой полноценный сет, а перед исполнением Iron Maiden Диккинсон, которому всё это время удавалось контролировать аудиторию, обратился к фэнам со словами, которые говорили о том, что только ради всех собравшихся группа терпит все эти унижения. «Сегодня здесь собралось огромное количество наших поклонников, и мы выступаем для них. Нужно приложить много больше усилий, чтобы заставить Iron Maiden покинуть сцену, и ничто не сможет встать между нами и нашими поклонниками. Мы лучше умрём, чем примем позор, мы - Iron Maiden!!!»
<p>
После завершения концерта на сцену вышла Шэрон Озборн и вполне предсказуемо сказала лживые слова благодарности в адрес Iron Maiden, упомянув, что они — «замечательная группа, но Брюс Диккинсон — козёл». Однако люди, взбешённые всем произошедшим, заставили её под свист удалиться со сцены и сами стали покидать зал. Black Sabbath играли в лучшем случае перед половиной пришедших и выглядели довольно жалко на фоне группы, которая выступала перед ними.
