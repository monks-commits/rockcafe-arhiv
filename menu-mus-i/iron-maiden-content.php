    <td colspan=2 valign=top class=menu1> <p><strong><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="iron_maiden_02.jpg" alt="<? echo $nam; ?> photo" width=264 height=235 hspace=10 vspace=3 border=0 align=left></a>История</strong></p>
<p>
Iron Maiden (в пер. с англ. — Железная дева) — британская рок-группа, основанная в 1975 году. Iron Maiden считаются одними из самых популярных и значительных исполнителей в стиле «хэви-метал», частью «Новой волны британского тяжёлого металла» (NWOBHM). Основатель группы и автор большинства песен (как музыки, так и текстов) — бас-гитарист Стив Харрис.
      <p> Группа была основана на Рождество 1975 года бас-гитаристом Стивом Харрисом. 
        Название группы, «железная дева» ,было позаимствовано Стивом из кинофильма 
        "Человек в железной маске" (The Man In The Iron Mask), который повествовал 
        о средневековой инквизиции. В средние века "железной девой" называли приспособление 
        для пыток - полую женскую фигуру с шипами на внутренней стороне. Осужденного 
        на пытку помещали внутрь и закрывали ту самую дверцу с шипами, так что 
        они медленно пронзали тело насквозь. Первым вокалистом группы стал Пол 
        Дэй, которого в мае 1976 года, после первого выступления в пабе Cart And 
        Horses (город Стратфорд) сменил Дэнис Уилкок. Ударниками в группе до ее 
        первого релиза были поочередно Рон Rebel Мэттьюс, Барри Паркинс, Thunderstick 
        и Дуг Сэмпсон. Чехарда составов постоянно сотрясала группу и являлась 
        сильной помехой на творческом пути. В 1978 году вокалистом Iron Maiden 
        стал Пол ди Анно. В таком составе группа записала демо-кассету The Soundhouse 
        Tapes в 1978. В группе не было постоянного второго гитариста до тех пор, 
        пока в 1980 к ней не присоединился Эдриэн Смит. В таком составе группа 
        выступила в Top of the Pops, крупнейшем британском теле-чарте, где музыканты 
        отказались играть под фонограмму. Это был первый случай живого исполнения 
        в этой программе после скандального выступления The Who в 1972 году. 
      <p> <a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="iron-maiden.jpg" alt="<? echo $nam; ?> photo" width=200 height=132 hspace=10 vspace=3 border=0 align=left></a>Дебютный 
        альбом, одноимённый группе — Iron Maiden — вышел в 1980 году и сразу имел 
        успех: он поднялся до четвертого места в британском чарте продаж альбомов. 
        Альбом представлял собой хэви-метал в духе Black Sabbath, но более быстрый 
        и энергичный, с некоторым влиянием панк-рока, с воинственными и мистическими 
        текстами. С самого начала на концертах группы начало появляться чучело 
        чертёнка-зомби, прозванного Eddie the Head (Эдди-голова). Эдди стал талисманом 
        группы и отныне изображался на всех её обложках. На обложках двух ранних 
        синглов, Sanctuary и Women in Uniform, Эдди был изображен дерущимся с 
        Маргарет Тетчер, премьер-министром Великобритании, что поначалу стяжало 
        группе слегка скандальную и протестную репутацию. 
