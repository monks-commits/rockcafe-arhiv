Творчество легендарной команды <b>«Procol Harum»</b> всегда было основано на союзе исполнителя и пианиста Gary Brooker и автора песен Keith Reid. Они заявили о своем таланте в 60-х классической композицией "A Whiter Shade Of Pale", дополнив список тех музыкантов, которые с первым релизом возглавляют престижные чарты. 
<br>Бэк-каталог <b>«Procol Harum»</b> говорит за себя, однако группа не собирается дублировать прошлые победы. Новая работа музыкантов станет важной частью их богатой дискографии. Секрет успеха Procol Harum Gary Brooker видит в том, что коллектив играет не поп-музыку, а песни в стиле блюз-соул: "Музыка соул вечна, она не стареет со временем. Вряд ли "A Whiter Shade Of Pale", "A Salty Dog" и многие другие песни стали хитами, если бы они не были соул-композициями". 
<br>"The Well's On Fire" - новый студийный альбом команды после продолжительного перерыва (в 1991 году вышла их последняя пластинка "The Prodigal Stranger"). Тринадцать треков принадлежат перу Gary Brooker и Keith Reid, представляя команду Procol Harum должным образом. Продюсером альбома стал Rafe McKenna, известный по своим работам с UB40, Big Country, Ash. 
