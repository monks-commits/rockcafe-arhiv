История этой знаменитой группы началась в одной из школ Кембриджа, где учились друзья Сид Барретт, Роджер Уотерс и Дэвид Гилмор. По ее окончании Сид и Дэвид поступили в лондонскую школу искусств, а Уотерс – в политех. Там он сколотил свою первую команду "Sigma 6" с товарищами Ником Мэйсоном и Риком Райтом. 
Группа сменила несколько названий (последнее – "Abdabs"). В конце 1965-го "Abdabs" развалился.
<br>
Барретт, Уотерс, Мэйсон и Райт тут же возобновили работу под вывеской "Pink Floyd Sound" (название было взято от имен двух блюзовых музыкантов: Пинка Андерсона и Флойда Кансила). Вскоре название сократилось до "Pink Floyd" и группа приступила к записи дебютного альбома "The Piper At The Gates Of Dawn". Его вдохновителями и основными авторами были Барретт и Уотерс, фантастические стихи которого удачно вписывались в "психоделический планетарный мистицизм" музыкальных открытий Барретта. 
<br>
Автор большинства композиции с этого диска Сид Барретт в ту пору "баловался" ЛСД. Со временем его пристрастия к этому наркотику увеличились. Так и не справившись с этим "недугом", Сид был вынужден покинуть состав группы, предложив на свое место Гилмора. 
<br>
Второй диск, "A Saucerful Of Secrets", оказался еще более совершенным. Почти целиком написанный Уотерсом, он содержал в себе великолепные, очень своеобразно аранжированные композиции. С тех пор начал формирование фирменный саунд группы, по которому она безошибочно узнавалась. 
<br>
Окончательное становление "Pink Floyd" как мега-группы с непререкаемым международным авторитетом произошло в 1970-1971 годах, когда вышли альбомы "Atom Heart Mother" и "Meddle" – наиболее глубокомысленные из раннего творчества ансамбля. С 1973 года в дискографии "Пинк Флойд" начинается целая серия шедевров: "Dark Side Of The Moon", "Wish You Were Here", "Animals" и двойник "The Wall". 

