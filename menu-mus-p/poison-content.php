Эта хэви-метал-группа была основана в 1982 году в американском городке Харрисбург Бретом Мичелсом (Брет Сичак, р. 15 марта 1962, Харрисбург, США; вокал) и Рикки Рокетом (Алан Рим, р. 8 августа 1959, США; ударные). Вскоре к ним присоединились Бобби Долл (Гарри Кай Кендалл, р. 2 ноября 1958, Майами, США; бас) и Мэтт Смит (гитара). Квартет поначалу назывался "Paris" и играл в местных клубах. Позднее команда перебралась в Лос-Анджелес и сменила вывеску на "Poison". После этого события группу покинул Смит, а его место занял C.C. Девил (Брюс Энтони Йоханнессон, р. 14 мая 1963, Нью-Йорк, США; гитара). В 1985 году команда подписала контракт с "Enigma Records". Записанный в следующем году дебютный альбом получил в Америке платину, а композиция "Talk Dirty To Me" с него попала в штатовский Топ 10. 
<br>Следующий диск "Open Up And Say ... Ahh!" тоже оказался успешным (спустя две недели после выхода он тоже стал платиновым), а песня "Every Rose Has Its Thorn" оказалась на вершине американских хит-парадов. Было также выпущено четыре сингла, из которых наибольшим спросом пользовался "Your Mama Don't Dance". 
<br>"Poison" поначалу получили ярлык "глэм" из-за грима, которым они пользовались на своих шоу, но после выхода в 1990 году "Flesh & Blood" команда сменила свой имидж. В этом же году группа провела свое первое британское турне. 18 августа 1990 года "Poison" выступили перед 72-тысячной аудиторией на фестивале "Монстры рока" в Донингтоне, где им был оказан хороший прием. Особой популярностью пользовались хиты типа "Unskinny Bop" и "Something To Believe In". В 1991-м вышел концертник "Swallow This Live!", после чего из группы ушел Девил. Его сменил гитарист Ричи Котзен. Для записи альбома "Native Tongue" была приглашена медная секция из "Tower Of Power Horns", что сделало саунд "Poison" более мелодичным и чем-то похожим на "Bon Jovi". 
<br>Тем временем Котзен не смог прижиться в группе, и в 1993 году был изгнан из команды. Его сменил Блюз Сарасено, при участии которого "Poison" завершили свой южноамериканский тур. 
<br>После двухлетней передышки группа засела в студии для записи нового студийного альбома "Crack A Smile". Но из-за проблем с их компанией грамзаписи выпуск диска был отложен, а вместо него на прилавках появился достигший впоследствии платинового статуса сборник "Greatest hits", в который были включены два новых трека: "Sexual Thing" и "Lay Your Body Down". После этого члены группы занялись каждый своими делами. Брет подался в кино и снялся в фильме "A Letter From Death Row", сценарий которого сам же написал и спродюсировал. Рикки ушел в "Glitter for your soul", а С.С. основал команду "Samantha 7". В 1999-м Брет выпустил сольный альбом. 
<br>К тому времени "Poison" воссоединились в своем классическом составе с С.С. Девилом. Тур, посвященный этому событию, оказался очень успешным, и "Capitol Records" соизволила наконец-то выпустить "Crack A Smile", добавив туда бонусные треки с сессий "MTV Unplugged" 1990 года. Вскоре после этого группа выпустила концертник "Power To The People" на своем собственном лейбле "CMI". 
