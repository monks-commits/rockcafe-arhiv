<img src="puhdys1.jpg" alt="<? echo $nam; ?>" width=200 height=150 hspace=10 vspace=3 border=0 align=left>
За свою более чем 30-летнюю карьеру "Puhdys" записали более двадцати пластинок, проданных общим тиражом около 20 миллионов и побывали с концертами в более чем 20 странах. Эта восточногерманская группа возникла в 1965 году под названием "Udo-Wendel-Combo" и состояла из четырех человек: Харри Йеске (бас), Питер Мейер (р. 5 января 1940; клавишные, саксофон), Удо Якоб (ударные) и Удо Вендел (гитара, вокал). В 1969-м, сменившая вывеску на "Puhdys", команда дала свой первый концерт во Фрейбурге. Новый состав включал в себя кроме Йеске, Мейера и Якоба Дитера Бирра (р. 18 марта 1944, гитара, вокал), Дитера Хертрампфа (р. 29 ноября 1944, гитара). В ту пору ребята исполняли каверы "Deep Purple", "ELP", "Uriah Heep", "Led Zeppelin" и других западных коллективов. 
<br>В 1970-м группа впервые появилась на телевидении ГДР. Единственным условием выступление было исполнение песен на немецком языке. Музыканты выбрали "Turen offnen sich zur Stadt", тут же ставшую хитом в Восточной Германии. 
<br>На следующий год их композиция "Geh dem Wind nicht aus dem Weg"была признана песней года, а коллектив впервые дал концерты за границей ГДР, в Советском Союзе. Группа записала саундтрек к фильму "Die Legende von Paul und Paula" и выпустила свой первый сингл "Geh zu Ihr"/"Zeiten und Weiten". В 1973-м появился первый лонгплей "Puhdys 1", а в 1974-м прошли первые концерты коллектива в Голландии. На следующий год композиции "Puhdys" три раза попадали в ГДРовский Топ 10, а команда у себя на родине была признана лучшей рок-группой. В то же время началась экспансия в Западную Германию, где музыканты дали концерты в Гамбурге и Западном Берлине. "Пудисом" заинтересовалась фирма "Hansa-International", она же выпустила пластинку "Rock'n'Roll Musik". 
<br><img src="puhdys2.jpg" alt="<? echo $nam; ?>" width=200 height=150 hspace=10 vspace=3 border=0 align=right>
<br>Теперь "Puhdys" уже не только гастролировали по ФРГ, но и появились на западногерманском телевидении. В 1978 году состоялось европейское турне группы, когда группа выступила в Польше, Австрии, Голландии, ФРГ и Советском Союзе. 
<br>К тому же музыканты приняли участие в молодежном фестивале на Кубе. К своему десятилетию команда получила звание "Золотого лауреата" телевидения ГДР. В 1979 году в группе появился новый ударник Клаус Шарфшвердт. Следующий год был ознаменован выходом англоязычного альбома "Far from Home", изданного не только в Германии, но и в Англии. К 15-летнему юбилею "Puhdys" порадовали своих поклонников хитом "Rockerrente", ставшим в ГДР бестселлером года. А еще через пять лет было принято решение о роспуске группы и музыканты отправились в прощальное турне. Однако в 1992 году по многочисленным просьбам фанов "Пудис" был возрожден и выпустил компакт-диск "Wie ein Engel". 
<br>В конце 1997 года из-за проблем со здоровьем коллектив покинул Харри Йеске, перебравшийся на Филлипины, а его место занял Питер "Бимбо" Расим (р. 7 июля 1953). В 2001 году впервые за свою историю "Puhdys" записали рождественскую пластинку "Dezembertage". 
