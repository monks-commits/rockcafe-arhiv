Антитеза любви и мира конца 60-х, нью-йоркская группа "Velvet underground" отражала обратную сторону гедонизма той эры.
Весной 1966-го группа записала свой дебютный альбом, но солидные лейблы отказались выпускать его из-за длинных треков с сомнительным содержанием. В конце концов "The Velvet Underground And Nico" вышел лишь в следующем году на "MGM/Verve".
Сам альбом являлся мощным собранием городских безумных увлечений в представлении Рида, с обаянием уличной культуры и безнравственности, граничащей с вуайяризмом. 
Современные критики были в замешательстве, не зная, с чем музыкальный мир имеет дело. Время доказало, что в лице группы рок получил основоположников нового влияния, а их история превратилась в легенду. 
<br>Восстановление команды в 1993 году в оригинальном составе (без Дуга Юла и Нико) для тура горячо приветствовалось фанами. 
Лишь один раз "Velvet underground" появились вместе на церемонии их посвящения в "Зал славы рок-н-ролла" в 1996 году.
Стерлинг Моррисон умер за несколько месяцев до этого события. 
