Обязанная своим появлением таким командам как "Yes" (Стив Хауи и Джеффри Даунс), "ELP" (Карл Палмер) и "UK" (Джон Уэттон), "Asia" - первая супергруппа 80-х и самое большое разочарование для всех поклонников арт-рока. За основанную в Лос-Анджелесе в августе 1981 года группу взялся известный по работе с "Foreigner", "Queen" и "Journey" продюсер Майк Стоун. В декабре того же года был подписан контракт с "Geffen Records". Вышедший в апреле 1982 года дебютный альбом был записан за пять месяцев в лондонской "Townhouse Studios". Пластинку ждал коммерческий успех: она на 9 недель возглавила штатовский хит-парад, а в Англии поднялась до 11 места. Песня “Heat Of The Moment” заняла 4-е место в Америке и 46-е - в Британии. Второй сингл, "Only Time Will Tell", занял 17 строчку штатовских чартов и 54-ю - английских. Сам диск в итоге стал трижды "платиновым" в США, разойдясь в этой стране тиражом более двух миллионов экземпляров, пятикратно "платиновым" - в Канаде, а также получил "золото" во многих других странах. Что касается музыки, которую исполняла группа, это был предельно мелодичный "тяжелый" рок, рассчитанный на усредненного слушателя. 
<p>
Дебютный альбом содержал ряд привлекательных композиций, самой популярной из которых стала изданная синглом "Heat Of The Moment". Из других вещей в подобном стиле были сделаны "Only Time Will Tell" и "Here Comes The Feeling". На второй стороне альбома присутствовали композиции в духе симфо-прогрессива 70-х. Сильной стороной группы всегда были концертные шоу, поскольку сказывался двадцатилетний опыт музыкантов. В свете успешного альбома и сингла первое мировое турне ансамбля ждал крупный успех. 

