<p>
 Весной 1983-го ушедшего к тому времени Уэттона во время концертов подменял Грег Лейк. Появившийся в августе 1983 года второй альбом (снова с Уэттоном), "Alpha", записывался с февраля по март в Канаде. Этот диск на 11 недель занял пятое место в английских чартах. Хитом стала композиция "Don’t Cry". 6 декабря того же года квартет дал концерт в Токио, изданный позже на видеокассете "Asia In Asia". В группе тем временем назрел конфликт. Все песни в основном были написаны тандемом Уэттон-Даунс. Стив Хауи настаивал на чем-то новом, тогда как Джон Уэттон хотел продолжать в том же духе. Несогласный с такой политикой Хауи ушел. В 1985-м вышел третий диск, "Astra", записанный с новым гитаристом Мэнди Мейером (экс-"Krokus"). Этот альбом на одну неделю попал на 68 строчку английских чартов. Наряду с несколькими выполненными в стиле AOR песнями типа "After The War" лонгплей содержал второсортные поп-композиции вроде "Wishing". Мейер явно не годился на роль Хауи, и в начале 1986-го группа оказалась распущена. 
<p>
Некоторое время музыканты были заняты сольными проектами, но спустя три года "Asia" была возрождена. Помимо Джона Уэттона, Карла Палмера, Джеффри Даунса в ее состав вошел Пэт Тролл (экс-"Automatic man"). "Then And Now" состоял из старого материала с первых двух пластинок и одной композиции ("Voice Of America") - с третьего. Из четырех новых песен ни одна особенно не была близкой к "прогрессиву". Некоторый успех в Штатах имела "Days Like These". 
<p>
 В июне 1990 года воскрешенная "Asia" дала запоминающийся концерт в Ноттингеме, изданный позже на видеодиске "Andromeda". Летом 1991-го был выпущен спродюсированный самими музыкантами концертник "Live In Moscow", записанный с Пэтом Троллом. Содержание диска составляла подборка проверенных временем старых вещей пополам с индивидуальными инструментальными соло. Интерес представляли интерпретации "Starless" и "Book Of Saturday" от "King Crimson", а также финальная композиция "Kari-Anne". После выхода диска из коллектива снова ушел Джон Уэттон. Даунс пригласил в команду Джона Пэйна (бас, вокал) и Эла Питрелли (гитара). Весной 1992-го появился альбом "Aqua", абсолютный лидером и продюсером которого был Джеффри Даунс. В его записи также принимали участие Карл Палмер и Стив Хауи. "Aqua" оказался еще менее прогрессивен, чем его предшественник. Лучшими вещами здесь были инструменталки Хауи и сингл "Who Will Stop The Rain?", хотя и не особенно арт-роковые. В силу контрактных обязательств Палмер, Хауи и Питрелли не могли принимать участие в концертной деятельности, поэтому на гастроли отправились Даунс, Пейн, Тревор Торнтон (ударные) и Винни Бернс (гитара). 
<p>
Перелом к лучшему произошел в 1996-м с выходом альбома "Arena" - самого сильного диска "Asia" в 90-е годы. На нем Джеффри Даунс наконец-то получил полную свободу самовыражения. К концу 90-х музыканты в основном занимались сольными проектами, а у группы выходили лишь сборники. Лишь в 2001-м был записан новый студийник, "Aura", в создании которого кроме Даунса и Пэйна принял участие новый ударник – Крис Слэйд. 
