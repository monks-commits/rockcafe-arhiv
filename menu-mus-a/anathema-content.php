 "Не "Beatles" едиными славен Ливерпуль" – решили пятеро суровых английских парней летом 1990 года и под вывеской "Pagan angel" начали лабать свой дум-готик-метал. Звали их Винсент (р. 29 августа 1973) и Дэниел Каванаги (р. 6 октября 1972) (оба-гитара), Даррен Уайт (вокал), Дункан Паттерсон (бас) и Джон Дуглас (р. 11 июля 1973; ударные). Выпустив через полгода первую демку, они подумали, что название "Anathema" будет ближе по духу к исполняемой ими музыке, и заменили его. После выступлений на разогреве у "Bolt Thrower" и "Paradise Lost" у команды потихоньку начала формироваться своя армия фанов. За второй демкой, "All Faith Is Lost", последовал сингл "They Die". Этот релиз получил хорошие отклики в музыкальной андеграундной прессе и был быстренько распродан. 
<p>

Этот факт заметили представители "Peaceville Records" и предложили группе контракт. Фирма подсуетилась и немедленно выпустила в свет EP-шник "The Crestfallen". Тут как раз в Британию нагрянули "Cannibal corpse", и "Anathema" взялись открывать их концерты. Испытание прошло успешно и компанда приступила к записи своего дебютного альбома. 
<p>

 В инди-чартах журнала "Kerrang!" релиз занял вторую строчку, а "Metal hammer" обозвал его "альбомом месяца". После прокрутки на MTV клипа "Sweet tears" тамошняя редакция подтвердила оценку, данную "Хаммером". Первое глобальное европейское турне, включающее участие в различных фестивалях состоялось в 1994 году. А после записи мини-альбома "Pentecost III" команда отправилась покорять Бразилию. Перед записью второго лонгплея коллектив покинул Даррен Уайт. Чтобы не брать никого со стороны обязанности вокалиста взял на себя один из братьев Каванагов – Винни. Поменялась и музыка - мелодии стали помрачнее. Пластинка 1996 года "Eternity" получилась еще более мелодичной и атмосферной, за что заслужила похвалу со стороны многих критиков. Некоторые даже начали сравнивать "Anathema" с "Pink Floyd". 
<p>

Число фанов группы значительно увеличилось и музыканты решили, что настало время выпустить полноценное видео. Пленка "A vision of a dying embrace" включила в себя ранние демки, а также концерт в Кракове, когда группа разогревала "My dying bride". 
<p>

 В 1997 году команду покинул барабанщик Джон Дуглас, а ему на смену пришел Шаун Стилс. После альбома "Alternative 4" настал черед замены басиста. Несогласный с выбранным направлением группу оставил Паттерсон, поменявшийся местами с Дэйвом Пибасом из "Dreambreed". Примерно в то же время к "Anathema" присоединился клавишник Мартин Пауэлл (экс-"My dying bride"). Перестановки на этом не закончились, поскольку к концу года вернулся Дуглас. После выпуска "Judgement" состоялся новый обмен, теперь уже клавишниками с "Cradle of filth". Новым членом коллектива стал Лес Смит. Состав продолжало и дальше лихорадить, и незадолго до выхода альбома "A fine day to exit", видно следуя этому названию, объявил о своем уходе к "Крэдлам" Дэйв Пибас. 
<p>

Толковой замены ему сразу не нашлось, и на время тура басуху отдали давнему другу "Анафемы" Джорджу Робертсу. А в марте 2002-го разразился очередной кризис и один из братьев, Дэн ушел в "Antimatter". Однако он довольно быстро одумался и вскоре вернулся в строй.

