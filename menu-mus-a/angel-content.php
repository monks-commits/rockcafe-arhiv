<p>
 К работе над третьим диском был приглашен продюсер Эдди Крамер, до этого подвизавшийся у "Kiss". Пластинка имела более коммерческий саунд и поэтому надежды на большие продажи не оправдались. Чтобы как-то исправить дело был взят курс на доведение до совершенства сценического имиджа, теперь аппаратуру тоже старались представить в белых тонах, а для создания чего-нибудь необычного на концерты пригласили известного иллюзиониста Джонни Кона. Он придумал хитрый трюк с появлением и исчезновением музыкантов на сцене. Но все равно в стане "Angel" было неспокойно – басист рассорился с остальными членами команды, и его пришлось выгнать. 
<p>
Где-то в Калифорнии Джиуффриа подыскал ему замену в лице Фредерика "Феликса" Робинсона, восхищенный тем, что Феликс мог сыграть на любом инструменте, какой бы ему ни дали в руки. После длительных гастролей с ним был записан новый альбом, "White Hot", на котором был взят курс на дальнейшую коммерциализацию звучания. 
<p>
 "Casablanca Records" вбухивала в группу порядком денег и, соответсвенно хотела хоть какой-то отдачи. Поэтому музыкантов отправили в долгосрочное турне, продолжавшееся практически год. Несмотря на то, что на их концерты народ ходил достаточно активно, "White Hot" все же провалился и не достиг уровня ожидаемых продаж. В 1978-м была сделана попытка записать видео "ANGEL Live At Midnight", но этот проект так и остался нереализованным. Студийник "Sinful" также не принес успеха, равно как и концертник "Live Without A Net". Глядя на такое положение дел первым свалил Робинсон, а вслед за ним Франк Димино решил заняться сольной карьерой. После нескольких попыток заменить их "Angel" оказался распущенным. Музыканты занялись каждый своими делами, но в последующие годы время от времени предпринимали слабые попытки возродить группу. 
<p>
Самая успешная из них состоялась в 1998 году, когда Димино и Брандт использовали вывеску "Angel" в компании с гитаристом Ричи Рэем и басистом Лео Боррео, записав альбом "In the beginning". Кстати Мидоус и Робинсон тоже приняли участие в создании нескольких треков. На последующем туре Рэй и Боррео были заменены Стивом Блэйзом и Рэнди Греггом, а к команде присоединился клавишник Гордон Геберт.








 
