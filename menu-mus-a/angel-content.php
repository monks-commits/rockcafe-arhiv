Американская команда "Angel" была основана в 1975 году Панки Мидоусом (гитара), Франком Димино (вокал), Грегом Джиуффриа (клавишные), Мики Джонсом (бас) и Барри Брандтом (ударные). Как водится, свою карьеру группа начала с выступлений в различных клубах. На одном из таких выступлений случайно оказался Джин Симмонс из "Kiss", которому ребята настолько понравились, что он в 3 часа ночи позвонил президенту "Casablanca Records" с требованием заключить с "Ангелами" контракт. Тому ничего не оставалось делать, как последовать прихоти Симмонса, и "Angel" приступили к работе над дебютным альбомом. Релиз состоялся в октябре 1975-го и впоследствии был признан лучшей работой группы. Особой популярностью пользовалась композиция "Tower", которой коллектив долгое время открывал концерты. Музыкальный стиль команды журналисты окрестили как "гранд помп рок". 
<p>
Ко времени выхода второй пластинки был подобран и сценический имидж, когда музыканты одевались во все белое. После выхода в 1976-м "Helluva Band" "Ангелы" получили звание лучшей новой группы от американского рок-журнала "Circus", оставив позади "Heart" и "Boston". 
