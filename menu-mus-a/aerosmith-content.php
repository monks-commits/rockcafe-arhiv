        начинают 2001 год с выступления на Суперболе вместе с Эн-Синк, Бритни 
        Спирс и Нелли. Они не отказываются, но в тот же день увольняют всех менеджеров 
        и решают, что больше не потерпят вмешательства в свои дела. 
      <p>
Группа сама продюссирует новый альбом Just Push Play и выпускает его в марте 2001. Альбом быстро получает платину, сингл "Jaded" - седьмую позицию в Биллборде. Aerosmith принимают в Зал славы рок-н-ролла (они уже были номинированы в 2000). После вашингтонского концерта в поддержку жертв террактов 11 сентября группа отправляется в Индиану, чтобы продолжить выступления.
<p>
В следущем году Aerosmith довели тур до конца, выпустили сборник O, Yeah! на двух дисках, для которого записали песню "Girls of Summer", поучаствовали в нескольких программах на музканалах (стоит отметить получение награды MTV Icon) и снова отправились на гастроли.
<p>
В 2003 группа работала над блюзовым альбомом и вместе с Kiss разъезжала с концертами по Америке. Долгожданный Honkin' on Bobo (2004) был хорошо встречен в Соединённых Штатах и Европе, где возобновился интерес к блюзу. Альбом сопровождался концертным видео You Gotta Move, которое появилось на dvd в декабре 2004. В 2005 Стивен сыграл самого себя в комедии "Будь круче". Джо Перри выпустил сольный альбом, за который получил номинацию на Грэмми. Многие считают, что Joe Perry намного ближе к музыке Aerosmith 70-х, чем большинство недавних альбомов группы. В октябре 2005 вышел концертный cd/dvd под названием Rockin' the Joint, Aerosmith записали его на концерте в клубе "Joint" во время очередных гастролей. До окончания тура планировался ряд концертов, в том числе с другими музыкантами, но вместо этого началась настоящая чёрная полоса. Сначала были отменены выступления в нескольких городах, а потом и все остальные, "из-за болезни участника группы". Позже невнятное объяснение все-таки уточнили: из-за операции на голосовых связках, которую сделали Стивену Тайлеру. "Я не то что петь, говорить не мог," - вспоминает Стив. Aerosmith решили не терять время даром, а заняться материалом для нового альбома, которого их фэны уже заждались.
<p>
4 июля 2006 Тайлер и Перри выступают на концерте в честь Дня независимости и рассказывают о том, что в планах Aerosmith тур вместе с Motley Crue, который назвали Route of All Evil ("Путь всего зла"). Фэны припомнили, что Motley Crue баловались сатанинскими выходками и вообще любят беспредельничать на концертах. Вместо студийного альбома выходит очередной бест Devil's Got a New Disguise (буквально - "Дьявол принимает новый облик"), с черепушкой на обложке. Чёрный юмор, особенно в свете сообщения о временной замене Тома Хэмилтона на Дэвида Хилла (David Hull, был басистом в Joe Perry Project) из-за того что у первого обнаружен рак горла. Том вернулся 1 декабря, а 17 гастроли были закончены.
<p>
Пару месяцев назад журналисты поговаривали о конце группы, но Aerosmith не в первый раз выкарабкались. Даже неприятный инцидент с Джо Перри, который был сшиблен стрелой операторского крана и продолжил концерт с сотрясением мозга, не выбил группу из седла. Aerosmith обещают к осени выпустить 15-ый альбом, а перед этим навестить страны, где не были много лет или вообще ни разу. На очереди Латинская Америка и Европа, а потом Объединённые Арабские Эмираты и Индия. Уже объявлено, что на июль запланированы два концерта в России.
