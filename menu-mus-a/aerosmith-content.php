"Aerosmith" - одна из наиболее популярных американских хард-роковых групп, была сформирована в 1970 году.
После их первого концерта в "Nipmuc Regional High School", группа решила взять себе имя "Aerosmith".
Их популярность в окрестностях Бостона быстро росла.
Альбом "Toys In The Attic" стал их первым настоящим успехом (сейчас продано более 6 миллионов его экземпляров по всем миру).
Четвертый альбом, "Rock", достиг платинового статуса в течение нескольких месяцев со дня его выпуска. 
"Aerosmith" приняли участие в съемках фильма "Sgt. Pepper's Lonely Hearts Club Band".
<br>Диск "Permanent vacation" стал одним из наиболее пользующихся спросом альбомов группы, и первым, который имел успех в Англии. Диски "Pump" и "Get a grip" закрепили популярность команды. 
<br>Желая закрепить за собой имидж экстраординарной группы "аэросмитовцы" издали внушительный бокс-сет "Box of fire" на 13-ти компактах, включавший редкие и концертные записи. В набор также входила коробка спичек!
В сентябре 1998-го "Aerosmith" лидировал в американских хит-парадах со своей композицией "I Don't Want To Miss A Thing", остававшейся наверху в течение 4 недель, и также ставшей первой их вещью, попавшей в британский Топ 10 (4 место). В новом тысячелетии группа выпустила альбом "Just push play", сделанный в лучших "аэросмитовских" традициях. 

