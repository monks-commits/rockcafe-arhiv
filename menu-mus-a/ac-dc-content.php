<p>

Братья Ангус (Angus Young) (родился 31 марта 1955 года; по требованию компании Atlantic Records годом рождения Ангуса официально указывался неверный 1959), Малколм (Malcolm Young) (родился 6 января 1953 года) и Джордж Янг (George Young) родились в Глазго (Шотландия) и еще детьми вместе с семьей уехали в Сидней. Джордж начал играть на гитаре первым и стал членом самой успешной австралийской группы 60-х, The Easybeats. Это была первая местная рок-группа, которая выпустила хит международного масштаба — «Friday On My Mind» в 1966 году. Малколм и Ангус вскоре последовали по стопам брата. Они даже по инициативе Джорджа приняли участие в записи альбома его следующей группы.
<p>
Первые годы
<p>
Малколм (ритм-гитара) вначале играл в группе The Velvet Underground из Ньюкасла (Новый Южный Уэльс, Австралия) (эта группа не имеет ничего общего, кроме имени, с нью-йоркской группой The Velvet Underground). Но этот проект развалился, и в 1973 году он позвал к себе гитаристом в группу младшего брата — Ангуса, который уже получил первый музыкальный опыт в группе Tantrum. Дебют группы состоялся 31 декабря 1973 года в сиднейском баре. Братья играли вместе с вокалистом Дейвом Эвансом (Dave Evans), бас-гитаристом Ларри Ван Кредтом (Larry van Kriedt) и барабанщиком Колином Бургессом (Colin Burgess) (бывший участник The Masters Apprentices).
<p>
Первоначальный состав часто менялся — группа сменила нескольких барабанщиков и басистов в течение 1974 года. В сентябре 1974 года AC/DС заменили Дейва Эванса на харизматичного Бона Скотта (Ronald «Bon» Scott) (родился 9 июля 1946 года в Кирример, Шотландия), вокалиста группы The Spectors в 1966 году. С этого события начался настоящий успех группы. С Эвансом AC/DC записали сингл состоящий из трех песен «Rockin In The Parlour», «Show Business» и «Can I Sit Next To You Girl». «Show Business» и «Can I Sit Next To You Girl» также были записаны со Скоттом.
<p>
Сестра Ангуса Янга предложила ему надевать на концерты школьную форму, которую он носил в Высшей школе для мальчиков Эшфилд в Сиднее (Ashfield Boys High School). Эту форму он позднее надевал на всех концертах группы.
<p>
Благодаря постоянному участию в транслируемом по всей Австралии телевизионному шоу популярной музыки «Обратный отсчет» (Countdown), в период между 1974 и 1978 годами группа стала одной из наиболее известных и популярных в стране. AC/DC в эти годы выпустили ряд успешных альбомов и синглов, включая нестареющий рок-н-ролльный гимн «It’s A Long Way To The Top (If You Wanna Rock 'n' Roll)» (Долог путь на вершину (Если тебе нужен рок-н-ролл)).
<p>
Всемирная известность
<p>
Группа подписала международный контракт с Atlantic Records и стала активно гастролировать по Великобритании и Европе, добиваясь известности и набираясь опыта выступая на подхвате известных рок-групп того времени, таких как Alice Cooper, Black Sabbath, Kiss, Cheap Trick, Nazareth, Foreigner, Thin Lizzy и The Who. В 1976 году был издан третий австралийский альбом AC/DC Dirty Deeds Done Dirt Cheap.
<p>
Нашествие и волну популярности панк-рока 76-78 гг. группа благополучно пережила благодаря своим грубоватым и провокационным текстам песен и, частично, из-за того, что в британской музыкальной прессе того времени ее относили к панк группам. Они добились успеха на британской рок сцене благодаря своим мощным и скандальным концертным шоу, а Ангус Янг быстро стал знаменит из-за своего провокационного поведения на сцене, что, в том числе, привело к тому, что группе запретили выступать на нескольких британских концертных площадках.
<p>
Спродюсированный Маттом Лангом (Mutt Lange) альбом 1979 года «Highway To Hell» вознес группу на вершины мировых хит-парадов рок-музыки всех времен. Многие песни этого альбома до сих пор часто можно услышать на радио.
<p>
19 февраля 1980 года умер Бон Скотт. Он ушел с очередной вечеринки и остался на ночь в автомобиле своего знакомого Алистера Кинниара (Allistair Kinnear). Он и обнаружил Бона на следующий день мертвым. Причиной смерти официально стало переохлаждение, хотя наиболее распространенной версией и по сей день является то, что Бон Скотт захлебнулся собственными рвотными массами. Эти слухи подкрепляются множеством противоречий в официальной истории его смерти, что также порождает множество теорий о заговоре, убийстве музыканта и о передозировке героина.
<p>
Члены группы первоначально планировали прекратить свою музыкальную деятельность в составе AC/DC, но позже решили, что Бон Скотт хотел бы, чтобы группа продолжала существовать. Музыканты перепробовали несколько кандидатов на место вокалиста, в итоге осталось два претендента: Терри Слессер (Terry Slesser) и Брайан Джонсон (Brian Johnson). Джонсон в этот период пытался восстановить свою группу Geordie, но исполнение на публике двух песен AC/DC и Тины Тернер (Tina Turner) («Whole Lotta Rosie» (Let There Be Rock) и «Nutbush City Limits», соответственно) впечатлило участников AC/DC и через несколько дней они сообщили Джонсону, что он новый вокалист группы.
<p>
Совместно с Брайаном Джонсоном группа дописала незавершенные из-за смерти Бона песни, и записала альбом «Back in Black», также спродюсированный Лангом. «Back in Black», выпущенный в 1980 году, стал самым продаваемым альбомом группы и одним из самых значительных в истории хард-рока. Среди всех хитов альбома, одноименная с названием альбома песня, написанная в память о Боне Скотте, и «You Shook Me All Night Long», многими считается квинтэссенцией музыки AC/DC и, даже, хард-рока в целом.
<p>
Следующий альбом, «For Those About to Rock We Salute You», выпущенный в 1981 году также очень хорошо продавался и был хорошо принят критиками. Одноименная альбому композиция, заканчивающаяся под гром палящих пушек, стала кульминационным и завершающим номером большинства последующих концертов AC/DC.
<p>
Альбом «Flick of the Switch» 1983 года группа продюсировала без Ланга. Барабанщик Фил Радд из-за личных разногласий с остальными членами группы, вызванных, по некоторым данным, проблемами с алкоголем, покинул группу. На его место после анонимного прослушивания взяли Саймона Райта (Simon Wright), бывшего члена группы Tytan. В 1985 году в новом составе группа записала менее успешный альбом «Fly on the Wall», спродюсированный братьями Янгами. Вместе с этим альбомом группа выпустила серию музыкального видео группы, исполняющей пять из десяти песен альбома в баре, с использованием различных спецэффектов, включая анимированную муху.
<p>
В 1986 году AC/DC вернулись в хит-парады с заглавной песней альбома «Who Made Who», являющемся саундтреком к фильму Стивена Кинга Максимальное ускорение (Maximum Overdrive). Альбом также содержал две новых инструментальных композиции и хиты из предыдущих альбомов. В феврале 1986 года группа была принята в Зал Славы Австралийской Ассоциации звукозаписывающей индустрии (Australian Record Industry Association Hall of Fame). Альбом 1988 года «Blow Up Your Video» группа выпустила вместе с первоначальным составом продюсеров, Гарри Вандой (Harry Vanda) и Джорджем Янгом. Этот альбом продавался лучше, чем предыдущий и попал в британский хит-парад двадцати лучших синглов с песней «Heatseeker».
<p>
После выхода Blow Up Your Video из группы ушел Райт и был заменен на сессионного музыканта Криса Слейда (Chris Slade). Джонсон не мог участвовать в работе группы несколько месяцев, поэтому братья Янги написали песни для следующего альбома самостоятельно, как и для всех последующих. В 1990 году вышел альбом The Razor's Edge. Он стал очень успешным для группы и содержал хиты «Thunderstruck» и «Money Talks». Альбом стал мультиплатиновым, вошел в десятку хит-парада США (2-е место) и двадцатку синглов в Великобритании.
<p>
 
Обложка альбома «Ballbreaker»В 1994 году в группу вернулся Фил Радд. Уход Криса Слейда, в этой связи, был дружественным и произошел, в основном, из-за сильного желания членов группы вернуть Радда. По мнению Ангуса Янга, Слейд был лучшим музыкантом в AC/DC, но желание увидеть в группе Фила было сильнее. В составе 1980—1983 годов группа записала в 1995 году альбом «Ballbreaker» с продюсером хип-хоп и хэви-метал групп Риком Рубином (Rick Rubin) и «Stiff Upper Lip» в 2000 году.
<p>
После выхода этих альбомов группа подписала долгосрочный контракт на несколько альбомов с Sony BMG, которые стали выходить под лейблом Epic Records.
<p>
Последние годы и признание заслуг
<p>
В марте 2003 года группа AC/DC была принята в Зал славы рок-н-ролла в Нью-Йорке и исполнила свои хиты «Highway To Hell» и «You Shook Me All Night Long» совместно со Стивом Тайлером из Aerosmith. В мае 2003 года Малколму Янгу была присуждена награда Теда Альберта (Ted Albert Award) за «выдающийся вклад в австралийскую музыку». В том же году, Ассоциация звукозаписывающей индустрии Америки (Recording Industry Association of America, RIAA) обновила расчеты количества продаж альбомов группы с 46,5 млн. копий до 63 млн., что сделало AC/DC пятой группой в истории США, продавшей наибольшее количество альбомов после The Beatles, Led Zeppelin, Pink Floyd и Eagles. Кроме того, был удостоверен «дважды бриллиантовый» (20.000.000 проданных копий) статус альбома Back in Black, что сделало его шестым в списке самых продаваемых альбомов в истории США. В 2005 году количество проданных копий альбома достигло 21 миллиона, что вывело его на пятую позицию.
<p>
В июле 2003 года группа провела совместный концерт с Rolling Stones на Сарсфесте (Sarsfest), концерте, посвященном борьбе с эпидемией SARS в Торонто в Канаде.
<p>
1-го октября 2004 года улица Корпорейшн Лэйн (Corporation Lane) в Мельбурне была официально переименована в ACDC Лейн (ACDC Lane) в честь группы (названия улиц в Мельбурне не могут содержать символ «/»). Улица находится рядом со Свонсон Стрит (Swanston Street), местом где, в кузове грузовика, группа записала свой видеоклип для хита 1975 года «It’s a Long Way to the Top». Также есть еще одна улица в мире, названная в честь группы AC/DC, в Испании, в городе Легане (Leganes), рядом с Мадридом — «Calle de AC/DC», недалеко от улиц названных в честь рок-групп Iron Maiden и Rosendo (испанская рок-группа).
<p>
В марте 2005 года вышел набор из двух DVD дисков, «Family Jewels», содержащий музыкальное видео и клипы с концертов. Первый диск относится к эре Бона Скотта (с концертными видеозаписями, снятыми за десять дней до смерти Скотта), второй содержал видеоматериалы эры Брайана Джонсона.
