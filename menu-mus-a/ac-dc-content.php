Эта австралийская хард-роковая команда была сформирована в 1973 году Малколмом Янгом (р. 6 января 1953, Глазго, Шотландия; ритм-гитара) после развала его предыдущего проекта "Velvet underground" (не имеет никакого отношения к группе из США).
Первые два альбома - "High voltage" и "TNT". 
Оба диска были спродюсированы Джорджем Янгом и его партнером Гарри Вандом. 
Ни один из дисков не был выпущен вне Австралии, хотя английская "Atlantic Records" предлагала выпустить материал из обоих альбомов под названием "High voltage" в 1976 году. 
Это сделало группу хорошо известной не только на родине, но и привлекли внимание в Европе. В январе 1976-го "АС/DC" перебрались в Лондон.
"AC/DC", однажды начав гастролировать вне Австралии, быстро создала себе культ своими заводными шоу. С "Let There Be Rock" они прорвались в чарты в Великобритании. 
<br>Но международные звездами они стали с альбомом "Highway to hell". 
В 1981 году "For Those About To Rock (We Salute You)" возглавляла американские чарты в течение трех недель, группа выступала хедлайнерами на фестивале в Донингтоне, а также два их сингла попали в английскую двадцатку. 
Два попадания их синглов в Британскую двадцатку (1986 и 1988) подтверждали их высокую популярность. В 1990 году "The Razor's Edge" стал одним из наиболее успешных альбомов их поздней карьеры, попав в британскую двадцатку с композицией "Thunderstruck" и достигнув № 2 в чартах США. 

