ABBA - Легендарная шведская диско-группа.
<br>
<br>ABBA Была сравнима по свей популярности с Битлз.
<br>
<br>Название ансамбля ABBA — это акроном их первых имен Agnetha, Bjorn, Benny, Anni-Fred. 

<p>
Основателями группы ABBA были музыканты, певцы и авторы песен Бьёрн Ульвеус и Бенни Андерссон. Впервые они встретились на вечеринке в Вастервике летом 1966 года, где решили, что им следует сочинять песни вместе. Бенни был в тот момент клавишником популярной в Швеции группы Hep stars, Бьёрн певцом и гитаристом в ансамбле Hootenanne Singers. На концерте в Мальмё Бенни познакомился с певицей Анни-Фрид Люнгстад, которая с тринадцати лет пела с разными группами и даже выступала на песенных фестивалях в Японии и Венесуэле. Тогда же Бьёрн услышал по радио, как поёт собственную песню I Was So In Love Агнета Фэльскуг, и решил её пригласить в группу.


<p>
Впервые вся четвёрка собралась для записи телепрограммы в Стокгольме, а петь начала вместе с ноября 1970 года. Одновременно с дебютом квартета, в одном из ресторанов Гётеборга (ранее каждый делал сольную карьеру), в конце года Бьёрн и Бенни записали собственный альбом, в котором Агнета и Фрида участвовали как бэк-вокалистки. Фирма Polar издала диск Lycka с песнями на шведском языке, а сингл People Need Love, вышел в США на фирме Playboy Records. В 1971 году Бенни и Бьёрн поступили на работу на фирму Polar уже в качестве продюсеров. Трагический уход из жизни Бенгта Бернхага - ближайшего друга и соратника главы Polar Стига Андерсона, привёл на освободившеесе место продюсера Бьёрна Ульвеуса. Стиг предложил молодому автору эту должность, но Бьёрна это не совсем устраивало. Он был согласен при условии, если на работу примут и его соавтора - Бенни Андерссона. Зарплаты на двоих у главы компании не было, и начинающим авторам пришлось работать на полставки.


