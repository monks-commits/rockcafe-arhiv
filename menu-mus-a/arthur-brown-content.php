Этот очень интересный певец с безошибочно узнаваемым голосом организовал свою первую группу в 1964 году, когда учился на философском факультете университета в Рединге. 
Blues And Brown - так называлась эта группа - исполняла стилизованный ритм-энд-блюз и явно находилась под влиянием The Beatles и Kinks. 
<p>
Дебют Blues And Brown состоялся в 1965 году.
В 1966 году он уехал в Париж и изучал там актерское мастерство - именно в Париже Браун нашел свой сценический имидж и разработал главные элементы шоу, которое спустя год потрясло Лондон.
<p>
В 1967 году Артур Браун вернулся в Лондон и собрал первую инкарнацию знаменитой группы The Crazy World Of Arthur Brown.
The Crazy World Of Arthur Brown стала одной из первых групп английского андеграунда - прогремевшее на весь мир шоу начиналось с того, что на сцене появлялся лидер группы с пылающей шевелюрой, а дальше начиналось нечто невообразимое - танцы, взрывы, потоки воды, в общем, так рождался театрализованный рок.
<p>
Популярность The Crazy World Of Arthur Brown стремительно росла и в 1968 году Питер Тауншенд помог Брауну подписать контракт с фирмой Track, на которой записывались The Who. Первый же сингл группы Артура Брауна «Fire» стал английским хитом №1. 

