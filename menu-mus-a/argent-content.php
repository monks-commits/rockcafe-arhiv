<p>
 В феврале 1971-го вышел второй альбом группы, "Ring Of Hands", в котором ее звучание приобрело индивидуальность: ее технически совершенный и изящный арт-рок был близок к музыке "ELP", "Genesis" и "Yes", сохраняя при этом мелодическую свежесть баллад "Zombies". Популярность "Argent" в этот период достигла своего пика. К сожалению, уже на следующем диске, "All Together Now" 1972 года участники "Argent" стали играть длинные сольные фрагменты. Правда, той же весной они сделали свой первый собственный хит: песня "Hold Your Head Up" стала 5-й в Англии и США. Вышедший в марте 1973-го альбом "In Deep" подтвердил опасения, высказанные наиболее прозорливыми критиками: группа все сильнее заостряла внимание не на творчестве, а на своих амбициях. 
<p>
В марте 1973-го "Argent" отметились в британских чартах с композицией "God Gave Rock'n'Roll To You" (№ 18), однако это было их последним достойным результатом. В феврале 1974-го сразу после выхода еще одного альбома, "Nexus", Баллард решил уйти. Заменой ему стал гитарист Джон Гримальди (p. 25 мая 1955). Также в команде появился второй гитарист и певец Джон Верити (р. 2 мая 1944), группа которого "John Verity Band" разогревала зал перед выступлениями "Argent" во время их последнего британского тура. В конце 1974 года появился двойной альбом "Encore", включавший концертные версии лучших номеров группы. За ним весной 1975-го последовал неудачный "Circus", после чего "Argent" расстались с "Epic" и перешли на небольшой лейбл "Good Earth". 
<p>
 Надеясь вернуть утраченные позиции, группа ушла в девятимесячную самоизоляцию, сочиняя материал нового альбома, но когда в октябре 1975-го вышел "Counterpoint", стало ясно, что чуда не произошло. В июне следующего года Гримальди объявил о своем уходе, а Род Арджент принял решение распустить команду. В том же году Гримальди организовал проект "Cheap flights", а Родфорд, Верити и Хенрит еще раз попытали счастья под вывеской "Phoenix". Верити впоследствии вернулся к собственным планам, а Родфорд и Хенрит вновь встретились в "Kinks". В дальнейшем Арджент работал как сессионный музыкант, сочинял музыку для театра и кино. В июне 1978-го он записал инструментальную пьесу "Argentine Melody", которая стала хитом в Британии после того, как была использована в качестве лейтмотива в телерепортажах с чемпионата мира по футболу в Буэнос-Айресе. Он выступал и как продюсер, а позднее открыл в Вест-Энде комиссионный магазин клавишных инструментов. 
<p>
Арджент принимал участие в ряде постановок Эндрю Ллойд Уэббера и записи "Variations" в 1978-м. В том же году он сделал на "MCA" сольный альбом "Moving Home". В 1983-84 Род исполнил композиции, которые вошли в два альбома известной джазовой саксофонистки Барбары Томпсон, а также записывался с "Who". В 1985-м оригинальный состав "Argent" собрался, чтобы принять участие в благотворительном концерте, посвященном жертвам пожара в футбольном клубе Брэдфорда. В 1988-м свою версию "God Gave Rock'n'Roll To You" сделали музыканты "Truth", а в 1992-м, спустя почти двадцать лет, песня вернулась в чарты в исполнении "Kiss", которые спели ее в популярной киноленте "Bill And Ted's Bogus Journey" 
