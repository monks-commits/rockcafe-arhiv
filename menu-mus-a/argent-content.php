Эта английская арт-роковая команда образовалась в первой половине 70-х годов. Своим названием, "Argent", группа была обязана фамилии своего лидера - клавишника, автора песен и певца Рода Арджента. Первые шаги к славе Арджент (p. 14 июня 1945, Сент-Олбенс) сделал еще в 1964 году как участник "Zombies", первая же записанная на сингле песня которых, "She's Not There", была лишь второй из сочиненных им и принесла группе известность. Разногласия с фирмой "Decca" ускорили печальный конец "Zombies", и летом 1969-го Арджент собрал в Лондоне новую команду, в состав которой кроме него вошли его двоюродный брат Джим Родфорд (p. 7 июля 1945, Сент-Олбенс; бас) и два бывших члена поп-группы "Unit 4+2" - Расс Баллард (p. 31 октября 1947, Уолтэм Кросс; гитара, вокал) и Боб Хенрит (p. 2 мая 1946, Броксбурн; ударные). Тогда же он вместе с коллегой по "Zombies" Крисом Уайтом открыл свою продюсерско-авторскую фирму "Nexus Production". "Argent" получили контракт от "Epic" и выпустили в январе 1970-го дебютный альбом. 
<p>
Присутствующие на диске эфирные мелодии и импрессионистские тексты напоминали "Odessey And Oracle". Пластинке не довелось оказаться в чартах, хотя песня Балларда "Liar" в исполнении "Three dog night" попала в августе 1971-го в штатовский Топ 10, заняв седьмое место. В 1970-м "Argent" предприняли первое американское турне, выступая в наиболее престижных и крупных залах страны. 

