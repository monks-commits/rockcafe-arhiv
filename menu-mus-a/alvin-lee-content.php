Группа образована в 1967 году в Великобритании.
<br>Хард-роковая группа "с блюзовым уклоном", фирменным знаком которой были длинные гитарные соло Элвина Ли. 
<br>Их интерпретация "Goin' Home" была одним из самых запоминающихся моментов фестиваля в Вудстоке (1969 год) и посвященного ему фильма. 
<br>В 1974 году Ли создал собственную группу Alvin Lee And Company In Flight, а Ten Years After записали свой последний альбом "Vibrations" и распались.

