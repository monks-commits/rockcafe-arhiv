Эндрю Ллойд-Уэббер родился 22 марта 1948 года в семье композитора Уильяма Ллойд-Уэббера и учительницы игры на фортепиано Джин Джонстоун Ллойд-Уэббер. Первый успех пришел к нему в возрасте 19 лет, когда он и Тим Райс написали оперу Иосиф и его удивительный разноцветный плащ (англ. Joseph and the Amazing Technicolor Dreamcoat) для школьной постановки. Уэббер и Тим Райс, продолжая работать вместе, написали оперы Иисус Христос — суперзвезда (Jesus Christ Superstar) (1970) и Эвита (Evita) (1976). Творческие пути Райса и Уэббера вскоре разошлись.
<p>
В 1981 Уэббер написал мюзикл Кошки (Cats) на слова нобелевского лауреата Т.С. Элиота. Через два года женился на оперной певице Саре Брайтман, которая исполнила главную партию в его новом мюзикле — Призрак оперы. В последущие годы Уэббер создал ещё несколько мюзиклов с большим успехом поставленных на Бродвее. По мотивом мюзиклов Уэббера было снято несколько фильмов, причем главную роль в экранизации Эвиты сыграла Мадонна. В 1997 г. был пожалован королевой в рыцари, через пару лет ему был дарован титул барона. По оценкам воскресного приложения к The Times, состояние композитора перевалило за 700 миллионов фунтов стерлингов; он входит в сотню самых состоятельных жителей Великобритании.
