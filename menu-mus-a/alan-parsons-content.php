      Алан Парсонс (р. 20 декабря 1949) добился высокой репутации студийного мастера 
      совместной деятельностью с самыми выдающимися исполнителями на рубеже 60-70-х 
      годов. Свою карьеру в студии он начал, когда <b>"Beatles"</b> взялись за 
      работу над альбомом "Abbey road". 
      <p>

В 1971 году Алан появился в качестве звукорежиссера у <b>Пола Маккартни</b> на записи "Wild Life" и "Red Rose Speedway", однако свое настоящее "боевое крещение" получил некоторое время спустя, работая вместе с <b>"Pink Floyd"</b> над легендарным диском "Dark Side Of The Moon". 
<p>
Он сотрудничал с Аланом Стюартом, Джоном Майлзом и группой <b>"Hollies"</b>, а также работал в американских студиях. 
Там же и родились два его первых сольных альбома. На пластинке "Tales Of Mystery And Imagination Edgar Allan Poe" Парсонс сделал попытку перевода на язык симфонического рока мрачной и загадочной прозы Эдгара По. 
<br>Парсонс стал первым музыкантом, пробившим барьер отождествления у широкой публики творца роковой музыки с обаятельным и процветающим певцом или инструменталистом.
Он показал, что на первом плане может находиться и человек в наушниках, сидящий в студии среди огромного количества приборов.
