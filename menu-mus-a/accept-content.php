	<tr><td  colspan=2 valign=top class=menu1><a href="http://www.rock-cafe.dp.ua/" target="_blank"><img src="accept.jpg" alt="<? echo $nam; ?> photo" width=202 height=150 hspace=10 vspace=3 border=0 align=left></a>

Это одна из немногих тяжелых немецких групп, которая в первой половине 80-х годов добилась как национального, 
так и международного признания. Как идея "Accept" зародилась еще в 1972 году. В конце 70-х группа обрела относительно 
стабильный состав, твердую ориентацию на тяжелый рок и зачатки собственного репертуара. В нее вошли Удо Диркшнайдер 
(p. 1952; вокал), Вольф Хоффманн (p. 1960; гитара), Герхард Вахл (гитара), Петер Балтес (p. 1959; бас) 
и Франк Фридрих (ударные).
<p>
В первые годы своего существавания Accept время от времени выступали на рок-фестивалях, в группе постоянно менялся состав. Их профессиональная карьера началась в 1976 году, когда они были приглашены выступать на известном рок-фестивале Rock am Rhein (нем. Рок на Рейне). После участия в фествиале группа получила предложение от профессиональной студии звукозаписи для записи первого альбома, который так же был назван «Accept».

