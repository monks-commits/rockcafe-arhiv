<p>
В первый стабильный состав группы вошли Udo Dirkschneider (вокал), Peter Baltes (бас), Франк Фридрих (ударные), Wolf Hoffmann и Герхард Валь (гитаристы). После выхода альбома «Accept» Фридрих и Валь покинули группу, так как они не планировали стать профессиональными музыкантами. Они были заменены Stefan Kaufman и Йоргом Фишером, и в таком составе группа записала альбом «I’m a Rebel». Этот альбом привлёк внимание прессы к группе, и Accept впервые были приглашены на телевизионное выступление.
<p>
В 1981 году в жизни группы произошло три важных события. Во-первых, вышел третий альбом — «Breaker». Во-вторых, был подписан контракт с менеджером Габи Хауке. И в-третьих, Accept присоединились к мировому турне группы Judas Priest, благодаря чему стали известными за пределами Европы.
<p>
В середине 1980-х Peter, Wolf и Stefan стали всё более интересоваться американской публикой, большую часть времени проводили в США, а не в Германии. Udo начал задумываться о сольной карьере и в 1987 году издал с другими музыкантами альбом «Animal House». Новая группа получила навание U.D.O.
<p>
Через некоторое время Peter, Wolf и Stefan решили вернуться к делам группы, для чего обратились за помощью к вокалисту Дэвиду Рису. Группа записала в 1989 году альбом «Eat the Heat», состав её снова стал нестабильным, и в том же году Hoffmann, Baltes и Хауке приняли решение о прекращении существования группы.
<p>
В начале 90-х Udo встретился с музыкантами Accept, и они решили, что нужно восстановить группу в классическом составе. После этого группа выпустила три студийных альбома, дала несколько концертов, но музыканты вскоре почувствовали, что устали. С 1994 в связи с проблемами со спиной место ударника занимает Stefan Schwarzmann ранее игравший в U.D.O.. В 1997 году группа вновь была расформирована.
<p>
В 2005 году группа вновь собралась в классическом составе (за ударной установкой находился Stefan Schwarzmann, Stefan Kaufman - гитара) и провела годовое мировое турне.



