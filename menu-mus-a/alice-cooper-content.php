<b>Элис Купер (Alice Cooper)</b> чье настоящее имя Винсен Дэймон Фурние (Vincent Damon Furnier) - пожалуй, самый старый рокер (сейчас ему уже за 50!) из ныне здравствующих и, главное, продолжающих работать музыкантов. 
Не удивительно, что на концерты Элиса Купера уже ходят целыми семьями, ведь начинал он свою карьеру - невозможно представить - в 1964 году! 
Не сложно догадаться, что толчком к созданию своей первой группы для 16-летнего Винни Фурние послужило творчество THE BEATLES, которые впервые посетили США как раз в 64-м.
В Детройте в 1964 году появилась группа, делающая пародии на "битловские" песни - THE EARWIGS.
В 1965 году группа переименовалась в THE SPIDERS, а затем в 67-м выпустила два сингла и переименовались в THE NAZZ.
Наконец, 16 марта 1968 года квинтет впервые выступил под именем ALICE COOPER, которое Фурние взял себе в качестве псевдонима и не расстается с ним и по сей день.

<p>

Первый альбом ALICE COOPER "Pretties For You" был записан на студии Фрэнка Заппы (Frank Zappa).
Первый настоящий успех пришел к ALICE COOPER в 1971 году с выходом третьего LP "Love It To Death".
Летом 1972 года вышел пятый альбом "School's Out' занявший в Америке второе место, а в Англии - четвертое а одноименный сингл там (в Англии) попал на самую вершину хитпарада!
Но настоящим труимфом стал следующий лонгплей "Billion Dollar Babies", возглавивший списки популярных пластинок по обе стороны Атлантики.
Купер исполнил партию Короля Ирода в очередной версии вебберовского "Jesus Christ Superstar".
Наконец, в 2000 году у ALICE COOPER вышел новый, крайне тяжелый альбом "Brutal Planet".
Остается добавить, что помимо своей музыкальной деятельности, Винсент Фурние снялся в полутора десятках фильмов, а также создал целый развлекательный комплекс имени своего неизменного спутника - Элиса Купера.


