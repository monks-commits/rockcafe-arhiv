Группа была основана в 1987 году, когда Джери Кантрел и Лейн Стейли репетировали в "The Music Bank". Вскоре к ним присоединились друзья Джери, басист Майк Старр и барабанщик Шон Кинни. Название группы менялось от Alice'n'Chains к Diamond Lie и наконец к Alice in Chains. У вокалиста Лейна Стейли какое-то время была идея назвать группу Fuck. Название Alice in Chains отражает ироническое отношение музыкантов к ортодоксальным металистам, которые украшали свою одежду множеством цепей.
<p>
Поначалу группа играла в местных сиэттлских клубах. Самым громким было выступление в Central Tavern, где Alice in Chains играли вместе с Mother Love Bone. К группе стали проявлять интерес звукозаписывающие компании и в 1989 году музыканты заключили договор с Columbia Records.
<p>
Первым альбомом Alice in Chains стал "Facelift", который вышел в августе 1990 года. В нем музыканты ориентируются прежде всего на хэви-метал с риффами в духе Van Halen. Диск получил статус золотого.
<p>
В том же году на празднике Хеллоуин было сделано концертное видео, получившее название Live Facelift.
<p>
В целом период 1987 — 1992 можно охарактеризовать как самый металлический в истории группы.

