<p>

После выхода первого альбома и совместного тура со знаменитыми металлическими командами группа стала известной в Сиэттле и за его пределами. В 1991 году Alice in Chains записывают мини-альбом Sap, в котором приняли участие Soundgarden и Mudhoney. Альбом получил хорошие отзывы, а песню "Got Me Wrong" включили в саундтрек к фильму "Clercs".
<p>
В 1992 году выходит фильм Камерона Кроу "Singles", в котром снялись участники Pearl Jam, Soundgarden и Alice in Chains. Alice in Chains исполняют песни "Ain't like That" и "Would?", которая станет заглавным синглом их второго полноформатного альбома.
<p>
Работа над "Dirt" началась в апреле 1992 года и закончилась осенью того же года. Альбом стал трижды платиновым. В этот же период появились вполне справедливые слухи о том, что лидер Лейн Стейли увлекается героином. На альбоме есть ряд песен ("Angry Chair", "Junkhead", "Godsmack"), в которых Стейли описывает свои ощущения после потребления наркотиков. Тексты же Кантрела ("Them Bones", "Damn That River") имеет более традиционную для метала окраску.
<p>
Тем не менее группа добилась ошеломительного успеха, её клипы находились в постоянной ротации на MTV, а песни проигрывали все металические радиостанции США. В интервью Rolling Stone Ларс Ульрих, барабанщик Metallica, поставил Alice in Chains в один ряд с Metallica и Motorhead.
<p>
Во время тура в поддержку "Dirt" басист Майк Старр не выдерживает плотного гастрольного графика и уходит из группы в Sun Red Sun. На его место вскоре пришел басист из групы Оззи Осборна Майк Инез. В новом составе группа записала две песни для саундтрека к фильму Арнольда Шварценеггера "Последний киногерой": "What the Hell Have I" и "A Little Bitter". В 1993 году группа выступила с такими командами как Primus, Tool, Rage Against the Machine, Babes in Toyland на фестивале альтернативной музыки Lollapalooza. Это был последний совместный тур Alice in Chains.
<p>
После фестиваля музыканты решают записать акустический альбом. Первоначальное название этого EP — "Seven Days", т. к. он был записан за семь дней, однако потом его переименовали в "Jar of Flies". На этом альбоме представлены только медленные, акустические номера. После выхода EP занимает 1-ое место в чартах, это был 1-ый EP в истории, сумевший подняться до такой позиции. После выхода альбома Alice in Chains должны были выступать в туре с Metallica, но отказались, породив слухи, что в группе существуют проблемы из-за наркотиков.

<p>
Все участники Alice in Chains занимались своими делами. Mike Inez присоединился к группе Slash's Snakepit и записывал с ними их альбом "It's 5 O'clock Somewhere". Layne Staley дал несколько концертов в качестве вокалиста группы "The Gacy Bunch". Это сиэтлская супергруппа, включившая в свой состав участника Pearl Jam Mike McCready, участника The Screaming Trees Barrett Martin и John Saunders. Группа впоследствии переименовала себя в Mad Season и выпустила альбом “Above” в начале 1995 года. К маю того же года их альбом достиг золотого статуса.
<p>
В апреле 1995 года группа снова собралась вместе, чтобы записать 3-ий LP. Продюсером был выбран Тоби Райт, работавший вместе со Slayer. Первоначально альбом назывался Tripod, потому что это было символом третьего альбома, а также потому, что в детстве барабанщика Шона Кини преследовала трёхногая собака, но потом название альбома было сменено на Alice in Chains. Кстати, трёхногую собаку, изображенную на обложке альбома, зовут Tripod. На этом альбоме группа возвратилась к своим металическим корням, однако были и акустические номера ("Over Now", "Heaven Beside You"). Первым синглом к альбому стал "Grind". Вместе с альбомом выпускается домашнее видео "Nona Tapes".
<p>
Альбом достигает сначала золотого, потом платинового, а потом и дважды платинового статуса. Но, как и в случае с "Jar of Flies", музыканты не смогли выступить с туром в поддержку альбома.
<p>
Концерт в Brooklyn Academy of Music's Majestic Theater был одним из последних выступлений группы. Запись концерта проходила в рамках проекта MTV Unplugged. Приглашенным гитаристом выступил Скотт Олсон. Было заметно, что вокалист Стейли ослаб: пропускал свои вокальные партии, пел тихим голосом, а на "Sludge Factory" вообще сбился с темпа. Тем не менее именно он получил больше всего аплодисментов от публики. Летом 1996 года был выпущен альбом и концертное видео. Через месяц альбом получает статус золотого.
<p>
 Смерть Лейна Стейли
<p>
После концерта на MTV Alice in Chains открыли несколько шоу Kiss. После этого группа в классическом составе концертов не давала. В группе усиливались разногласия в связи с наркотической зависимостью Лейна Стейли. Кантрел пытался сохранить творческую атмосферу в группе, но убедить вокалиста работать ему так и не удалось. В итоге он записал cольный альбом "Boggy Depot", на котором присутствовали Инез и Кини. Этот альбом иногда называют "потерянным" альбомом Alice in Chains. Продюсировал эту запись Тоби Райт.
<p>
В конце десятилетия Columbia Records выпускает сборник лучших песен, живых выступлений и демо "The Music Box", на котором можно найти две новых песни: "Get Born Again" и "Died".
<p>
До 2002 года в истории группы ничего примечательного не происходило, пока 19 апреля не был найден труп Лейна Стейли в его доме. До этого, как отмечают свидетели, музыкант занимался только тем, что сидел в соседнем баре, ни с кем не разговаривая. Говорят, что на него произвела большое впечатление смерть его бывшей девушки. Тело Стэйли было найдено 19 апреля, и медицинская экспертиза установила, что смерть наступила 5 апреля, кстати, в тот же самый день 8 лет назад застрелился Курт Кобейн. Продолжая связь между лидерами Nirvana и Alice in Chains можно сказать, что они занимали одну и ту же комнату в центре реабилитации.
<p>
Экспертиза установила, что музыкант умер от смеси кокаина и героина (speedball). В одном интервью он высказался отрицательно по поводу наркотиков: "Почему люди принимают наркотики? Неужели они не слышат мою музыку? Неужели они не понимают слова?" К сожалению, побороть роковую зависимость так и не смог.

<p>
2005-
<p>
В 2005 году Кантрелл, Инез и Кини решили провести благотворительный концерт для жертв цунами, прошедшего в Юго-Восточной Азии. Ведущим вокалистом был взят Пэт Ланчман, а на должность дополнительных были приглашены Вес Скантлин из P.O.D., Мэйнард Джеймс и Энн Уиллсон из Heart. Сейчас на должности ведущего вокалиста состоит Вильям ДуВаль. По признанию участников коллектива, данное воссоединение было сделано лишь для того, чтобы дать несколько концертов. Новых песен под маркой "Alice in Chains" производится не будет. Как сказали музыканты, дальнейшее творческое движение возможно только под другим названием.
