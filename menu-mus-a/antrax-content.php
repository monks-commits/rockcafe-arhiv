<div align=justify>

Эта нью-йоркская трэшевая команда была основана в 1982 году. 
Квинтет выпустил в 1984 году первый диск "Fistful of metal".
Диск "Spreading The Disease" по достоинству был оценен слушателями, а турне вместе с <b>"Металликой"</b> принесло "Антраксу" известность в Европе. 

<p>
С выходом альбома "Among The Living", спродюсированного Эдди Крамером, "Anthrax" становится самой популярной спид-метал-группой. Их композиции "I Am The Law" и "Indians" заняли верхние строчки британских чартов, а неимоверно интересные "живые" шоу сделали любимцами прессы и публики. 
Вышедший в 1991 году сборник "Attack of the killer b's" стал одним из наиболее популярных альбомов "Anthrax", а больше всего публике нравилась записанная вместе с "Public enemy" композиция "Bring The Noise". 
<p>
В первый состав входили: Нил Турбин (вокал), Дэнни Лилкер (бас-гитара), Чарли Бенанте (ударные), Дэн Спитц (гитара), Скотт Иэн (гитара). Первый альбом «Fistful Of Metal» был записан в 1984 году и издан лейблом Megaforce Records. После выхода дебютного альбома Дэнни Лилкера сменяет Фрэнк Белло, а Нила Турбина - Джоуи Белладонна.
<p>
Записанный в обновлённом составе EP «Armed And Dangerous», позволил заключить Anthrax новый контракт со звукозаписывающей компанией Island Records. Следующие два альбома «Spreading The Disease» (1985) и «Among The Living» (1987) сделали Anthraх одной из самых известных групп, играющих в стиле трэш-метал.
<p>
После выхода «State Of Euphoria» (1988) и «Persistence Of Time» (1990) группу покидает Джоуи Белладонна, а после альбома «Sound Of White Noise» (1993) - Дэн Спиц. «Sound Of White Noise» записывался с участием Джона Буша, заменивший Джоуи Белладонну. Следующий альбом «Stomp 442» (1995) Anthrax записали с участием гитариста Даймбэга Даррела из группы Pantera.

