Бесспорно наиболее популярное явление в рок-музыке 80-х в Англии, ирландская группа "U-2" начала свою музыкальную карьеру в школе в Дублине 1977 году. Боно (Пол Дэвид Хьюсон, р. 10 мая 1960, Дублин, Ирландия; вокал), Эдж (Дэвид Эванс, р. 8 августа 1961, Баркинг, Эссекс; гитара), Адам Клейтон (р. 13 марта 1960, Чиннор, Англия; бас) и Лэрри Маллен младший (Лоренс Маллен, р. 31 октября 1961, Дублин, Ирландия; ударные) поначалу исполняли кавер-версии "Rolling stones" и "Beach boys" в команде под названием "Feedback". Позже они сменили название на "Hype" а в 1978 году окончательно остановились на "U-2". Под руководством менеджера Пола Макгиннесса группа подписала контракт с ирландской "CBS records". За их дебютным EP "U2:3" последовал сингл "Out Of Control" (1979), который занял 1-ю позицию в ирландских чартах. "U-2" повторили свой успех с "Another Day" (1980) и в этом же году подписали контракт с английской "Island records". 
<br>Их британский дебют "11 O'Clock Tick Tock", продюсировавшийся Мартином Ханнеттом, хорошо продавался, но в чарты так и не попал. Два следующих сингла, "A Day Without Me" и "I Will Follow", разошлись маленьким тиражом, но группа уже подготовила свой первый альбом с продюсером Стивом Лилливайтом. Вдохновительный юношеский "Boy" получил одобрение со стороны критиков, которое было укреплено концертами, что "U-2" предпринимали по всей стране. Возбужденный вокал Боно и музыкальный ритм группы сделали ее наиболее многообещающей концертной командой 1981 года. После американского тура, возвратясь в Англию, парни обнаружили, что их композиция "Fire" попала в местный Топ 30. 
<br>Альбом 1981 года "October" был пронизан религиозным, почти евангелистским по своей силе воодушевлением. В феврале 1983-го группа попала в английский Топ 10 с "New Year's Day", песней надежды, вдохновленной польским движением "Солидарность". Вскоре вышел альбом "War", встреченный аплодисментами критиков. Темой диска являлись религиозные и политические конфликты, особенно в ключевом треке "Sunday Bloody Sunday", который являлся одной из наиболее потрясающих песен группы. Следом вышел "концертник" "Under a blood red sky", поднявшийся на 2-ю позицию в английских чартах и на 28-ю – в американских. К лету 1984 "U-2" входили в авангард рок-элиты. Группа организовала собственную записывающую компанию, "Mother Records", чтобы помогать становлению молодых ирландских музыкантов. 
<br>
<br><img src="U2_pop.jpg" alt="<? echo $nam; ?> photo" hspace=10 vspace=3 border=0 align=right>
"Unforgettable Fire", продюсировавшийся Брайеном Ино и Дэниелом Лануа, показал новую зрелость "U-2" и укрепил популярность группы в США. Очередной сингл, "Pride (In The Name Of Love)", показывал страсть и гуманность, которые были к настоящему времени постоянными компонентами музыки и лирики коллектива. Приверженность группы к своим идеалам подчеркивалась ее участием в акциях "Live Aid", "Ireland's Self Aid", "Amnesty International". В течение этого периода "U2" совершили кругосветное турне и закончили работу над следующим альбомом. 
<br>"Joshua Tree" появился в марте 1987 года и подтвердил положение "U-2", как одной из наиболее популярных групп в мире. Возглавивший американские и английские чарты диск представил более экспансивный звук, который дополнили тексты на тему переоценки ценностей. Особенно это чувствовалось на сопутствующих синглах "With Or Without You" и "I Still Haven't Found What I'm Looking For", являвшихся лидерами американских хит-парадов. После такого глобального альбома 1988-й год прошел для "U-2" относительно спокойно. Боно и Эдж появились на "Mystery Girl" Роя Орбисона, а в конце года вышел "живой" двойник "Rattle And Hum". В конце 1991 года песня "The Fly" возглавила британские чарты. Лонгплей "Achtung Baby" оказался довольно впечатляющим, прежде всего потому, что группа для создания ощущения спонтанности сознательно пошла на некоторое огрубление звука. 
<br>Хотя о следующих альбомах "Zooropa" и "Pop" критики отзывались более прохладно, группа остается одним из наиболее привлекательных явлений современного рока. В середине 90-х Боно посвятил много времени сочинению песен для других музыкантов. С Эджем он написал тему для фильма о Джеймсе Бонде "Goldeneye". В марте 2000 по сценарию Боно вышел фильм "The Million Dollar Hotel". "U-2" записали к нему саундтрек "The Ground Beneath Her Feet" на стихи Салмана Рушди. Эта песня была представлена также на диске "All that you can't leave behind", на котором группа отошла от "электроники" и вернулась к своему саунду конца 80-х. Возглавившая чарты композиция "Beautiful Day" получила три награды "Grammy", включая "Песню года". 
