U2 are an Irish rock band featuring Bono (Paul David Hewson) on vocals, rhythm guitar and harmonica; The Edge (David Howell Evans) on lead guitar, keyboards and backing vocals; Adam Clayton on bass guitar; and Larry Mullen, Jr. on drums and, occasionally, backing vocals.
<p>
Founded in 1976, U2 has consistently remained among the most popular acts in the world since the mid 1980s. The band has sold approximately 50.5 million albums in the U.S., according to the RIAA, and upwards of 170 million worldwide, has had six #1 albums in the US and nine #1 albums in the UK and is one of the most successful bands of all time. Since the release of their album The Joshua Tree they have been frequently referred to as the biggest rock band in the world by fans and critics alike. The band has won 22 Grammy awards, the most for recording artists.
<p>
The band is also politically active in human rights causes, such as the Make Poverty History campaign as well as Live Aid, Live 8, and Bono's DATA (Debt, AIDS, Trade in Africa) campaign.
