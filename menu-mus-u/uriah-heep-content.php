<div align=justify>
<img src="uriah-heep.jpg" alt="<? echo $nam; ?>" width=200 height=146 hspace=5 vspace=5 border=0 align=left>
Эта английская группа была основана вокалистом Дэвидом Байроном (на самом деле Дэвид Гаррик) и лидер-гитаристом Миком Боксом. 
Сначала, в середине 60-х, они играли в "Stalkers", а затем стартовали проект "Spice".
Затем "Spice" эволюционировала в "Uriah Heep" (название было принято от персонажа романа Чарльза Диккенса).
Их дебют, "Very 'eavy ... Very 'umble" в 1970 был разноплановым от электрофолка до тяжелого саунда. 
Группа положила начало новому стилю , впоследствии названному "прогрессив рок". 
Лонгплей "Look at Yourself стал их первым попаданием в британские чарты и достиг 39 места в ноябре 1971. 
Стабильность нового состава позволила "Хипам" войти в их наиболее успешный период в течение ранних 70-х, когда фантастический, эксцентричный характер их лирики был поддержан грандиозным музыкальным подходом. 
Квинтет сделал запись пяти альбомов, начинающихся с "Demons And Wizards", их первого попадания в чарты США. 
