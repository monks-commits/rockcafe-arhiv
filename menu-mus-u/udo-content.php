В 1987 году один из основателей "Accept", Удо Диркшнайдер решил, что настало время обзавестись собственной группой и скромно назвал свой новый проект "U.D.O.".
Первые же альбомы, "Animal House" и "Mean Machine" продолжили линию, начатую еще на акцептовском диске "Russian Roulette". Как ни крути, а вокал Удо давал о себе знать. Соответственно часть фанов перетекла из лагеря "Accept" к "U.D.O.".
В 1990 году неписаный закон группы не использовать клавишные был нарушен с выходом альбома "Faceless world". Несмотря на этот факт, пластинка получилась очень сильной.
После выхода этого альбома Удо на неопределенное время заморозил свой проект, поскольку состоялось воссоединение "Accept". Со своими бывшими коллегами Диркшнайдер гастролировал и записывался вплоть до 1996 года. 
Вышедший в 1997-м "Solid" показал, что Удо хотел всегда исполнять хард-энд-хэви. 
Его тандем с Кауфманом оказался удачным, и фаны с энтузиазмом восприняли новую пластинку. 
Также хорошо отнеслись к альбому и музыкальные издания. 
Так, в чартах журнала "Rock Hard" этот релиз пробыл больше года. 
В 2001 году группа выпустила концертник "Live From Russia". 
