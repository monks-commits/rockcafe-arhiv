Впервые участники этой английской супергруппы встретились в публичной школе в Чартерхаусе. Питер Габриэль (р. 13 мая 1950, Лондон; вокал), Тони Бэнкс (р. 27 марта 1951, Ист Хетли, Англия; клавишные) и Крис Стюарт (ударные) играли в ансамбле под названием "Garden Wall". Впоследствии к ним присоединились Энтони Филипс (гитара, вокал) и Майк Рутерфорд (р. 2 октября 1950; бас, гитара, вокал). В январе 1967 студенты-музыканты послали демо-ленту другому выпускнику Чартерхауса, Джонатану Кингу, работавшему в "Decca". Кинг профинансировал дальнейшие записи коллектива, окрестив группу "Genesis". В 1968-м парни запись свой первый сингл "The Silent Sun", а в следующем году вышел их дебютный альбом "From Genesis To Revelation". Диск не имел особого успеха, и группа осталвалась без контракта, пока инициативный Тони Страттон-Смит не подписал их к своей недавно основанной компании "Charisma Records" в 1970-м. 
<br>
В "Genesis" сменились три ударника, пока в августе того же года не появился достойный кандидат на это место. Фил Коллинз (р. 31 января 1951, Лондон) уже работал с профессиональной группой "Flaming Youth", и его привлечение явилось критическим моментом в достижении "Генезисом" международного успеха. 
<br>
Уже записанный "Trespass" вышел в октябре 1970-го, но продавался плохо. Последовали дальнейшие изменения в составе группы: к ней присоединился новый гитарист, Стив Хакетт (р. 12 февраля 1950, Лондон). "Genesis" был уже хорошо известен как концертный коллектив, но это не помогало продажам дисков. Альбом 1971 года "Nursery Cryme" также потерпел коммерческую неудачу, и группа снова была на грани разрыва контракта со своим лейблом. Надежды на улучшение положения оправдались с выходом "Foxtrot", имевшим успех на континенте. Альбом попал в британский Топ 20 и включал в себя хит "Supper's Ready". В последующие два с половиной года "Genesis" увеличил свою популярность изданием бестселлеров "Selling England by the pound" и "The lamb lies down on Broadway". 
<br>
И вдруг неожиданно на пике славы, в мае 1975 года из группы ушел Габриэль. Многие предрекали конец "Генезису", особенно, когда было объявлено, что новым вокалистом будет их ударник Коллинз. Однако и вчетвером группа записала прекрасные альбомы "A Trick Of The Tail" и "Wind and Wuthering", тепло встреченные публикой. 
<br>
В серединой 80-х творчество музыкантов перешло рамки группы, и Коллинз параллельно занялся сольной Летом 1977 года из команды ушел Хакетт, решивший заняться сольной карьерой, после чего из "Genesis" получилось трио, привлекавшее иногда сессионных музыкантов. Удивительно, но казалось, что популярность группы росла с уходом кого-нибудь из членов группы. В 1978 году "Genesis" получили первый золотой диск за "And Then There Were Three", а два года спустя альбом "Duke" завис на верхних строчках хит-парадов. В конце 1981-го они попали в американский Топ 10 с "Abacab", и можно было справедливо утверждать, что "Genesis" является одним из наиболее популярных коллективов в мире рок-музыки. В середине 80-х творчество музыкантов перешло рамки группы, и Коллинз параллельно занялся сольной карьерой, а Рутерфорд основал проект "Mike And The Mechanics". В 1991 году трио возобновило работу, чтобы сделать запись нового альбома "We can't dance". 
<br>
Хотя это был их первый диск после пятилетнего перерыва, он немедленно возглавил мировые хит-парады, подтверждая статус "Генезиса", как одной из ведущих мировых групп. Коллинз, однако, решил, что его переезд в Швейцарию и сольная карьера несовместимы с ведущей ролью в коллективе и официально ушел в отставку. Ему на замену был взят Рэй Вилсон, бывший вокалист "Stiltskin". Его дебют состоялся на альбоме "Calling all station", выпущенном в августе 1997-го. Этот диск стал заключительным в дискографии "Genesis". 

