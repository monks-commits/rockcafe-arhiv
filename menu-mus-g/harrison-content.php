<div align=justify>

Единственным из "Битлз", кто родился в большой семье и кого миновали в детстве драматические ситуации, был Джордж Харрисон - самый млдаший из битлов.
<br>Джордж родился 25 февраля 1943 года в ливерпульском районе Вейтвери, улица Арнольда Гроува, 12. В семье он был младшим из детей и поэтому являлся всеобщим любимцем.
<br>"Я пошёл его посмотреть в первый же день. Это было сверхъестественным. Он был моей маленькой копией. Впрочем, все это могло и показаться мне", вспоминал потом Харольд Харрисон.
<br>Джордж рос очень независимым и самостоятельным. Уже в два с половиной года он ходил в магазин за покупками. Все соседи по улице знали его. Первые воспоминания Джорджа о доме связаны с цыплятами. Ему и его братьям принесли цыплят. Но выжил из них только один - Питер. Этот цыплёнок вскоре превратился в сильного и агрессивного петуха, которого вынуждены были съесть на Рождество.
<br>В 1954 году мальчик поступил в школу "Ливерпул Инститьют". Пол МакКартни уже в течение года учился здесь, а Джон Леннон - четыре года в "Куорри Бэнк Хай Скул".
<br>С самого начала Джордж выделялся в школе своей экстравагантной для того времени манерой одеваться. Майк МакКартни, брат Пола, будучи на год моложе Харрисона, вспоминал, что у Джорджа всегда были длинные волосы, ещё задолго до того как они вошли в моду. И если Джон утверждался в школе с помощью бесконечных стычек и драк, то Джордж делал то же самое своим внешним видом, чем очень досаждал учителям. Причина же того, почему Харрисон не стригся, была проста. Отец в целях экономии сам стриг всю семью, чем и вызывал неависть Джорджа к этой процедуре. Он ходил в школьной кепке, сидевшей где-то на верхушке его причёски, и в очень узких брюках. 
<br>Музыкой Джордж вначале не увлекался. Заразился он ею примерно в тоже время, что и остальные битлы. Как-то, купив по случаю у одного из одноклассников гитару, он стал учиться играть на ней.
<br>Однажды для участия в одном из многочисленных фестивалей Джордж и его брат Питер организовали группу под названием "Ребелс". Но пока Харрисон не всретился с "Куорримен" (группой Джона, давшей начало "Битлз"), до тех пор ничего серьёзного не произошло.
<br>
<br>Период в "Битлз". Отметим лишь, что за время существования "Битлз" 20 песен Джорджа вышли на 13 альбомах "Битлз". 
<br>Ещё 2 вышли на синглах (The Inner Light и Old Brown Shoe), а затем и на The Beatles Past Masters Vol. 2 (1988). 
<br>
<br>
<img src="Harrison_George.jpg" alt="<? echo $nam; ?>" hspace=10 vspace=3 border=0 align=left>
После распада "Битлз" Джордж в ноябре 1970 года выпускает тройной альбом "All Things Must Pass" ("Всё должно пройти"), заслуживший лестную оценку критиков, хотя в то же время и ошеломивший их своими параметрами.
<br>Его последний альбом "Cloud Nine" ("Облако девять"), сделанный в содружестве с Ринго Старром, Эриком Клэптоном, Элтоном Джоном и появившийся в конце 1987 года, сразу же занял одно из лидирующих мест в мировых хит-парадах. Кстати, популярным стал и сингл с песней из альбома "Got My Mind Set On You" ("Всё время думаю о тебе"), получивший, например, в американском хит-параде второе место. Можно также отметить и песню "When We Was Fab" ("Когдамы были знаменитыми") - ностальгию о временах "Битлз". 
<br>Далее следовал двойной концертный альбом "Live In Japan", выпущенный в 1992 году и... восьмилетнее молчание. Следующий альбом "12 Arnold Groove" вышел только в 2000 году. Также в 2000 году был выпущен альбом "Through many Years".
<br>А 30 ноября 2001 года Джорджа Харрисона не стало. Вышедший уже после его смерти альбом "Brainwashed" (2002 год) остаётся последним в его дискографии.
