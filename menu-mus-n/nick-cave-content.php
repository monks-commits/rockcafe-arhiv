<div align=justify>
Николас Эдвард Кейв родился 22 сентября 1957 года в Австралии, под Мельбурном. 
С Миком Харви он организовывал практически все свои проекты. 
Одним из первых таких проектов стала группа Boys Next Door, которая распалась к 1980 году.
После этого созлается группа The Birthday Party, названная так в память одной из сцен из "Преступления и наказания". 
