Незадолго до распада Ник Кейв познакомился с Бликсой Баргельдом, лидером очень шумной немецкой команды Einsturzende Neubauten, и вскоре предложил ему играть у себя. В 1984 году выходит первый альбом новой группы Кейва - The Bad Seeds - From Her To Eternity. 
<br>В 1996 году Ник Кейв выпустил альбом Murder Ballads, который, благодаря дуэту с Кайли Миноуг Where The Wild Roses Grow завоевывает большой коммерческий успех.
<br>В 1997 году выходит альбом The Boatman's Call, видимо, знаменующий новый этап в творчестве Nick Cave & The Bad Seeds, настолько он отличается от предыдущих работ.
<br>Самого себя в интервью Кейв начинает называть office man, клерком, потому что теперь работает над песнями в офисе, по строгому распорядку. 
