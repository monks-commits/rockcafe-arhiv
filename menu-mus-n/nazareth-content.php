<p>

Вряд  ли  кто-либо  может  представить  себе  настоящий рок-н-ролл без
"Nazareth"... Их история уходит корнями в далекие 60-е. Участники группы
играли  в  разных  составах, пока не объединились под флагом
"Nazareth". Сформированный в 1968 в Данфермлайне, Шотландия, "Nazareth" был создан
на  основе  местной  группы "Shadettes". Дэн Маккаферти (вокал), Мэнни
Чарлтон  (гитара),  Пит  Агню  (бас)  и  Даррел  Свит (р. 16 мая 1947,
Борнмаутс,  Англия,  у. 30 апреля 1999, Нью-Олбани, США; ударник) дали
новое   название  группе,  соответствующее  библейскому  палестинскому
городу. Параллельно с этим команда переезжает в Рок-н-ролльную Мекку - Лондон.
<br>
Популярность   Nazareth   в  золотые  годы  расцвета  рок-музыки  была
огромной. В 70-х от них фанатели чуть   ли   не   больше,  чем  от  концептуального  LED  ZEPPELIN  или
классического  DEEP  PURPLE.  Возможно, один из главных компонентов их
успеха - своеобразный голос Dan McCafferty. А если к этому добавить безукоризненный мелодизм основных хитов группы и
классную технику игры - от такого коктейля никому не устоять.


