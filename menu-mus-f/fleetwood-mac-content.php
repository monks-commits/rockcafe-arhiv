Английская группа <b>«Fleetwood Mac»</b> образовалась в 1967 году. Исходный состав: Мик Флитвуд (р. 24 июня 1947, Редрутс, Англия; ударные), Джон Маквэй (р. 26 ноября 1945, Лондон, бас-гитара), Питер Грин (настоящее имя – Питер Аллен Гринбаум, р. 29 октября 1946, Лондон; вокал, гитара), Джереми Спенсер (р. 4 июля 1948, Лондон; гитара). 
<br>В 1969 году выходят два первых диска группы. Они представляют собой набор несложных песен, ориентированных на традиционный блюз. Двум из них немедленно нашлось место на верхних ступенях британского национального хит-парада. Вышел также хитовый сингл с обалденной вещью Грина "Black magic women", которую впоследствии исполнял Сантана. 
<br>Год спустя популярность "Fleetwood Mac" пришла и в Новый Свет. 
<br>В 1970 году "FM" уже были известны повсеместно, и на волне успеха группу покидает Питер Грин. Выпустив, однако, только один сольный диск, Питер заявил, что прекращает занятия рок-музыкой и вступает в какую-то секту. 
<br>Год спустя историю Грина повторил Джереми Спенсер: было похожее заявление журналистам о прекращении занятий рок-музыкой, связанное со вступлением в секту. Впрочем, аскетами Грин и Спенсер пробыли не так уж долго, а стали выпускать сольники. 
<br>Альбом 1995 года "Time" уже не имел такого успеха, как любой из предыдущих. Это заставило Флитвуда пересмотреть свое отношение к руководству группой, и он начал переговоры с Грином, Бакингемом и Никс. Вскоре было объявлено о восстановлении звездного состава «Fleetwood Mac» времен "Rumours". С тех пор группа так и играет в этом составе. 

