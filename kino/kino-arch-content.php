<h2>23 июля 2003 - "Jesus Christ - Superstar"<hr size=1 color=#ffffff></h2>
<img src="../menu-mus-j/JCS1.jpg" alt="Jesus Christ - Superstar" width=350 height=140 hspace=10 vspace=0 border=0 align=right>
<b>Демонстрация фильма "Jesus Christ - Superstar" ("Иисус Христос - Cуперзвзеда")</b>, снятого в 1973 году Норманом Джуисоном по мотивам одноименной Рок-Оперы, созданной в 1970 году Эндрю Ллойдом Уэббером и Тимоти Райсом.
<br>
<br>Judas Iscariot - BEN VEREEN
<br>Jesus of Nazareth - JEFF FENHOLT 
<br>Mary Magdalene - YVONNE ELLIMAN 
<br>Pontius Pilate - BARRY DENNEN 
<br>...and others :)
<br>
<br>
<img src="../menu-mus-j/JCS2.jpg" alt="Jesus Christ - Superstar" width=181 height=132 hspace=10 vspace=0 border=0 align=left>
Не все читали Священное Писание, но мюзикл "Иисус Христос - суперзвзеда" слышали почти все.
Это произведение в этом году отмечает возраст Христа - премьера состоялась в "Альберт-холле" 1 марта 1970 года.
За эти годы тираж одноименного альбома по самым скромным подсчетам (без "пиратских" изданий) перевалил за сто миллионов. 
Империя грамзаписи не знает аналогичного размаха для одного произведения.
<br><br>
<img src="../menu-mus-j/webber.jpg" alt="Andrew Lloyd Webber" width=150 height=100 hspace=10 vspace=0 border=0 align=right>
Авторы привлекли около полустони певцов и музыкантов, а диапазон инструментов был почти необъятен - от Лондонского струнного оркестра до Лондонского симфонического. 
Главную партию исполнил один из лучших голосов мирового рока - Йан Гиллан (недаром его дед пел в Венской опере). 
Причем свою партию Гиллан выучил за день, а записал за три часа. 
Эта роль и работы в "золотом" составе Deep Purple - двуглавая вершина в его долгой карьере (тем паче что премьера мюзикла совпала с выходом монументального альбома "Deep Purple in Rock"). 
Мало кто знает, что Уэббер и Райс, чтобы сорвать уж полный банк, приглашали Джона Леннона, популярнее которого тогда не было никого на рок-сцене. И тот дал согласие - при условии, что Марией Магдалиной будет Йоко Оно. Но Йоко не согласилась :)
<br><br>
Удивительнее всего, что мюзикл одобрила церковь. 
Англо-американская паства пыталась что-то критиковать, но из Ватикана поступило высочайшее благословение. 
Ворота к феноменальному успеху распахнулись настежь. Началась "Джизусомания".
<br>
А на недавнем всемирном симпозиуме музыковедов ария Марии Магдалины заняла девятое место в дюжине самых популярных мелодий двадцатого столетия.
<br>
<br>Разумеется, кинематограф тоже не остался безучастным. В 1973 году Норман Джуисон перенес музыкальную притчу на экран.
