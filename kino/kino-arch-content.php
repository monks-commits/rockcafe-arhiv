<h2>24 сентября 2003 - «Metallica» и симфонический оркестр<hr size=1 color=#ffffff></h2>
<b>24 сентября</b>, в "Рок-Кафе" состоялась демонстрация видео-записи <b>концерта группы <a href="../menu-mus-m/metalica.php">«Metallica»</a> с симфоническим оркестром Сан-Франциско (1999 год)</b>.
<br>
<br>Фильм показан в рамках <b>НЕДЕЛИ «HEAVY METAL»</b>, которая прошла в "Рок-Кафе" с 22 по 28 сентября 2003г.
<br>
<br>
<a href="../menu-mus-m/metalica.php"><img src="../menu-mus-m/metalica.jpg" alt="Metallica" width=190 height=250 hspace=10 vspace=3 border=0 align=right></a>
<a href="../menu-mus-m/metalica.php"><img src="../menu-mus-m/metalica-ticket.jpg" alt="Metallica" width=300 height=169 hspace=10 vspace=3 border=0 align=left></a>
<a href="../menu-mus-m/metalica.php">«Metallica»</a> - пожалуй, самая известная металлическая группа конца 80-х и 90-х была сформирована в 1981 году в Калифорнии (США). 
<br>В течение многих лет группа исповедовала трэш, начавшийся с альбома "Kill 'Em All". Их музыка послужила шаблоном для целой плеяды новых металлических групп, хотя сами ее создатели не считали обязательным придерживаться одного и того же стиля. 
<br>И хотя "Ride The Lightning" (1984) был неплохим альбомом, музыкантов потянуло на эпический стиль, что и проявилось на диске 1986 года "Master Of Puppets". 
<br>Первой записью "Металлики" в новом составе стал "The .98 EP - Garage Days Re-Revisited" - собрание кавер-версий песен таких групп как "Budgie", "Diamond Head", "Killing Joke" и "Misfits". 
<br>В 1991 году вышел альбом "Metallica", занявший первые места в хит-парадах по обе стороны Атлантики. Тематика песен с него была посвящена глобальным темам: правосудие и возмездие, безумие, война, религия и взаимоотношения людей. По сравнению с альбомом десятилетней давности "Kill 'Em All" группа выросла от направленного против предрассудков хаоса до вдумчивой гармонии, пронизанной внезапными изменениями настроения и темпа. 
<br>К июню 1996-го в США было продано девять миллионов копий "Metallica", а появившийся месяцем позже долгожданный альбом "Load", дебютировал в штатовских чартах под номером 1. На этом диске группа немного сменила свой имидж, сделав закос в "альтернативу". 
<br>
<br><b>Альбом 1999 года, "S&M", был записан "вживую" с симфоническим оркестром Сан-Франциско.</b>
