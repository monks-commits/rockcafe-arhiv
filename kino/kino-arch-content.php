<h2>3 сентября 2003 - "Bee Gees"<hr size=1 color=#ffffff></h2>
<b>3 сентября</b>, в "Рок-Кафе" состоялась демонстрация <b>фильма "Sgt. Pepper's Lonely Hearts Club Band"</b>, звуковую дорожку к которому записали участники легендарной диско-группы 70-х <a href="../menu-mus-b/bee-gees.php">"Bee Gees"</a>. Это не опечатка, именно <b>"Bee Gees"</b>! :-)
<br>
Показ фильма приурочен дню рождения <b>Барри Гибба</b> (01.09.1947), старшего брата <b>"Bee Gees"</b>.
<br>
<br>
<a href="../menu-mus-b/bee-gees.php"><img src="../kino/bee-gees01.jpg" alt="Bee Gees" width=200 height=197 hspace=10 vspace=3 border=0 align=left></a>
<a href="../menu-mus-b/bee-gees.php"><img src="../kino/bee-gees02.jpg" alt="Bee Gees" width=200 height=202 hspace=10 vspace=3 border=0 align=right></a>
То была на редкость удачная идея — снять музыкальный фильм на основе пластинки "Битлз", признанной лучшим альбомом на все времена. 
<br>
В 1977 году исполнилось десять лет с того дня, когда мир услышал "Sgt. Pepper".
<br>
Переданный режиссеру Майклу Шульцу сценарий был выстроен на основе битловских альбомов "Sgt. Pepper's Lonely Hearts Club Band" и "Abbey Road" и предусматривал некоторое количество отрицательных персонажей, на роль каковых симпатяги из <a href="../menu-mus-b/bee-gees.php">"Bee Gees"</a> не годились совершенно. Зато музыканты AEROSMITH подходили как нельзя лучше. И, после того как отказались THE ROLLING STONES, они согласились — желая угробить (хотя бы в кино) исполнявших главных персонажей чистюль Питера Фрэмптона и BEE GEES, побегать с немецкими "шмайсерами" и поработать с музыкальным директором фильма Джорджем Мартином. 
<br>
Джордж, продюсер THE BEATLES был единственным — кроме музыки, конечно, — что связывало постановку с Великолепной четверкой. <br>
К октябрю 1977-го звуковая дорожка была готова и сведена битловским инженером Джеффом Эмериком в студии на Эбби-Роуд.
<br>
Верхом шика стала финальная сцена, в которой заглавную песню картины исполнял звездный хор в сотню с лишним человек — среди них были Тина Тернер и Хосе Фелисиано, Джек Брюс и Джордж Бенсон, группы HEART и SHA NA NA... 
<br>
Двойной — в отличие от битловского, но по примеру "Fever" — альбом появился в продаже практически одновременно с тем, как ленту увидели критики. 
<br><br>
Информация: http://www.beegees.narod.ru/bgs_inf17.htm

