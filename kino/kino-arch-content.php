<h2>20 августа 2003 - «The Song Remains the Same»<hr size=1 color=#ffffff></h2>
В честь <b>55-летия Роберта Планта</b> (Robert Plant), вокалиста легендарной группы <a href="../menu-mus-l/led-zeppelin.php">Led Zeppelin</a>, в "Рок-Кафе" - демонстрация <b>фильма "The Song Remains the Same"</b> ("Песня Остается Прежней").
<br>
<br>
<a href="../menu-mus-l/led-zeppelin.php"><img src="../menu-mus-l/plant01.jpg" alt="Robert Plant" width=200 height=134 hspace=10 vspace=0 border=0 align=left></a>
В январе 76 зеппелиновцы улетели в Нью-Йорк - работать там было выгоднее из финансовых соображений. 
Там они и трудились над кинокартиной, которую решили назвать "The Song Remains the Same - Песня Остается Прежней".
Режиссер Питер Клифтон смонтировал в ней концертные кадры с фантастическими эпизодами, создав документальный, в общем, фильм с сильным мистическим, оккультным уклоном. Звуковая дорожка была записана в конце 73 года.
<br>
В октябре 76 года <a href="../menu-mus-l/led-zeppelin.php">Лед Зеппелин</a> прибыли в Нью-Йорк на премьеру фильма, а за два дня до этого их пластиночная компания выпустила двойным альбомом саундтрек.
