Слово «блюз» вошло в лексикон киноманов после выхода в свет блестящей музыкальной комедии Джона Лэндиса «Братья Блюз» (1980). Этот почти гениальный бурлеск родился из реально существовавшей группы «Blues Brothers», в которой на самом деле пели Джон Белуши и Дэн Эйкройд. Эйкройд и Белуши долгое время вели знаменитое шоу «В субботу вечером - в прямом эфире» на телеканале NBC, в котором принимала участие и группа, впервые снялись вместе в военной комедии Стивена Спилберга «1941». В «Братьях Блюз», как писала критик Полина Каэл, «поп-культура того времени спрессована в сутки». Взяв за основу полукомедийные, полугротесковые версии известных ритм-н-блюзовых хитов и феерический комедийный талант актеров, бывший каскадер Лэндис создал фильм, в котором очень мало реального - и в то же время очень много того, что мы расцениваем, как реальность. Нервный, взрывной Джейк (Белуши) и невозмутимый Элвуд (Эйкройд), неизменно в черных костюмах, шляпах и темных очках (так вот кто был на самом деле «людьми в черном!») при помощи дружков-музыкантов пытались спасти от закрытия сиротский приют, в котором были воспитаны. По дороге они удирали от федеральных агентов, иллинойсских фашистов, разозленных фермеров, бывшей невесты Джейка (Кэрри Фишер, знаменитая прицесса Лейа из «Звездных войн»; обычно вспоминают трогательнейший диалог в канализации, во время которого Джейк - единственный раз за весь фильм! - снимает очки). Им и помогали, и просто встречались на пути Рэй Чарлз, Арета Фрэнклин, крестный отец музыки соул и фанк Джеймс Браун, Кэб Кэллоуэй, главный из ныне живущих блюзмен Джон Ли Хукер... Действие, естественно, происходило в колыбели блюза, в Чикаго (сам Лэндис оттуда же родом, как и Белуши), а одну из эпизодических ролей сыграл Стивен Спилберг. И - музыка, музыка! И блюз, и рок-н-ролл (это в самом конце, когда вся компания уже сидит), и даже кантри! Но главное, что было в этом фильме, помимо рекордного числа задействованной массовки, технических средств и разбитых автомобилей - убежденность, и похоже, искренняя, в том, что Музыка превыше всего. 
