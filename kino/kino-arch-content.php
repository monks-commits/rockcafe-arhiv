<h2>06 августа 2003 - группа «Stomp»<hr size=1 color=#ffffff></h2>
<img src="http://pages.ykt.ru/dj-nikita/images/stomp.jpg" alt="Stomp" width=200 height=261 hspace=10 vspace=0 border=0 align=right>
<b>Идея и постановка:</b> Люк Крессуэлл и Стив МакНиколас.
<br><b>Исполнители:</b> Кэл Хауэс, Тим Паркер, Мэнассэ Джексон, Пол Эмери, Майк Робертс, Шон Эдвардс, Фрэйзер Моррисон, Укачи Акалаву, Уоррен Ричардсон, Стив Дрэйпер, Найджел Кларк, Билли Хиклинг, Мелани Джозеф, Ник Хэндфорд, Наоми Ричардс, Де-Наполи Кларк, Стивен Дин Дэвис.
<br>
<br>Одну из самых больших ошибок в жизни совершили (совершают, будут совершать) те, кто до сих пор ЭТОГО не посмотрел... То, что вытворяет британская танцевально-музыкальная труппа "Stomp" не поддается абсолютно никакому описанию.
<br>Каждый из нас когда-нибудь один раз в жизни, при тех или иных обстоятельствах, выстукивал ногой ритм запомнившейся мелодии. Ну, согласитесь, бывало такое. Притопывали, прихлопывали. Но делали это в основном ногами и руками. В крайнем случае, использовали крышку стола. Однако нам с Вами и в голову не могло придти то, что пришло 7 лет назад в головы двум простым уличным танцорам - Люку Крессуэллу и Стиву МакНиколасу: из любых подручных средств (будь то крышка мусорного бака, сам мусорный бак, пустая ржавая бочка, чай (!), яблоки (!), сломанные ванны и унитазы, швабры-метелки, носки) создать ритм. И чтобы выбивал его не один человек. И даже не два. А десять, двадцать, тридцать... И сделать так, чтобы все шумы сливались воедино. И получалась музыка.
<br>Идея нашла отклик у многих, кто сейчас играет в труппе. Заметьте, что в заголовке написано "исполнители". Словосочетание "главная роль" здесь неуместно. Потому что каждый из этих людей играет свою главную роль. И играет ее просто потрясающе. Хотя по-настоящему профессиональных актеров в "Stomp" нет. Есть гениальнейшие танцоры, которые обладают совершенно невероятным чувством ритма. И, безусловно, отменным чувством юмора: в одной из сцен участники "Stomp" начинают выстукивать ритм, шурша карточными колодами, швыркая чаем, грызя яблоки и стуча гранеными стаканами по столу одновременно!
<br>Совершенно заслужены многочисленные награды, которыми удостаивался этот замечательный коллектив. Абсолютно естественна любовь зрителей, которые приходят на концерты "Stomp" (а уж там труппа вытворяет совершенно безумные вещи!). Вполне понятны шок и восхищение, которые одолевают людей, выходящих из зрительного зала после концерта. 

