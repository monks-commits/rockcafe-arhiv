Многие полагают, что первой рок-оперой был "Jesus Christ Superstar" Ллойд-Уэббера. Однако, по крайней мере за год до ее появления, мир увидел другую рок-оперу, "Tommy". "Tommy" не только принес всемирную известность своим создателям, группе "The Who", но и совершил настоящий переворот в сознании молодежи того времени.
<br>
В середине 60-х <a href="../menu-mus-t/the-who.php">"The Who"</a> была одной из многих групп, чья музыка укладывалась в мейнстрим, возглавляемый "Битлз" и "Роллинг Стоунз". 
<br>
Переломным стал 1966 год. Именно в этом году Тауншенд принес менеджеру группы Киту Лэмберту демо-запись композиции "Grat's Amat's", которую скорее можно было назвать дурачеством - 10 минут под простенькую аранжировку голоса, у которых был поднят тембр, пели фразу "Grat's Amat's". Тауншенд в шутку назвал это произведение "рок-оперой".
<br>
Пит Таушенд был учеником индийского мистика Меера Бабы, который, среди прочего, полагал, что человек не может понять Бога только потому, что слишком увлечен земными чувствами. Сюжет о слепоглухонемом мальчике, которому было дано увидеть Бога и стать новым Мессией, прекрасно иллюстрировал эту идею. Он и лег в основу рок-оперы.
<br>
Пластинка, вышедшая в 1969 году, представляла собой цикл песен, объединенных неким подобием сюжета. Именно поэтому в нем было много недосказанного, и слушателю многое приходилось додумывать самому. Экранизация прояснила историю Томми.
<br>
Критика отнеслась к появлению "Томми" неоднозначно. Одни говорили, что альбом очень интересен, возможно, даже талантлив. Другие считали эту запись одной из самых ужасных за всю историю рок-музыки. Более того, "Томми" был запрещен на Би-Би-Си и некоторых других радиостанциях.
<br>
Однако на фестивале в Вудстоке в августе 1969 года рок-опера произвела настоящий фурор, кардинальным образом изменив судьбу своих создателей. <a href="../menu-mus-t/the-who.php">"The Who"</a> стали полноценной "альбомной" группой. Их выступления собирали полные залы и стадионы, концертная запись "Томми", сделанная в университете Лидс, имела потрясающий успех. На волне этой популярности группа выпустила новый альбом, "Who's Next", признанный многими критиками их лучшей работой.
<br>
В 1975 году по рок-опере был снят фильм, о котором когда-то мечтал Кит Лэмберт. 
<br>
Группа <a href="../menu-mus-t/the-who.php">"The Who"</a> распалась в 1984 году, к началу 90-x "Томми" перешел в разряд музейных экспонатов, однако в 1993 году рок-опера была вновь поставлена в Нью-Йорке режиссером Дезом Макэнаффаном (при моральной поддержке Пита Тауншенда). 
<br>
Сказать, что бродвейская постановка "Томми" была успешной - значит не сказать ничего. За первые три года, прошедшие со дня премьеры, рок-опера была поставлена в Америке и в Европе более тысячи раз. 
<br>
У группы "The Who" есть еще одна рок-опера, <b>Quadrophenia</b>, по которой в 1979 году также был снят кинофильм.
<br>
