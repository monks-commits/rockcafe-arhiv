Эту английскую хард-рок-группу на протяжении всей ее карьеры возглавляет Дэвид Ковердейл. 
Ведущий вокалист "Deep purple" с 1973 года, Ковердейл оставил группу в 1976-м и записал два сольных альбома, "Whitesnake" и "Northwinds". 
Вскоре он сформировал команду для гастролей, состоящую из тех музыкантов, которые участвовали в их создании. 
Конец 70-х команда провела в активных гастролях по Англии, Европе и Японии, а в 1980 году состоялся их первый американский тур. 
Первым британским хитом "Whitesnake" стала композиция Ковердейла, Марсдена и Муди "Fool For Your Loving" (номер 13 в мае 1980-го), а двойной альбом, "Live ... In The Heart Of The City" поднялся до пятого места в том же году. Диск "Come An' Get It", взобрался на вторую позицию в британских чартах в апреле 1981-го, а композиция "Don't Break My Heart Again" с него заняла 17-е место в том же месяце.
<br>Частые туры способствовали успеху и альбом 1987 года "Whitesnake" только в США разошелся более чем миллионным тиражом а композиции "Here I Go Again" и "Is This Love" располагались в первой десятке хит-парадов по обе стороны Атлантики. 
<br>После выступления команды на донингтонском фестивале в качестве хедлайнеров в августе 1990-го, Ковердейл в конце года приостанавливает деятельность "Whitesnake".
