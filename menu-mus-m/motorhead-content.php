В 1975 году Лемми (Ян Килмистер, р. 24 декабря 1945, Сток-он-Трент, Англия; вокал, бас) был выгнан из "Hawkwind" после неприятностей с канадской таможней. Последняя песня, которую он написал для них, называлась "Motorhead", и также стала называться его группа, которую он основал совместно с гитаристом Ларри Уоллисом из "Pink fairies" и ударником Лукасом Фоксом. Уже в июле этого же года состоялось их первое выступление в Лондоне. 
В этом составе (Лемми, Тейлор, Кларк) группа играла 6 лет вплоть до 1982 года, став к этому времени наиболее известным хард-роковым трио. 
<br>Успех пришел к ним после выхода альбомов "Overkill" и "Bomber". Группа успешно колесила по всему миру и в 1980-м выпустила еще один хитовый альбом "Ace of spades". Их репутация как лучшей живой группы своего поколения значительно выросла с выпуском "No sleep 'Til Hammersmith", который который занял верхнюю строчку в хит-парадах Англии. 

