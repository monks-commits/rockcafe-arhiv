Оззи Осборн (настоящее имя - Джон Майкл Осборн) родился 3 декабря 1948 года в Бирмингеме.
Он уже более трех десятков лет входит в когорту самых почитаемых и культовых рок-музыкантов планеты. Крестный отец целого жанра - хэви-метала - и скандальный герой светских хроник - Оззи по праву заслужил титул Великого и Ужасного. А его история жизни является ярким примером того, как простой парень, благодаря своему таланту и харизме, может покорить весь мир.
<br>
На сцене Оззи Осборн появился в середине 60-х годов в группе Rare Breed, в 1968 году попал в группу Earth, впоследствии - Black Sabbath, где и добился широкого признания. Однако в 1977 году он, протестуя против экспериментов, которыми занялся лидер и основной композитор коллектива Тони Йомми, покинул группу и начал сольную карьеру. Правда, первый блин оказался комом, и Оззи вновь возвратился в Black Sabbath, для того чтобы окончательно покинуть группу в 1979 году.
<br>
Несмотря на первые неудачи талантливый и харизматичный фронтмент сумел доказать, что может добиться успеха и признания, будучи самостоятельной творческой единицей. 
<br>
Естественно успешные продажи альбомов и аншлаги на концертах постоянно подогревались феерическими шоу и скандальными историями, многие из которых уже стали хрестоматийными. Достаточно вспомнить - откушенные головы голубей, летучих мышей, бросание в зал кусков мяса и расстрел цыплят во дворе собственного дома.
