<b>Том Петти</b> /Tom Petty/ родился 20 октября 1950 года в городке Гэйнсвилль, Флорида в семье страхового агента. Совсем юным он влюбился в рок-н-ролл благодаря Элвису Пресли и решил, подобно Королю, посвятить свою жизнь музыке. 
<br>Вместе со школьными приятелями, Том основал группу Sundowners, позже переименованную в Epics, а к началу 70х трансформировавшуюся в Mudcrutch. 
<br>Позже Петти реанимирует коллектив с Тенчем (клавишные) и Майком Кэмпбеллом (гитара), к которым присоединились еще два музыканта из Гэйнсвилла - басист Рон Блэйр и барабанщик Стэн Линч. Назвавшись <b>Tom Petty And The Heartbreakers</b>, квинтет записал осенью 1976 года одноименную пластинку, поначалу принятую на родине довольно прохладно и лишь после триумфальных гастролей Петти и Ко. в Британии начавшую довольно прилично продаваться. Вышедший в 1978 году You're Gonna Get It получил статус золотого, успех следующей пластинки превзошел все ожидания - Damn The Torpedoes стал трижды платиновым.
<br>В 1989 Том Петти выпускает сольный альбом Full Moon Fever, спродюссированный Джеффом Линном и соратником по Heartbreakers Майком Кэмпбеллом. 
<br>Сборник хитов образца 1993 года удостоился платинового сертификата четырежды, за видео на включенную в него новую песню Mary Jane's Last Dance МТВ вручило Петти Best Male Video award . 
<br>В 1996 году Том Петти попробовал себя в качестве кино-композитора, написав музыку к романтической комедии Эдварда Бернса She's The One. В конце 90х музыкант удостоился множества наград и получил свою Звезду на Аллее Славы в Голливуде, не только за музыкальные достижения, но и за участие в работе многих благотворительных и правозащитных организаций от Гринпис до Фонда борьбы со СПИД. 
<br>Последний альбом Tom Petty And The Heartbreakers вышел в 2002 году под названием The Last DJ. 
