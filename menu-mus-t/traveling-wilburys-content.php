<img src="traveling-wilburys.jpg" alt="<? echo $nam; ?>" width=200 height=135 hspace=5 vspace=5 border=0 align=left>
Этот суперпроект был создан ради прикола пятью великими музыкантами – Джорджем Харрисоном, Бобом Диланом, Томом Петти, Роем Орбисоном и Джеффом Линном.
Тогда музыканты подумали о том, а почему бы им не записать что-нибудь всем вместе. Вскоре для нового проекта появилось и название, "Traveling Wilburys". 
Словечком "Wilburys" Джордж и Джефф называли различные глюки в звукозаписывающей аппаратуре, и сначала Харрисон предложил "The Trembling Wilburys", но потом первое слово было изменено на "Traveling". Заодно каждый взял себе псевдоним: так, Харрисон стал Нельсоном Уилбери, Петти – Чарли Ти Уилери младшим, Орбисон – Лефти Уилбери, Дилан – Лаки Уилбери, а Линн – Отисом Уилбери. 
<br>Первый диск назвали просто "Vol. 1". Роль лидер-вокалиста все исполняли по очереди, причем в основном это были Джордж, Боб и Рой, поэтому пластинка представляла собой нечто среднее между "Cloud nine" Харрисона, "Desire" Дилана и "Greatest Hits" Орбисона
<br>Второй раз "Traveling Wilburys" собрались вместе через два года. Правда, на этот раз их осталось только четверо, поскольку Рой Орбисон скончался от сердечного приступа спустя несколько недель после выхода в свет первой пластинки. На этот раз музыканты поменяли свои псевдонимы: Харрисон стал Спайком, Петти – Мадди, Дилан – Бу, а Линн – Клэйтоном Уилбери. Второй альбом озаглавили почему-то "Vol. 3", хотя "Vol. 2" в природе не существовало. Успех диска был поменьше, чем у его предшественника, однако его общий тираж перевалил за миллионную отметку.
<br>После его выпуска музыканты занялись каждый своим делом, а тем временем в течение 90-х годов упорно циркулировали слухи о воссоединении группы. 
Но особых оснований они под собой не имели, и лишь в 1999-м Петти заикнулся о том, что неплохо бы выпустить новый альбом. На следующий год эту мысль поддержал Джефф Линн, однако до дела так и не дошло. Воссоединение "Wilburys" окончательно поставила под сомнение смерть Харрисона, скончавшегося в ноябре 2001 года. 
