Ансамбль был организован в Англии под названием "Detours" Роджером Долтри.
В 1963 году группа сменила название на "The Who". 
Их судьбу сначала взял в руки издатель Питер Миден, который дал им новое название "High Numbers", сочинил первый сингл "I'm the Face", переделав песню Слима Харпо "Got Live If You Want lt", изменил их имидж в духе молодежного движения "модов", которое возникло в начале 60-х годов и достигло пика в 1964-м. 
Попытка Мидена пристегнуть к модам "High Numbers" особенного успеха не имела.
Внутри коллектива лидерство постепенно перешло к Питу Тауншенду. Группа вновь приняла это название "The Who".
<br>Их первая запись "I Саn't Explain", как и все последующие, вышла из-под пера Тауншенда и была фактически плагиатом с хорового стиля "Kinks". Собственно, такое заимствование в аранжировке было очень характерным для начального периода творчества группы.
Тауншенд стал использовать различные приставки к своей электрогитаре, что придавало звучанию инструмента эффект анархического взрыва.  
Пит написал песню, ставшую гимном модов – "My Generation", и наиболее часто исполняемую на концертах в течение целых 10 лет. 
<br>Тауншенд проявил себя как жесткий, умелый руководитель, а как поэт, добился того, что ансамбль стали слушать. 
Пит также отлично уяснил, как создать музыкой драматический эффект и использовать его. 
Альбом "A Quick One", вышедший в 1966 г., продемонстрировал инструментальную исключительность группы. 
В нем также дала о себе знать гротескная лирика. К этому времени "Who" добились стабильного высокого положения в чартах. 
<br>В 1968-м году "The who" впервые исполнили рок-оперу "Tommy". 
Их внезапно ставший легендарным статус был закреплен после выступления в Вудстоке в 1969 году, где Тауншенд был признан наиболее выдающимся артистом.

