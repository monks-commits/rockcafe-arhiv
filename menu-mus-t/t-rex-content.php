Марк Болан (Марк Фелд) начал профессионально заниматься музыкой еще в 1965 году, выпустив свой первый сингл "The Wizard". 
Затем он присоединился к психоделическому фолк-роковому проекту "John's Children", просуществовавшему недолгое время. 
После распада этой группы Болан организовал дуэт "Tyrannosaurus Rex" в компании с перкуссионистом Стивом Перегрином Туком.
Музыканты приступили к записи дебютного альбома "My People Were Fair And Had Sky In Their Hair But Now They're Content To Wear Stars On Their Brows". 
Неожиданный успех этой пластинки, а также сопуствующего сингла "Debora" заставил музыкантов работать интенсивнее, и к осени 1968 года уже был готов второй альбом "Prophets, Seers, And Sages, The Angels Of The Ages". 
Этот релиз также как и первый был чисто акустическим, однако уже на третьем диске группы появилась электрогитара. 
<br>В конце 1970 года группа окончательно переключилась на электрогитары и сократила свое название до "T. Rex". Дуэт выпустил сингл "Ride A White Swan", занявший второе место в английских чартах.
С выходом "Electric Warrior" началась повальная "тирексомания". Ажиотаж подогревался сценическим имиджем Болана, появлявшемся на концертах в боа, высокой шляпе и туфлях на толстенной платформе. 
Альбом "Slider" вышедший летом 1972 года сразу же ворвался на вершину английских хит-парадов, а за четыре первых дня было продано 100000 экземпляров этой пластинки.
Диск 1973 года "Tanx" имел несколько непривычный для "T. Rex" саунд, по словам Болана это был госпел-альбом. 
