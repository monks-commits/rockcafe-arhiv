В 1994 году "Type o negative" наконец-то получили широкое признание, и их даже пригласили участвовать в записи трибьютной пластинки "Black Sabbath". 
Третья работа группы, "October Rust", получила очень теплые отклики в прессе и обосновалась на верхних строчках чартов. 
На этом альбоме "Type o negative" уже приобрели свое оригинальное звучание. 
В 1999 году группа приняла участие в записи саундтрека к нашумевшему фильму "Ведьма из Блэр".
