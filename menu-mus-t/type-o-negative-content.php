Название для проекта Питер взял из телефонной книги, обнаружив там словосочетание "blood type: O negative". 
На следующий год квартет приступил к сессиям и записал дебютный альбом "Slow, Deep And Hard". Пластинка получилась мрачной, с "бритвенными" гитарными партиями Кенни, тягучим басом Стила и атмосферными клавишами Силвера.
