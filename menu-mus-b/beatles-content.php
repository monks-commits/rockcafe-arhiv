Об этой легендарной группе сказано столько, что едва ли можно сделать серьезное дополнение к известному. 
Группа "The Beatles" образовалась в Ливерпуле, Великобритания, в 1959 году. 
Прямые наследники американского рок-н-ролла 50-х "Битлз" поначалу почти не были известны за пределами родного Ливерпуля. 
Их "выручила" Германия, куда они уехали в 1960 году работать в одном из гамбургских клубов за весьма скромную плату.
Осенью 1961 года "Битлз" заинтересовался владелец магазина грампластинок Брайен Эпштейн, который с тех пор и на долгие годы стал менеджером и просто другом и помощником великой четверки. 
