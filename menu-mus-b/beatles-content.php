Первый авторский сингл "Beatles" под названием "Love me do" был записан на "Парлофоне" в декабре 1962-го, а дебютный альбом - в начале 1963 года. Диск включал в себя сразу три международных хита, и к 1964 году битломания охватила весь земной шар, а группа всех времен и народов начала восхождение к вершине популярности, доселе непокоренной. 
<br>В 1967 году был записан монументально-новаторский альбом "Клуб одиноких сердец сержанта Пеппера", который дал прямой толчок к появлению в рок-музыке таких жанров как apт-poк, хард-рок и психоделия. 
<br>Последним значительным произведением оказался альбом 1969 года "Abbey Road", названный так в честь лондонской улицы, на которой разместились многие ведущие английские студии. В эту же пору личные разногласия между участниками великого квартета достигли своего апогея (особенно между Ленноном и Маккартни) и в начале 1970 года "Beatles" официально объявили о распаде. Каждый из бывших участников легендарной четверки сделал впоследствии сольную карьеру. 
<br>8 декабря 1980 года был убит Джон Леннон, а в ноябре 2001-го скончался Джордж Харрисон. 

