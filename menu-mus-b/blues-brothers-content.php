Американский актерский музыкальный дуэт. 
<p>Давно увелкаясь блюзом, дуэт "Blues Brothers" со знанием дела и специфическим чувством юмора  разработали имидж, представлявший добрую пародию на «золотые денечки» соул и ритм-энд-блюза 50-х: мешковатые костюмы, галстуки узкой ленточкой, фетровые шляпы, светонепроницаемые очки - все черное. Свою речь сдабривали устаревшим блюзовым слэнгом. Начали выступать в ньюйоркских клубах и кафе; часто им аккомпанировала группа “Room Full of Blues”.
<p>Первый альбом записан в сентябре 1978г., когда на гастролях комика Стива Мартина (Steve Martin) они разогревали публику. Репертуар полностью состоял из стандартов ритмэндблюзовой эпохи. В 1979г. альбом стал платиновым, две песни из него вошли в TOP20 журнала Биллбоард (Billboard). «Блюз Бразерз» стали культовой группой. Вышедший в 1980г. Фильм “Братья Блюз” за первые два месяца проката в США принес 32 млн. долларов кассовых сборов (позже сумма еще утроилась) . Слоган из фильма: “We are on a mission from God”, произнесенный суровым тоном, превратился в девиз для тех, кто всей душой стоит за блюз. 
