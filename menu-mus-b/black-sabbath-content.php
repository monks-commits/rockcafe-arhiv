<div align=justify>

Члены английской группы "Earth", в 1969 году изменили свое название на "Black Sabbath".
Это название намекало на их тяжелую, груженую музыку, которую они производили. 
Раньше (еще до "Earth") у группы (тогда еще "Polka Tulk") была песня, озаглавленная "Black Sabbath", название которой было взято из одноименного культового фильма ужасов. 
В 1970 году группа записала одноименный дебютный альбом, а спустя некоторое время в этом же году вышел второй диск "Paranoid" (заглавная песня с которого стала лидером британских хит-парадов). 
К 1977 году между членами группы сложились напряженные отношения, и музыка "Black Sabbath" потеряла часть своего раннего оркестрового, претенциозного блеска. 
Всё это побудило Оззи Осборна уйти из группы и заняться сольной карьерой, что он и сделал в январе 1979-го. 
Дио присоединился к команде в конце 1991 года на записи "Dehumanizer".


