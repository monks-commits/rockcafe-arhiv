Бадди Холли (Buddy Holly) (7.09.1936 - 3.02.1959) по традиции относится к когорте "отцов-основателей" рок-н-ролла и по праву считается прямым предшественником рок-музыки в ее современном виде. Вместе с тем именно творчество Холли подвело черту под первым этапом развития рок-н-ролла, привнеся в него явственную коммерческую ноту и тем самым лишив "бунтарского" ореола в глазах слушателей.
<p>Чарльз Хардин Холли родился в техасском городе Лаббокк и уже в 13-летнем возрасте основал с Бобом Монтгомери дуэт BUDDY & ВОВ, исполнявший песни в стиле блюграсс. Однако понемногу музыка дуэта становилась более энергичной, и в октябре 1955 г. он уже выступал "на разогреве" у Билла Хейли и Элвиса Пресли. В январе 1956 г. компания "Decca Records" подписала контракт с Холли как с солистом. 
<p>27 мая 1957 г. дочерний лэйбл фирмы "Decca" - "Brunswick" выпустил сингл "That'll Be The Day", который к концу сентября был продан миллионным тиражом и занял 3-е место в чартах США (1 ноября он вышел на 1-е место в Англии). Второй сингл "Peggy Sue" (US-3/UK-6), посвященный невесте ударника Пегги Сью Джеррон, вышел уже под именем Бадди Холли.


