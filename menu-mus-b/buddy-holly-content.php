<p>Сценический имидж Холли был еще одной вариацией на тему "простого американского парня". Вечно улыбающийся, в больших роговых очках, Бадди демонстрировал, что рок-звездой можно стать, не будучи красавцем-суперменом. Рок-н-роллы BUDDY HOLLY AND THE CRICKETS были аранжированы в доступной, совсем не лихой, почти интимной манере, тексты уже не кипели скрытой сексуальной энергией, как в хитах 1955-1956 гг., а воспевали традиционную на эстраде неразделенную (или разделенную, но непременно ведущую под венец) любовь. Бадди Холли одним из первых в роке начал использовать гитару "Фендер-Стратокастер", которую впоследствии высоко оценили все ведущие инструменталисты. 
<p>
3 февраля 1959 г. небольшой самолет "Бичкрафт", на котором Холли летел из Клир-Лэйк в Фарго, потерпел катастрофу, и певец погиб. Вместе с ним погибли еще два известных поп-музыканта - Ричи Валенс и Биг Боппер.
<p>Трагическая смерть 22-летнего Бадди Холли вызвала новый всплеск интереса к его творчеству, особенно в Англии. Успешно продолжала работать и группа THE CRICKETS (последний хит-сингл она выпустила в августе 1964 г.). Музыку Бадди Холли высоко ценили Джон Леннон и Пол Маккартни, под сильным влиянием его группы находились в начале 60-х THE SHADOWS, a THE HOLLIES даже название себе выбрали в честь кумира. 
