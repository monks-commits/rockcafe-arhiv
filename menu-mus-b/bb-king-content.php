Настоящее имя: Райли Б.Кинг (Riley B. King)
<br>Дата рождения: 16 сентября, 1925 в Индианола, Миссисипи, США
<br>Жанр: Блюз
<br>Стиль: Модерн электрик блюз, Соул блюз (Modern Electric Blues, Soul-Blues)
