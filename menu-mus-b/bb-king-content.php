<p>Кантри и госпел музыка оказали решающее влияние на формирование творческих пристрастий Кинга вместе с такими великими стилистами исполнения, как Ти-Бонк Уолкер (T-Bone Walker) и Лонни Джонсон (Lonnie Johnson) и джазовыми гениями Чарли Крисченом и Джанго Рейнхардтом ( Charlie Christian, Django Reinhardt). 
<p>1949-й стал для Кинга годом звездного прорыва. Он записал свои первые 4 трека для Jim Bulleit's Bullet Records, затем подписал контракт со студией RPM в Лос Анжелесе. В течение 2-х последующих лет жизни в Мемфисе Кинг написал множество композиций для RPM. 
<br>В ноябре 1964 года гитарист выступил со своим новаторским концертным альбомом "Live at the Regal" в легендарном чикагском театре "Chicago" и его великолепно сыгранный концерт произвел настоящий фурор. Альбом стал истинным шедевром современного блюза. 
<p>Моментально узнаваемый гитарный стиль Кинга, его фирменная вибрирующая трель, которой он научился в Мемфисе у своего кузена Букка Уайта много лет назад, выделяют его из рядов современных гитаристов. Добавьте к этому его призывный взволнованный вокальный стиль и вы получите наиболее влиятельного и новаторского блюзмена за весь послевоенный период. Не может быть никаких сомнений, что Кинг будет господствовать как бесспорный король жанра (и посланник доброй воли) на всю оставшуюся жизнь.

