<p>Повсеместно признанный, титулованный король блюза - легендарный Би Би Кинг, без сомнений, самый уникальный и наиболее знаковый электрогитарист уходящего столетия. Современное блюзовое гитарное соло просто немыслимо без, как минимум, пары узнаваемых Кинговских фраз. Он остается и в высшей степени одаренным певцом, передающим каждый нюанс исполняемой песни.
<p>До сих пор Би Би Кинг остается по сути подлинно народной суперзвездой, совершенно доступным для всех кумиром, который приглашает посетителей в свою гримерную с шиком скромного изящества. С 1951 по 1985 год, Кинг постоянно занимал верхние позиции в чартах наиболее популярных исполнителей ритм-энд-блюза журнала Billboard. И он стал одним из немногих блюзменов, прекрасным аранжировщиком популярных хитов.
<p>Корни выдающегося таланта Кинга глубоко спрятаны в блюзовых местах Дельты Миссисипи. Если говорить точнее, Райли Би Кинг был рожден в Итта Бена, штат Миссисипи. Его детство не было простым. Молодой Райли "болтался" между домами матери и бабушки. Юность проходила в долгих днях работы на хлопковых полях и пении псалмов в церкви до переезда в 1943 году в Индианолу, другой город расположенный в самом сердце Дельты.

