<br>У "Helloween" появились проблемы с их компанией грамзаписи, и парни начали вести переговоры с несколькими известными лейблами, кто ранее проявлял к ним интерес. 
<br>Чтобы заполнить пробел группа выпустила "живой" альбом, записанный в Хаммерсмите в 1989 году. 
<br>Тогда же из группы уходит <b>Кай Хансен</b>, чтобы начать свой собственный проект <b>"Gamma ray"</b>. Ему на замену пришел Роланд Грапов. 
