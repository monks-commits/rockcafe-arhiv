Группа <b>"Helloween"</b> была сформирована в 1984 году в Гамбурге, Германия, на пепелище местных групп "Second Hell" и "Iron Fist".
<br>После издания двух треков, включенных в сборник "Death Metal", выпущенный фирмой "Noise Records" в 1984-м, на этом же лейбле в 1985 году вышел дебютный мини-альбом "Helloween". Группа получила признание с их уникальной маркой спид-пауэр-металла. 
<br>В 1988 году был выпущен "Keeper Of The Seven Keys Part II" . Его выходу сопутствовало успешное выступление на фестивале "Монстры рока" в Донингтоне. 
