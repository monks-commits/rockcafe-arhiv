<p> <b>Aria - History</b> 
<p>Aria (Russian: Ария) is a popular Russian heavy metal band that formed in 1985. As a leading band of the genre in Russia, they have been called "Russia's Iron Maiden". Since 1985 the band has gone through several line-up changes. Although vocalist Valerii Kipelov was the main voice of the band for the majority of the albums they have released, he departed after the release of Chimera and formed his own heavy-metal band "Kipelov", which released their first studio album "Rivers of times" ("Реки времен") in 2005. Rammstein has covered an Aria song, Shtil' (Штиль). In this cover version, the song's name is Germanized and spelled as Schtiel. Several ex-members of Aria formed the band Master, who are still considered, along with Aria, one of the most influential Russian metal bands.

