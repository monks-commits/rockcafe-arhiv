<p> <b>Lюк - About</b> 
<p>[журнал «НАШ»_№1_2003 г.] «… Lюк прорвало. Как обычно люки прорывает. Только 
  на смену привычно журчащему ручью из-под крышки люка испарялились другие субстанции. 
  Субстанции впитывались в одежду, волосы, мозги, заставляли видеть все иначе. 
  Ага, сила искусства. И вот Lюки на сцене.. накачивают людей своей позитивой. 
  А девочка им добрыми голосом подпевает про все. Про небеса, про асфальты, про 
  па-па-па, про дружбу и согласие. Только музыка слишком добрая, это чем так себя 
  раздобрить можно? Вроде погожим солнечным днем, сидишь на терраске, взбудоражен 
  острым ароматом кофе, под плетеным столиком тяжело дышит и чешется угомонившийся 
  сеттер. Единственное неудобство этого состояния заключается в мысли о том, что 
  ты находишься в дурдоме будущего. 
<p>В будущем люди еще не научатся лечить шизофрению, но смогут направлять больных 
  в правильное русло, делать их счастливыми. Сначала появится первый такой дурдом, 
  экспериментальный, экологичеки чистый, где все 100% шизофреников будут играть 
  на инструментах и улыбаться не только в ответ. Когда у них начнет хорошо получаться, 
  медперсонал организует им публичное выступление и своей музыкой шизофреники 
  будущего будут успокаивать и попускать остальных окружающих людей. </p>
<p>Музыка Lюка могла бы стать также идеальным саундтреком к воскресному шопингу 
  на Марсе и звучать в гуманоидском супермаркете, где торгуют исключительно воздушными 
  шарами. Марсиане рассеяны и несколько ленивы, поэтому забывают взять на входе 
  Корзину Потребителя Шаров. Без этой тачки им приходится слегка парить, ведь 
  шарики накачаны гелием. Нежелают они таскать тачки. Ладно, нужно сказать администрации 
  шарокового супермаркета, чтобы они запустили по верху воздушные потоки. Покупатель 
  будет в этом случае равномерно распределяться в пространстве. Особые потоки 
  будут их подгонять к нужной администрации полочки, набитой, например, шариками 
  со скидкой или новинками дизайнерской мысли. Ненаправленный марсианин глупо 
  сбивается в кучи, тучи и создает непродуктивные для торговли ситуации. Из всего 
  этого становится ясно, что музыка Lюка очень легкая, а их тоннели какие-то немэстные. 
  Несмотря на то, что некоторые тексты плодоносят благодяря родючей украинской 
  земле. Да, команда специально обученных украинскому языку инопланетян.»<br>
</p>


