<p> <b>"Spiritual Seasons" - About</b> 
<p>
Spiritual Seasons фолк-рок группа из Харькова, Украина.Созданная в 1994 году, группа до 2004 года существовала как студийный проект. Изначально стиль коллектива можно определить как Британский готический фолк.
<p>В 2001 году в репертуаре появляются ирландские народные песни и постепенно они становятся его основой .
<p>В 2004 году группа начинает концертную деятельность.
<p>Записано и выпущено 6 альбомов.
<p>Группа постоянно концертирует в таких клубах как "Арт-кафе Агата" (Харьков), "Gung'Ю'Bazz" (Донецк),"Docker Pub", "Пивная Бочка" (Киев)
<p>Участники фестиваля «Рыцарский Замок» 2005 и 2006 в Судаке.
<p>В мае 2006 года вышел новый, шестой студийный аудио альбом под названием ”All For Me Grog”.
<p>В июне 2007 года — прошла презентация МР3-диска группы.
<p>В репертуаре Spiritual Seasons стали появляться средневековые мелодии, финские народные песни и скандинавские боевые песнопения.

