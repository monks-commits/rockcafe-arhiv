<div align=justify>
<img src="queen.jpg" alt="<? echo $nam; ?>" width=193 height=200 hspace=10 vspace=3 border=0 align=left>
Возможно наиболее пользовавшаяся успехом британская группа прошлых двух десятилетий, <a href="http://www.rock-cafe.dp.ua/menu-mus-q/queen.php">"Queen"</a> начала свою историю в 1972 году. 
В конце 1972-го "Квины" подписали контракт с "EMI Records" и следующей весной начали свою концертную деятельность с выступления в Лондонском клубе "Marquee". 
Первый альбом представлял собой интересный сплав глэм-рока 70-х и хард-рока конца 60-х. 
<a href="http://www.rock-cafe.dp.ua/menu-mus-q/queen.php">"Queen"</a> много гастролировали и записали второй альбом, с помощью которого они выполнили их ранее данное обещание попасть в британский Топ 5. 
Вскоре сингл "Seven Seas Of Rhye" стал их первым хитом, попав в Топ 10, а альбом "Sheer Heart Attack" упрочил их коммерческий успех, достигнув 2 позиции в английских чартах. 
Диск "Killer Queen" стал первым альбомом группы, имевшим успех в Штатах.
<br>1975 год стал переломным в карьере группы. "Квин" записали шедевр "Bohemian Rhapsody", на котором Меркури преуспел в преобразовании семиминутного сингла в мини-оперу. 
Композиция занимала верхнюю строчку в британских чартах в течение девяти недель. 
Сопутствующий альбом, "A Night At The Opera", став бестселлером того времени, тоже лидировал в английских хит-парадах и не выходил из пятерки лучших в Штатах. 
Отныне <a href="http://www.rock-cafe.dp.ua/menu-mus-q/queen.php">"Queen"</a> стали суперзвездами. Все их альбомы, синглы и концерты пользовались большим успехом. 
В конце 1981 года "Королева" снова лидировала в Англии впервые со времен "Bohemian Rhapsody" с композицией "Under pressure".
<br>В конце 80-х студийная деятельность группы поубавилась, альбомы стали выходить пореже. Но это не уменьшило популярность группы, и диск 1991 года "Innuendo" вполне подтвердил это, заняв лидирующие позиции в английских хит-парадах. 
<br>Карьера группы закончилась со смертью от СПИДа вокалиста Фредди Меркури 24 ноября 1991 года. "Bohemian Rhapsody" была немедленно переиздана, чтобы собрать деньги для научно-исследовательских работ по борьбе со СПИДом, и тут же попала в верхние строчки британских и штатовских чартов. 20 мая 1992 года на лондонском стадионе "Уэмбли" состоялся концерт памяти Меркури, на котором выступало множество звезд, таких как Лайза Минелли, Элтон Джон, "Guns N'Roses", Джордж Майкл, Дэвид Боуи и Энни Леннокс ("Eurythmics"). Из оставшихся членов <a href="http://www.rock-cafe.dp.ua/menu-mus-q/queen.php">"Queen"</a> наиболее успешную сольную карьеру сделал Брайен Мей. Роджер Тейлор работал с "Cross". "Квины" не стали объявлять об официальном роспуске группы и в 1995 году появился их новый альбом "Made in Heaven", на котором были использованы последние записи Фредди, сделанные им перед смертью. 
