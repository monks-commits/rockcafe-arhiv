		<p><strong>Смерть 
          Джона Бонэма</strong></p>
<p>История Led Zeppelin прервалась внезапно, 25 сентября 1980 года Джон Бонэм 
  был найден мёртвым в спальне дома Джимми Пейджа после вчерашней пьянки. Пейдж, 
  Плант и Джонс собрались, чтобы ехать на следующие репетиции, но Бонзо никто 
  не видел. Посчитав, что он спит, Плант послал своего ассистента за Бонзо. Но 
  придя в спальню ассистент Планта увидел следующее: Бонэм лежал на спине в луже 
  рвотной массы. Джонс вызвал скорую, но барабанщик был уже мертв. Новость быстро 
  просочилась в СМИ и начала передаваться в каждом выпуске теле- и радионовостей 
  в Великобритании.</p>
<p>7 октября 1980 года следствие вынесло решение, что смерть Бонзо произошла в 
  результате несчастного случая. Выяснилось, что в тот день Джон принял много 
  водки, что вызвало рвоту. Рвотные массы попали в лёгкие, вследствие чего барабанщик 
  и умер. И хотя в организме присутствовали следы наркотиков, было установлено, 
  что они не способствовали его смерти.</p>
<p>10 октября 1980 года состоялись похороны Джона Бонэма в Рашоке, Вустершир, 
  в церкви возле фермы Бонзо. Останки были кремированы. На похоронах присутствовали 
  также Пол Маккартни и Джефф Линн. Поначалу появились слухи о том, что его могут 
  заменить Кози Пауэлл, Кармин Эппис, Барримор Барлоу, Саймон Кирк или Бив Биван, 
  однако в декабре группа выступила с заявлением, в котором объявила об окончательном 
  распаде.</p>
        <p><strong><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_04.jpg" alt="<? echo $nam; ?> photo" width="450" height="458"  hspace=10 vspace=3 border=0 align=left></a>Темная 
          сторона Led Zeppelin</strong></p>
<p>С самого начала у Led Zeppelin сложились крайне напряженные отношения с музыкальной 
  прессой. Корреспондент Rolling Stone впервые удостоился права сопровождать группу 
  лишь в 1975 году, да и то только благодаря тому что сначала солгал, будто работает 
  на Los Angeles Times, а потом произвел на Пэйджа впечатление своим глубочайшим 
  знанием творчества группы (это был 17-летний Камерон Кроу, впоследствии известный 
  автор и сценарист). Формально причины взаимной антипатии были очевидны:</p>
<p>Ненависть гитариста Led Zeppelin к «Роллинг Стоун» была неприкрытой. Наш журнал 
  опубликовал пять (!) негативных рецензий на альбомы ансамбля, но главное — разгромил 
  его дебютный диск. Этого Пэйдж простить не смог: он поклялся, что никогда в 
  жизни не даст журналу интервью. «Они пытались уничтожить мою группу, — говорил 
  он. — С какой стати я должен сейчас иметь с ними дело? Когда я нуждался в их 
  поддержке, они плюнули мне в лицо, теперь они хотят, чтобы я помог им продать 
  очередной номер. Почему я должен идти им навстречу?..» — Камерон Кроу, 13 марта 
  1975 года. <br>
  Причины негативного отношения рок-прессы к Led Zeppelin (если не считать природной 
  агрессивности Питера Гранта, которая — с одной стороны, многих отталкивала, 
  с другой — вписывалась в общий имидж ансамбля) имеют глубинный характер. Дело 
  в том, что с самого начала за группой закрепилась репутация тайных злодеев — 
  с одной стороны, интересующихся мистикой и черной магией, с другой — исповедующих 
  крайне правые взгляды, близкие к неофашистским (последнее автоматически делало 
  их изгоями в глазах журналистского корпуса). Ни первое, ни, тем более, второе 
  обвинение, не нашло себе прямых доказательств, и тем не менее уже с конца 60-х 
  годов рок-пресса объявила Led Zeppelin негласную войну.</p>
<p>Гастроли Led Zeppelin производили странное впечатление: с одной стороны — многотысячные 
  толпы, сияющие лимузины, лучшие отели; с другой — атмосфера подозрительности, 
  недомолвок, вечное ощущение какой-то отверженности. С некоторых пор на группу 
  навесили клеймо паршивой овцы английского хард-рока. Стоило Led Zeppelin выйти 
  в турне, как тут же наперерез им выпускали Rolling Stones. Первые имели над 
  вторыми явное преимущество и в любой момент способны были его доказать, но… 
  из газетных репортажей догадаться об этом было не так-то просто! В те годы группу 
  искуственно придерживали в тени. — Камерон Кроу, 13 марта 1975 года. <br>
  Лирика двух первых альбомов Led Zeppelin была нарочито «блюзовой»: у своих любимых 
  стандартов Вилли Диксона и Сонни Боя Уильямсона Пэйдж и Плант заимствовали весь 
  набор «романтических» штампов (с бесконечными: «baby baby baby…» и абсурдными 
  псевдо-эротическими метафорами (The Lemon Song). Несоответствие слащавых текстов 
  и взрывного, почти авангардного рока (Black Dog, Communication Breakdown) словно 
  бы обесценивало слова, намекая на то, что свое истинное «послание» группа, не 
  формулируя, доводит до слушателя иррациональными методами. Единственным реальным 
  симптомом «темной стороны Led Zeppelin» являлась в первые годы поистине дьявольская 
  энергетика группы, позволявшая ей превращать блюзовые номера с безобидными текстами 
  в сверхтяжелые пласты зловещего, подавляющего звука.</p>

        <a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_05.jpg" alt="<? echo $nam; ?> photo" width="532" height="160"  hspace=10 vspace=3 border=0 align=left></a> 
        <br clear="all">
         <br>

		
		<p>Первым 
          свидетельством того, что Пэйдж и Плант не такие простаки, какими пытаются 
          показаться, явилась Ramble On из второго альбома (оба зачитывались Толкиеном, 
          когда тот был еще андеграундным автором). А затем появилась Immigrant 
          Song с мастерски сработанным, сверхкомпактным и крайне оригинальным 
          текстом. Песня произвела на предубежденную прессу тяжелое впечатление: 
          после этого убийственно-мрачного «морского марша» викингов (с угрожающим: 
          «we are you overlords…») ни одна заметка о Led Zeppelin не обходилась 
          без упоминаний об «арийской внешности» Планта и его же (несомненно, 
          «болезненном») интересе к скандинавскому фольклору.</p>
<p>Слухи о том, что гитарист группы всерьез занимается магией, стали муссироваться 
  особенно активно после того, как стало известно, что тот поселился в особняке, 
  ранее принадлежавшем скандально знаменитому Алистеру Кроули. Позже, когда участникам 
  группы пришлось пережить ряд личных трагедий, общественное мнение склонилось 
  к тому, что таким образом пострадавшим пришлось расплачиваться с «темными силами» 
  за некие мистические эксперименты Пэйджа, позволявшего себе с этими силами заигрывать.</p>
        <p><strong><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_06.jpg" alt="<? echo $nam; ?> photo" width="425" height="400"  hspace=10 vspace=3 border=0 align=right></a></strong>Уже 
          в начале 70-годов многие пытались искать зашифрованные послания и намеки 
          в текстах группы, и та охотно давала поводы для такого рода исследований. 
          Текст Stairway To Heaven (в котором, несмотря на все старания энтузиастов, 
          сатанинского компромата найдено не было) набран из фразеологических 
          обрывков, словно бы выдернутых из некоего более полного текста, и создает 
          галерею завораживающе-неопределенных образов и пейзажей, толкования 
          которым до сих пор никто предложить не смог. О еще более зловещем тексте 
          Led Zeppelin, No Quarter (который выглядел особенно мрачно на общем 
          пасторально-умиротворенном фоне пятого альбома[2]), говорили даже, что 
          речь там шла о «рабочей ночи» отряда гестапо.</p>
<p>Когда в 1976 году Джон Бонэм и Питер Грант были арестованы за жестокое избиение 
  одного из служащих концертного зала (подчиненного Билла Грээма), журнал Rolling 
  Stone живописал это деяние с такой убедительной предвзятостью, словно нашел 
  окончательное доказательство того факта, что Led Zeppelin — это фашиствующие 
  сатанисты, в которых нет ничего человеческого. Слухи о реальных похождениях 
  музыкантов, свидетельствовавших об их не слишком джентльменском отношении к 
  женщинам (широкую огласку получил так называемый 'red snapper incident', когда 
  между ног одной из девушек-groupies была насильно всунута только что выловленная 
  из реки рыба) подавались прессой в качестве «доказательств» того, что участники 
  группы склонны к садизму.</p>
<p>Лишь спустя несколько лет после распада Led Zeppelin, когда Пэйдж и Плант начали 
  общаться с прессой, и мир увидел перед собой не монстров, а интеллигентных людей, 
  лишенных агрессивности, зловещая аура вокруг группы стала рассеиваться. Но нет 
  сомнения в том, что Led Zeppelin могли бы продержаться дольше и, возможно, изменить 
  к лучшему весь ход музыкальной истории, если бы рок-пресса и некоторые представители 
  музыкального бизнеса не пытались постоянно душить их в атмосфере злобной, необоснованной 
  подозрительности.</p>
<p><strong>Пост-Zeppelin`овское</strong></p>
<p>После распада группы все ее участники занялись сольной деятельностью. Джон 
  Пол Джонс вернулся с продюсерской деятельности (свой первый сольник «Zooma» 
  он выпустил лишь в 1999 году). Джимми Пейдж записал саундтрек к «Death Wish 
  II», после чего собрал коллекцию ранее отвернутых студийных треков и в 1982 
  году выпустил ее под названием Coda. В том же году у Роберта Планта вышел первый 
  (и очень тепло принятый) сольный альбом «Pictures at Eleven». В 1984 году Пейдж 
  и Плант встретились вновь: в составе почти спонтанно созданной группы Honeydrippers, 
  записавшей один мини-альбом. После этого Пейдж (вместе с Полом Роджерсом из 
  Free, Bad Company) образовал Firm.</p>
<p>В 1985 Пейдж, Плант и Джонс воссоединились для выступления на фестивале Live 
  Aid (в Филадельфии, на стадионе JFK). Для исполнения партии ударных были приглашены 
  Тони Томпсон и Фил Коллинз. Бывшие участники Led Zeppelin были недовольны своим 
  выступлением, которое по их просьбе было исключено из выпущенного в 2004 DVD 
  с материалами фестиваля.</p>
<p>Группа снова собралась на 40-летнем юбилее Atlantic Records (1988). Место покойного 
  отца за ударными в этом концерте занял Джейсон Бонэм.</p>
        <p><strong><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_07.jpg" alt="<? echo $nam; ?> photo" width="490" height="315"  hspace=10 vspace=3 border=0 align=left></a></strong>В 
          1990 году вышел (и стал мегахитом) бокс-сет из четырех CD, подготовленный 
          Пейджем, проведшим полный ремастеринг старых записей. Три года спустя 
          за ним последовал The Complete Studio Recordings – десятитомный бокс-сет.</p>
<p>12 сентября 2007 промоутер Харви Голдсмит официально объявил о том, что Роберт 
  Плант, Джимми Пейдж и Джон Пол Джонс вместе с Джейсоном Бонэмом объединятся 
  для благотворительного концерта памяти Ахмета Эртигана — основателя Atlantic 
  Records[1]. Концерт состоится 26 ноября 2007 года на площадке O2 Arena в Лондоне. 
  Поскольку среди поклонников группы ожидается большой ажиотаж, право покупки 
  билетов (цена — 125 английских фунтов, или 250 долларов США) будет разыгрываться 
  среди зарегистрированных пользователей сайта Ahmettribute.com.</p>
<p>Также известно, что в памятном концерте примут участие гитарист The Who Пит 
  Тауншенд, бас-гитарист Билл Уаймен, входивший в состав The Rolling Stones с 
  1962 по 1993 год, Паоло Нутини и группа Foreigner.</p>
