<p><strong>История</strong></p>
        <p><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_members.jpg" alt="<? echo $nam; ?> members photo" hspace=10 vspace=3 border=0 align=right></a>Led 
          Zeppelin (англ. Свинцовый дирижабль, но чаще употребляется транскрипция 
          Лед Зеппелин) — британская рок-группа, существовавшая между 1968 и 1980 
          годами. С тремястами миллионами [1] проданных альбомов Led Zeppelin 
          является самым успешным представителем хард-рока за всю его историю. 
          Судя по цифрам продаж, в США они являются самой коммерчески успешной 
          группой в любом жанре, после The Beatles.</p>
<p>Состав группы</p>
<p>Роберт Плант — вокал.<br>
  Джимми Пейдж — гитара.<br>
  Джон Бонэм — ударная установка, перкуссия.<br>
  Джон Пол Джонс — бас-гитара, клавишные.</p>
<p>Спустя три года после ухода Эрика Клэптона и через восемь месяцев после ухода 
  Джеффа Бека 7 июля 1968 года окончательно распалась группа «The Yardbirds». 
  Джимми Пейдж, оставшийся с правами на имя группы и концертными обязательствами, 
  был вынужден начать собирать новый состав. Первым приглашённым стал Джон Пол 
  Джонс (John Paul Jones), затем по рекомендации певца Терри Рида (Terry Reid) 
  к ним присоединился Роберт Плант (Robert Anthony Plant), приведший и ударника 
  Джона Бонэма (John Henry Bonham). Осенью они провели турне по Скандинавии под 
  названием «The New Yardbirds», на что ударник группы «The Who» Кит Мун (Keith 
  John Moon) заметил, что с таким названием они провалятся как свинцовый дирижабль 
  (англ. Lead Zeppelin). Пейдж недолго думая сменил название на «Led Zeppelin».</p>

<p> <strong>1969—1980</strong></p>

<p><strong>Led Zeppelin I</strong></p>

        <p><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_I.jpg" alt="<? echo $nam; ?> photo"  hspace=10 vspace=3 border=0 align=left></a> 
          Релиз первого альбома состоялся 17 января 1969 года в US и 28 марта 
          1969 года в UK. Назывался он просто — «Led Zeppelin». Утяжелённый блюз-рок 
          (который уже играли до этого Yardbirds, Cream, The Jimi Hendrix Experience), 
          получил здесь новое развитие. Пронзительно-высокий вокал Планта, мощная 
          работа ритм-секции и новаторская работа Пэйджа, создавшего собственную 
          разновидность заострённо-убойного риффа, позволила группе избавиться 
          от психоделических элементов и практически собственноручно создать новый 
          жанр — hard rock. Альбом, записанный за 30 часов и стоивший группе (по 
          свидетельству менеджера Питера Гранта) 1750 фунтов, к 1975 году собрал 
          7 миллионов. Альбом добрался до 10-го места в хит-параде США и 6-го 
          в UK, став в последствии «мультиплатиновым». После нескольких концертов 
          Led Zeppelin, заказы на первый альбом составили около 50 тысяч экземпляров.</p>
<br clear=all><br>


<p><strong>Led Zeppelin II</strong></p>
        <p> <a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_II.jpg" alt="<? echo $nam; ?> photo"  hspace=10 vspace=3 border=0 align=left></a> 
          За первый год своего существования группа успела провести 4 американских 
          и 4 британских концертных турне. Led Zeppelin II записывался в нескольких 
          американских студиях — по мере продвижения группы по стране. Испытывая 
          острый недостаток оригинального материала квартет как минимум три трека 
          («Whole Lotta Love», «The Lemon Song», «Bring It on Home») выстроил 
          на основе блюзовых стандартов, входивших в его тогдашний концертный 
          репертуар. Спонтанно созданный, импровизированный альбом вышел на 1-е 
          место в США и Великобритании. Он до сих пор присутствует в сотне лучших 
          рок-пластинок «Биллборда» и является одним из трех самых продаваемых 
          за всю историю США.</p>
<p>Вопреки желанию участников группы, считавших свои альбомы «неделимыми», Atlantic 
  Records выпустили сорокапятками укороченную (до 3:10) «радиоверсию» Whole Lotta 
  Love (за которой через год последовала Immigrant Song). Питер Грант и Led Zeppelin 
  не раз заявляли, что своими официальными синглами эти релизы не считают.</p>
<br clear=all><br>


<p><strong>Led Zeppelin III</strong></p>
        <p> <a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_III.jpg" alt="<? echo $nam; ?> photo"  hspace=10 vspace=3 border=0 align=left></a> 
          Проведя ещё несколько коротких гастролей в США (где каждый концерт длился 
          иногда по 3-4 часа из-за продолжительности импровизаций), группа для 
          работы над третьей пластинкой решила взять отпуск и удалилась в уединённое 
          валлийское местечко Bron-Yr-Aur (читается: Брон-эр-айр; в названии трека 
          «Bron-Y-Aur Stomp» на обложке была допущена ошибка). Эклектичный, отчасти 
          акустический Led Zeppelin III, стал также по обе стороны океана чарттоппером. 
          Поначалу сдержанно встреченный критикой, он был позже «реабилитирован» 
          и объявлен классическим. Выдающимися треками здесь считаются «Immigrant 
          Song» (любимая песня японских фанатов «цеппелинов») и «Since I’ve Been 
          Loving You».</p>

<br clear=all><br>


<p><strong>Led Zeppelin IV</strong></p>
        <p> <a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_IV.jpg" alt="<? echo $nam; ?> photo"  hspace=10 vspace=3 border=0 align=left></a> 
          К моменту выхода четвёртого альбома имидж группы заметно изменился: 
          участники группы стали появляться на сцене в роскошных кафтанах и в 
          украшениях, а гастрольные фургоны сменили на собственный самолет («The 
          Starship»). Группа стала снимать не отдельные номера, а целые секции 
          в отелях (в частности, в лос-анджелесском «Континентал Хайатт Хаус»), 
          где (под предводительством Бонэма и Гранта) нередко случались дикие 
          и подчас зловещие оргии, обеспечившие почву для целой отрасли цеппелиновской 
          мифологии.</p>
<p>Четвёртый альбом, вышедший 8 ноября 1971 года, вместо названия группы и заголовка 
  нёс на обложке четыре загадочных символа. Единственным, что указывало на принадлежность 
  пластинки, было упоминание имени её продюсера — Джимми Пейджа. Сегодня его принято 
  именовать Led Zeppelin IV (другие варианты заголовка: The Fourth Album, Four 
  Symbols, Zoso, Runes, Sticks, Man With Sticks). Со стороны Led Zeppelin это 
  был вызывающий жест в адрес средств массовой информации, которые к этому времени 
  сформировали крайне негативное отношение к группе и считали её репутацию раздутой 
  искусственно. Увлечённость группы фолк-музыкой и кельтским мистицизмом достигла 
  здесь апогея («The Battle Of Evermore»), но развитие получили также хард-роковые 
  и «металлические» тенденции («Black Dog», «When The Levee Breaks»). Обе линии 
  идеально соединились в «Stairway to Heaven» — песне, которая, хоть и не была 
  выпущена синглом, стала негласным чарттоппером англоязычного музыкального радио. 
  То, что вы слышите на записи «When The Levee Breaks» — это звучание коридора, 
  записаное через микрофон двумя лестничными пролётами выше. Плант написал текст 
  «The Battle Of Evermore» после того, как прочёл книгу о войнах в Шотландии. 
  Но для украшения мелодии Планту нужен был ещё один голос-противовес и тогда 
  группа обратилась к бывшей вокалистке группы Fairport Convention Сэнди Денни. 
  Композиция «Black Dog» появилась благодаря собаке, которая постоянно ошивалась 
  около поместья, никто не имел понятия как её звать и потому называли просто, 
  Чёрный Пёс. Led Zeppelin IV по праву считается одним из лучших альбомов всех 
  времён, а песня «Stairway to Heaven» (русск. «Лестница в небо») превратилась 
  в глобальный гимн рок-музыки. Существует даже байка о том, что одна американская 
  радиостанция после выхода альбома несколько суток непрерывно передавала в эфир 
  только эту песню. Сегодня известно, что Led Zeppelin IV — это самая продаваемая 
  хард-рок-пластинка в истории, на сегодняшний день только в США продано 45 миллионов 
  её экземпляров. Почти весь альбом был записан в поместье Headley Grange на передвижной 
  студии The Rolling Stones.</p>






        <p><strong><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_01.jpg" alt="<? echo $nam; ?> photo" width="450" height="293"  hspace=10 vspace=3 border=0 align=left></a>Houses 
          of the Holy</strong></p>
<p>Завершив сравнительно короткое турне (в поддержку сразу двух альбомов), группа 
  вновь взяла отпуск, который посвятила работе над пятым альбомом Houses of the 
  Holy, выпущенным весной 1973 года. Эксперименты по смешению стилей здесь были 
  продолжены: к базовым элементам (хард, хэви, блюз, фолк) добавились рэггей и 
  фанк. Последовавшие американские гастроли побили все кассовые рекорды, которые 
  до этого принадлежали The Beatles. Июльский концерт Led Zeppelin в «Мэдисон 
  Сквер Гарден» лёг в основу фильма «The Song Remains the Same», выпущенного три 
  года спустя. Завершив турне, группа весь 1974 год отдыхала от музыки, отчасти 
  посвятив его обустройству записывающего лэйбла Swan Song (названного так по 
  неизданной инструментальной композиции Джимми Пейджа), на котором впоследствии 
  выходили все её пластинки, а также альбомы Дэйва Эдмундса, Мэгги Белл, Detective, 
  Midnight Flyer, Bad Company, The Pretty Things.</p>
<p><strong>Physical Graffiti</strong></p>
<p>Первым релизом Swan Song стал двойной альбом «Physical Graffiti», вышедший 
  в феврале 1975 г. (записывать его начали в поместье Хэдли Грейндж, где было 
  подготовлено 8 треков: остальные были набраны из старых записей, не вошедших 
  в предыдущие релизы). Предварительные заказы на эту пластинку были примерно 
  15 млн долларов, так что не удивительно, что она стала четырежды «платиновой».</p>
<p>В мае 1975 года Led Zeppelin дали пять концертов в лондонском Эрлс-корте (в 
  2003 году эти пленки вышли на DVD и считаются лучшими живыми выступлениями в 
  карьере группы). Однако начавшиеся затем гастроли пришлось прервать после того, 
  как Роберт Плант попал в серьезную автокатастрофу, когда находился на отдыхе 
  в Греции. Сам вокалист отделался переломом лодыжки, но его жена Морин долгое 
  время находилась в критическом состянии и выжила лишь благодаря своевременно 
  проведенному переливанию крови.</p>
        <p><strong>Presence</strong></p>
        <p><strong><a href="http://rock-cafe.dp.ua/menu-mus-l/led-zeppelin.php" target="_blank"><img src="Led_Zeppelin_02.jpg" alt="<? echo $nam; ?> photo" width="203" height="152"  hspace=10 vspace=3 border=0 align=right></a></strong>Восстановившись 
          в течение полугода, Плант присоединился к остальным участникам группы 
          в работе над новым альбомом Presence: он вышел весной 1976 года, также 
          возглавил хит-парады многих стран мира, но был встречен критикой сдержанно 
          (равно как и последовавший за ним фильм «The Song Remains the Same»). 
          Весенние гастроли 1977 года вновь оказались прерванными: шестилетний 
          Карак, сын Планта, внезапно скончался от осложений после инфекционной 
          болезни кишечника. Лишь летом 1978 года Led Zeppelin приступили к работе 
          над восьмым студийным альбомом (в шведской студии Polar). В перерывах 
          они сумели провести короткое европейское турне и дважды сыграли в Небуорте 
          (эти концерты для английской аудитории оказались последними).</p>
<p><strong>In Through the Out Door</strong></p>
<p>In Through the Out Door (сентябрь 1979) стал всемирным хитом, хотя экспериментальным 
  звучанием и стилистическим разнообразием у критиков и фанов вызвал неоднозначную 
  реакцию. Аранжировки группы упростились, энергетическое напряжение спало: возобновились 
  подспудные разговоры о том, что причиной тому — тайное увлечение Пейджа героином 
  (начавшееся еще во время работы над «Presence»). В мае 1980 года Led Zeppelin 
  вышли в свое последнее европейское турне, а в сентябре собрались в особняке 
  гитариста группы, чтобы начать репетиции с новым материалом.</p>

