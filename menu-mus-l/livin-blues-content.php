Один из немногих представителей блюзового бума второй половины 60-х на европейской рок-сцене, этот голландский коллектив на протяжении целого десятилетия оставался едва ли не самым существенным музыкальным экспортом своей страны, за исключением <a class=white href=../menu-mus-s/shocking-blue.php><u>"Shocking Blue"</u></a>. 
Сделав ставку на традиционный репертуар, который состоял главным образом из ритм-энд-блюзов середины 50-х годов, группа начала со скромных домашних концертов, однако быстро нашла спрос на свою музыку в континентальной Европе, где принимала участие в различных рок-фестивалях. 
В 1969-м музыканты записали дебютный альбом "Hell's Session". Запись отличали мощный вокал Кристиансена, виртуозная гитара Оберга и техника Лагранда, искусно подражавшего Литтлу Уолтеру.
Альбом "Blue Breeze" (1976) стал самой успешной пластинкой коллектива, получившей "золотой" статус. 
Группа продолжала работать на концертах до конца 70-х годов, снова вернувшись к исполнению блюзовой классики.

