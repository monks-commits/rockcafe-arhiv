"Kansas" была сформирована в 1974 году в США. Вначале они назывались "White Clover". Хотя "Kansas" и были штатовской группой, в их творчестве было очень заметно влияние британских коллективов того времени типа <b>"Yes"</b> и <b>"Genesis"</b>. 
За первым альбомом "Kansas", имевшем умеренный успех, вышли еще два - и оба получили золотой статус, принеся команде широкую известность в США (хотя в Европе они были малоизвестны). 
<br>К 1977 году музыкантам начал надоедать приклеенный им прессой ярлык "прогрессив", и они решили попробовать более коммерческий подход. Их популярность была подтверждена 27 июня 1978 года на церемонии в "Madison Square Garden" в Нью-Йорке, на которой представитель "UNICEF" назвал "Kansas" послами доброжелательности. 
<br>Однако саунд группы становился слишком уж коммерческим, чего не смог выдержать Уолш. В начале 80-х он принял решение оставить коллектив. Но в октябре 1986-го Уолш, Эхарт и Вильямс решили возродить "Kansas" при участии Билли Грира (бас) и талантливого гитариста Стива Морса. Воссоединение было ознаменовано выпуском "Power", альбома, на котором саунд группы сместился от джаз-рока ранних релизов к "харду". "Power" буквально через две недели после выпуска получил "платину". 
<br>На сессиях альбома 2000 года "Somewhere To Elsewhere" собрались вместе все шестеро основателей "Kansas". 
