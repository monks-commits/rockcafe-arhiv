Возможно, наиболее категоричный образец прогрессив-рока, "King crimson" был сформирован в январе 1969-го на пепле эксцентричного "Giles, Giles And Fripp".
<br>Дебютный альбом группы "In The Court Of The Crimson King" привел в экстаз критиков, а Пит Тауншенд из 
<a class=white href=../menu-mus-t/the-who.php>"The Who"</a>
 назвал его "сверхъестественным шедевром". Экспансивное использование меллотронов делало "King crimson" чем-то сродни 
<b>"Moody Blues"</b>, но прогрессии сложных аккордов Фриппа и сильное вступление "21st Century Schizoid Man" показывали редкое воображение.
<br>В апреле 1972 года команда распалась. Коллинз, Уоллис и Баррелл занялись сольными студийными записями
<br>В октябре 1974 года Фрипп, занявшийся сольной карьерой объявил что с "King crimson" покончено. Но в 1981 году гитарист взял свои слова обратно, возродив группу.
<br>Последовавшие альбомы "Discipline", "Beat" и "Three Of A Perfect Pair" показали, что не все еще потеряно и команда способна на многое. 
Однако, спустя некоторое время Фрипп возобновил свое индивидуальное творчество и начал новый проект "League Of Gentlemen". 
Впоследствии он работал под вывеской "The League Of Crafty Guitarists", но в 1994-м возобновил деятельность "King crimson", чтобы записать альбом "Thrak".
