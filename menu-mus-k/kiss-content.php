<img src="kiss.jpg" alt="<? echo $nam; ?>" width=250 height=165 hspace=10 vspace=3 border=0 align=left>
"Kiss" были основаны в 1972 году в США.
В течение первого года "Киссы" выпустили первые три альбома, но с минимальным успехом. 
Летом 1975 их фортуна повернулась к ним с выпуском "Alive!", хитовая вещичка ("Rock’n’roll all nite") откуда поднялась на 12-е место в штатовских чартах в ноябре. 
<br>Привлекательность "Kiss" всегда была основана на их живых шоу: гримировка, возмутительные костюмы и пиротехнические эффекты на сцене, наряду с их хард-роковыми гимнами создавали великое рок’н’рольное шоу. Их концертная репутация побуждал фанов скупать миллионы их пластинок и "Alive" стал их первым платиновым альбомом в США. 
<br>"Kiss" в 1977 возглавил престижный опрос института Гэллапа как наиболее популярный ансамбль в США. Товары с символикой "Киссов" расходились влет. Были выпущены пара комиксов про них и даже снят фантастический фильм с их участием "Kiss встречают призрака". Армия их фанов приблизилась к шестизначному числу. В сентябре 1978 все четыре члена группы выпустили по сольнику в один и тот же самый день. 
Вышедший в 1981 году потом альбом "Music from the elder" представил радикальный отход от традиционной музыки "Kiss" и включал в себя несколько баллад, оркестр и хор. 
<br>К 1983 популярность "Киссов" все уменьшалась, и те приняли решительные меры. Легендарный грим, который скрыл их истинные лица на 10 лет был удален на передаче MTV в США. Фотографии без грима появились и на альбоме "Lick it up", который попал в первую десятку в Британии. Последующие альбомы подтвердили нежелание "Kiss" покидать поле боя просто так.
<br>В 1998 "Psycho Circus" отметил возвращение первоначального состава к студийной работе и достиг первых позиций в чартах США. В истории, охватывающей три десятилетия, воздействие музыки "Киссов" на сознание поколения фанов, особенно в США, остается огромным. 
