Следующий 1968 год принес еще больший успех. Гастроли Хендрикса проходили при переполненных залах, а две долгоиграющих пластинки "Аxis: Bad As Love" и другая пластинка "Electric Ladyland" - стали золотыми, то есть разошлись тиражом более чем в миллион экземпляров. Казалось бы, для счастья было все: слава, деньги, неограниченные возможности работать, но жизнь Хендрикса не ладится - у него трения с товарищами. После ссоры с Ноэлем Реддингом он проводит ночь в тюрьме, вскоре наступает разрыв с менеджером Чандлером, группа "Experience" распадается. В 1969 году в Торонто он попадает в неприятности из-за наркотиков, и, даже переехав в Нью-Йорк, не может уже избавиться от внимания полиции. В августе 1970 года он выступил в последний раз на фестивале на острове Уайт, а 18 сентября стало известно о внезапной смерти Хендрикса на квартире некой Моники Даннерман в Лондоне. От барбитуратного отравления его стошнило и неудачно сделанный вдох вскоре вызвал удушье -- так, по крайней мере, было объявлено официально. К сожалению, карьера Хендрикса продолжалась недолго. Известным он был около 4 лет, однако за это короткое время он значительно расширил границы рок-музыки, оставив о себе память, как об одном из самых значительных и влиятельных инструменталистов рока наших дней. 
