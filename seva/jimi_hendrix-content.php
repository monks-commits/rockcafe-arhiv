<b>James Marshall "Jimi" Hendrix</b> (November 27, 1942 – September 18, 1970) was an American musician, singer, songwriter, guitarist, innovator, and cultural icon. Lauded by music fans and critics alike, Hendrix is considered by many to be the most influential and talented electric guitarist in rock music history. He achieved worldwide fame in 1967 playing in the Monterey Pop Festival, then headlined the iconic 1969 Woodstock Festival before his sudden death in 1970 at the age of 27.
<br><br>
A self-taught musician, the left-handed Hendrix played a right-handed Fender Stratocaster guitar turned upside down and re-strung to suit him. As a rock guitarist, Hendrix exploited and integrated the sonic tools of feedback and distortion into his music to an extent that previous pioneers (such as The Kinks' Dave Davies, The Yardbirds' Jeff Beck and The Who's Pete Townshend) had never achieved. He built upon the innovations and influence of blues stylists such as B.B. King, Albert King, Buddy Guy, T-Bone Walker, and Muddy Waters, as well as rhythm and blues and soul guitarists like Curtis Mayfield, and the traditions of jazz. Hendrix was also inspired by rock pioneer Little Richard, having toured in Richard's back-up band "The Upsetters" before forming his own group in 1966.
<br><br>
<b>Hendrix</b> strived to combine what he called "earth", a blues, jazz, or funk driven rhythm accompaniment, with "space", the high-pitched psychedelic sounds created by his guitar improvisations. He also integrated instruments rarely used in rock, such as the harpsichord, recorder, and glockenspiel. As a record producer, Hendrix was an innovator in using the recording studio as an extension of his musical ideas: he was notably one of the first to experiment with stereophonic and phasing effects during the recording process. Hendrix was also an accomplished songwriter whose compositions have been performed by countless artists.

<br><br>
<b>Jimi Hendrix</b> was inducted into the United States Rock & Roll Hall of Fame in 1992 and the UK Music Hall of Fame in 2005. His star on the Hollywood Walk of Fame (at 6627 Hollywood Blvd.) was dedicated in 1994. In 2006, his debut album Are You Experienced was inducted into the United States National Recording Preservation Board's National Recording Registry. In 2003, Rolling Stone magazine named Hendrix number one on their list of the "100 greatest guitarists of all time".
<br><br>
<b>Names</b>
<br><br>
<b>Jimi Hendrix</b> was born Johnny Allen Hendrix, the son of Al Hendrix and Lucille Jeter Hendrix, in Seattle, Washington on November 27, 1942. As a toddler and young boy he was known as Buster, a family nickname inspired by the early 20th century comic strip character Buster Brown. In 1946, Al changed the legal name of his son to James Marshall Hendrix, which it remained until his death. As a school-age boy and young adult, he was simply known as Jimmy or James. In his early career, Hendrix used the stage name Maurice James and later Jimmy James. He did not assume the moniker Jimi until after his discovery in 1966, although most writings refer to him as Jimi throughout the timeline of his life for purposes of consistency.
