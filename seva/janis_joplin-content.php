src="http://www.rock-cafe.dp.ua/menu-mus-j/janis.jpg" alt="Джанис Джоплин - Janis Joplin" hspace=10 vspace=3 border=0 align=left></a>

She was cremated in the Westwood Village Memorial Park Cemetery in Westwood, California, and her ashes were scattered into the Pacific Ocean. The album Pearl, released six weeks after her death, included a version of Nick Gravenites' song "Buried Alive In The Blues", which was left as an instrumental because Joplin had died before she was able to record her vocal over the backing track.
<p>
The movie The Rose, with Bette Midler in the lead role, was loosely based on Janis's life.
<p>
Janis Joplin is now remembered best for her powerful, distinctive voice — her rasping, overtone-rich sound was significantly divergent from the soft folk and jazz-influenced styles that were common at the time — as well as for her lyrical themes of pain and loss.
<p>
Many comparisons can be drawn with her close contemporary Jimi Hendrix, who was similarly catapulted to fame by his appearance at Monterey, and whose career burned bright for a few years, but who also died from drug-related causes within weeks of Joplin. But unlike Hendrix, whose fame continued to grow after his death, Janis did not enjoy a significant revival of public interest until the late 1990s.
<p>
In part this was due to the fact that she made a relatively small number of recordings during her career, and because she was not a prolific songwriter. By comparison, although Hendrix released only three official LPs in his lifetime, he was both a prolific songwriter and a tireless studio worker, laying down many albums' worth of material, that has continued to be released in the decades since his death.
<p>
Joplin's contributions to the rock idiom were long overlooked, but her importance is now becoming more widely appreciated, thanks in part to the recent release of the long-unreleased documentary film Festival Express, which captured her at her very best. Janis's scorching vocal style, her flamboyant dress sense, her outspokenness and sense of humour, her liberated stance and her strident, hard-living "one of the boys" image all combined to create an entirely new kind of female persona in rock.
<p>
Although there were some notable exceptions, it can be argued that, prior to Janis, there was a tendency for solo female pop performers to be pigeonholed in to a few broadly-defined roles -- the gentle, guitar-strumming 'folkie' (e.g. Judy Collins, Joni Mitchell), the virginal 'pop goddess' (Doris Day, Rosemary Clooney) or the cool, elegantly dressed chanteuse (Dusty Springfield, Diana Ross). In her attitude, image, repertoire and performance style, Janis was pivotal in redefining what was possible for female singers in popular music.
<p>
She was arguably one of the only white female singers of the period who could generate the same kind of power and excitement as black performers like Aretha Franklin or Tina Turner, and who could sing blues and R&B with total conviction. Alongside Grace Slick of Jefferson Airplane she pioneered an entirely new range of expression for women in the previously male-dominated world of post-Beatles rock. It is also notable that, in a very short time, she transcended the role of "chick singer" fronting an all-male band, to being an internationally famous solo star in her own right.
<p>
In terms of her visual image, Janis is also notable as one of the few female performers of her day to regularly wear pants (or slacks), rather than skirts or dresses. Another trademark was her flamboyant hair styles, often including coloured streaks and accessories such as scarves, beads and feathers, a style strikingly at odds with the 'regulation' perms or wigs sported by most female singers of the day. It is especially remarkable that she is probably the only major female pop-rock star of the period who never wore makeup -- a stance that was very striking (and undoubtedly quite provocative) at a time when the wearing of makeup was still considered to be de rigeur for women.
<p>
