src="http://www.rock-cafe.dp.ua/menu-mus-j/joplin_janis.jpg" alt="Джанис Джоплин - Janis Joplin" hspace=10 vspace=3 border=0 align=right></a>

Многие критики считают, что, возможно, восхождение Джанис на музыкальном небосклоне было чересчур головокружительным. Количество пластинок, записанных ею, например, совсем не соответствовало ее популярности. Таким образом, Джанис пришлось как бы оправдывать свою известность, обосновывать свою высокую репутацию. Джанис, и ранее отличавшаяся несколько буйным характером, стала жить лихорадочно, истерически, каждый день в ее жизни казался буквально последним. С вновь собранным оркестром Джанис приступает к записи новой пластинки "Pearl" ("Жемчуг") - это было ее прозвище среди музыкантов. 11 песен новой пластинки были уже записаны и вдруг утром 4 октября 1970 года Джанис Джоплин была найдена мертвой в своем номере гостиницы в Голливуде. Причина смерти - большая доза героина. 

<p>
Пластинка "Pearl" вышла посмертно -- в нее вошло все, то что успели записать, и то, что было не совсем доделано. В 1974 году из документальных лент был составлен фильм "Janis". Вскоре появились две книги - биографии Janis Joplin, одна из них "Buried Alive" ("Погребенная заживо"), была ее подругой и сотрудницей Мирой Фридман, другую книгу "Going Down With Janis" ("Опускаясь вместе с Джанис"), написала Пегги Касерта. Тем временем бывший оркестр Джанис "Big Brother And The Holding Company" благополучно пережив разрыв со своей известной вокалисткой, нашел себе другую певицу и продолжает выпускать пластинки. О, превратности судьбы! 

