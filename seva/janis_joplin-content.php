Итак, наша сегодняшняя программа посвящена певице Джанис Джоплин. Американская певица из Техаса Джанис Джоплин  (Janis Joplin) родилась 19 января 1943 года в городе Порт-Артуре. Название пусть вас не смущает, поскольку в Соединенных Штатах есть также города Одесса, Санкт-Петербург и Москва, только, может быть, размером поменьше. На техасских просторах детство Джанис проходило в достатке, но в некотором одиночестве. Ее любимой музыкой были записи блюзовой певицы Бесси Смит и потому не удивительно, что в начале 1960-х годов она начала свои первые выступления с оркестром, исполнявшим блюзы и country-music. 

<p>
В 1964 году Janis Joplin ненадолго становится студенткой Техасского университета, однако вскоре вновь она уезжает в Сан-Франциско и начинает петь с популярным танцевальным оркестром "Big Brother And The Holding Company", а вскоре оркестр и певица записывают на фирме Mainstream Records свою первую пластинку. 

<p>
Звезда Джанис начала подниматься с выступлений ее вместе с оркестром на Монтеррейском фестивале в 1967 году - том самом фестивале, который сделал известными Джими Хендрикса и Отиса Реддинга. Печальным совпадением, однако, стало и то, что все три быстро взошедшие звезды через несколько лет при различных обстоятельствах погибли. 

<p>
Известность Джанис быстро увеличивалась, и в январе 1968 года она подписывает контракт с известным импрессарио Альбертом Гроссманом. Оркестр "Big Brother" и Janis Joplin записывают пластинку "Cheap Thrills" ("Дешевое удовольствие"), который уже в сентябре 1968 года расходится тиражом более миллиона экземпляров. Однако уже к концу года становится очевидно, что певица и оркестр находятся на разных уровнях популярности, и как следствие безжалостного капиталистического закона спроса и потребления, сотрудничество Джанис Джоплин с оркестром заканчивается. 

<p>

