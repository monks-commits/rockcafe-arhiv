The band's big break came with their performance at the Monterey Pop Festival, which included a version of Big Mama Thornton's "Ball and Chain" and featured a barnstorming vocal by Joplin. (The D.A. Pennebaker documentary Monterey Pop captured Cass Elliott in the crowd silently mouthing "Wow, that's really heavy" during Joplin's performance.) Their 1968 album Cheap Thrills featured more raw emotional performances and together withthe Monterey performance, it made Joplin into one of the leading musical stars of the late Sixties.
<p>

