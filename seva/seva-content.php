<i>"Рок-посевы" - еженедельная передача о популярной музыке, выходившая в эфир Русской службы Би-би-си 
в течение 27 лет - с 10 июня 1977 года по 12 июня 2004 года. Именно благодаря этой получасовой программе 
имя Севы Новгородцева стало известным на территории Отечества: в те годы она была единственным независимым 
источником информации о западной музыке, доступным для советских меломанов, а сам Сева, таким образом, стал 
первым русским диск-жокеем, родоначальником жанра музыкальной радиопередачи. 
</i>

