Группа была основана в 1969 году в Бирмингеме (Англия), гитаристом К.К. Даунингом (Кеннет Даунинг) и его другом, басистом Яном Хиллом. Свой первый концерт они отыграли в 1971-м в Эссингтоне с вокалистом Аланом Аткинсом и ударником Джоном Эллисом. Название "Judas Priest" было взято от предыдущей группы Аткинса (а тот взял его из песни Боба Дилана, "The Ballad Of Frankie Lee And Judas Priest").
<br>Дебютный альбом группы "Rocka Rolla" вышел в сентябре 1974 года.
В 1975 году появление группы на фестивале в Рединге привлекло к ней внимание намного более широкой аудитории. Диск "Sad Wings Of Destiny" был по-лучше, чем его предшественник. 
Сингл "Take On The World" стал их первым британским хитом. 
<br>В 1979-м во время японского тура был записан концертник "Unleashed In The East". После глобальных туров с "Kiss" и "AC/DC" популярность "Judas Priest" приобрела новый импульс. 
После появления в 1980 году на фестивале "Монстры рока", группа приступила к записи "Point Of Entry". 
Период "Screaming For Vengeance" был феноменально успешным для "Judas Priest". 
Великолепный сингл "You've Got Another Thing Comin'", сопровождался прибыльным шестимесячным американским туром. Сам же альбом получил в Штатах платину. 
На "Ram It Down" группа вернулась к чистому тяжелому металлу.
<br>В 1997 году "Judas Priest" записали альбом "Jugulator" с новым вокалистом Риппером Оуэнсом.
