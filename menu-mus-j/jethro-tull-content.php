Английская группа "Jethro Tull" (это имя ученого-аграрника, жившего около 200 лет назад), образовалась в Лютоне (Великобритания) в 1967 году. 
<br>Весной 1968 года ансамбль успешно выступил на Виндзорском фестивале джаза. Критики охарактеризовали его как восходящую звезду арт-рока.
<br>Первый диск группы, возглавляемой лучшим флейтистом рок-музыки, появился в конце 1968 года. Это единственный проект "Jethro Tull", композиции которого базировались на блюзовой гитаре (это манера Мика Абрахамса). 
<br>Первый же диск, записанный с участием гитариста Мартина Барре, "Living in the Past", оказался на третьей строчке британских хит-парадов. 
Начиная со второго альбома стиль "Jethro Tull" укрепился в рамках тяжелого арт-рока с выраженными гитарными риффами и удивительными импровизациями флейты Андерсона. 
Такого в истории рок-музыки до сих пор не было, и ансамбль очень быстро занял место в символической пятерке самых новаторских групп мира вместе с "Beatles", "Rolling stones", "Genesis" и "Led zeppelin". 
Начиная с 1970 года продукция "Jethro Tull" имела большой успех во всех странах, но от диска к диску композиции группы становились все более сложными и глубокими, что (невзирая на высочайшее качество музыкального материала тех лет) в 1973 году привело к конфликту с критиками музыкальных изданий, обвинивших ансамбль в излишней "вычурности" и "заумности". 
<br>Из дискографии в период с 1970 по 1980 год трудно выделить какой-то один диск в качестве лучшего. Легче указать на те, что чуть слабее: это диск 1974 года и альбом 1979 года. В концептуальном плане наиболее глубокомысленны философские работы 1972, 1973, 1975, 1978 и 1980 годов. 

