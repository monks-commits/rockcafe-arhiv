Джо Кокер (Joe Cocker) родился 20 мая 1944 года в городе Шеффилде. Английский певец. 
<br>Еще со времен обучения в техучилище Джо сохранил за собой многие привычки, в частности, склонность к пиву - за вечер он обычно выпивал за 10 кружек. В 15 лет играл на ударных и гармонике в местной группе The Cavaliers, а годом позже начал петь. Свой первый сингл, песню Beatles, I'll Cry Instead, он записал на фирме Decca в 1964 году. Вернувшись в Шеффилд, он набрал себе группу сопровождения под названием Grease Band, ансамбль часто выступал в Шеффилде и гастролировал по северу Англии, играя негритянский ритм-энд-блюз и соул. 
<br>Почти два года отсиживался измотанный и увлекшийся наркотиками Джо у родителей в Шеффилде. За это время он сделал лишь одно поверхностное концертное выступление в 1971 году в Шеффилд-Сити-Холле, где в пении ему помогала Рита Кулидж и вокальный ансамбль Dixie Flyers, когда-то поддерживавший the Byrds. 
<br>В качестве «приглашенной звезды» Кокер некоторое время работал с группой THE CRUSADERS — результатом этого сотрудничества стал хит-сингл I'm So Glad I'm Standing Here Today. Невероятный коммерческий успех имел и альбом 1982 Sheffield Steel, который Джо Кокер записывал на Багамских островах вместе с президентом фирмы Island Records Крисом Блэкуэллом и великолепной ритм-секцией в лице Слая Данбара и Робби Шекспира. 
<br>В том же 1982 Кокер возглавил хит-парад синглов с композицией Up Where We Belong (в дуэте с Дженнифер Уорнз) — из популярного фильма «Офицер и джентльмен». В 1984 Джо Кокер подписал свой первый с начала 70-х годов долгосрочный контракт, и сейчас, когда позиции певца в США и Великобритании остаются достаточно высокими и положение его более чем стабильное, в Германии Кокер продает миллионы своих альбомов, как новые, так и переиздания. 
