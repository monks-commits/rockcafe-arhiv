<img src="../menu-mus-j/jarre.jpg" alt="<? echo $nam; ?>" width=200 height=160 hspace=10 vspace=3 border=0 align=left>
Жан-Мишель Жарр родился 24 августа 1948 в Лионе. С раннего возраста он купался в мире музыки - его отец был знаменитым композитором Морисом Жарром.
С пяти лет Жан-Мишель учится играть на пианино. Одновременно с занятиями в средней школе, он посещает уроки и игры на фуге в Парижской консерватории. Под влиянием англо-саксонской музыки 60-х годов он начинает играть на электрогитаре в группах-однодневках. С группой "Mystere IV " Жарр получит первый приз Фестиваля Улиц в Париже.
<br>
Получив диплом о филологическом образовании, он продолжает учиться музыке. Под покровительством Pierre Schaffaeffer он входит в "Группу музыкальных исследований" в 1968. Там он открывает сольфеджио, затрагивающее совокупность всех звуков окружающего нас мира. Тогда он начинает создавать свою собственную студию звукозаписи, приобретает за бешеные деньги электронные музыкальные инструменты.
<br>
В 1969 в "Группе музыкальных исследований" Жарр создает электро-акустическую пьесу "Hapiness is a sad song", предназначенную для дома культуры города Реймс. В этом же году Жарр сочиняет "La Cage" и "Erosmachine", два инструментальных отрезка для магнитного альбома. Его стиль и принадлежность к электронной музыке становятся очевидными.
<br>
В 1971 он выпускает первый альбом, "Deserted Palace", который заинтересовал лишь нескольких эксцентриков.
<br>
Диск "Oxygene" станет мировым успехом, многократно золотым диском, он будет на первой строчке мировых хит-парадов. Жан-Мишель Жарр будет даже избран человеком года в Соединенных Штатах. Музыка Oxygene будет использована в качестве заставки во многих телепередачах; этот альбом будет саундтреком в австралийском фильме " Gallipoli" в 1981.
За все время было продано 10 миллионов копий "Oxygene"!
<br>
Его второй альбом, "Equinoxe",будет иметь успех в 35 странах, и будет продан тиражом 7 миллионов экземпляров.
<br>
"Магнитные Поля" ("Magnetic Fields"), его 3-й альбом, вышедший в 1981, также будет иметь большой успех. Он поднимется на вершину Top 10 самых продаваемых альбомов в Европе и станет многократно золотым диском.
<br>
Жан-Мишель Жарр по праву является одним из самых известных французских композиторов: до сегодняшнего дня было продано 55 миллионов его альбомов.
