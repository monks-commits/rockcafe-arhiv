<img src="john-lee-hooker.jpg" alt="<? echo $nam; ?>" width=146 height=200 hspace=5 vspace=5 border=0 align=left>
Джон Ли Хукер (John Lee Hooker) родился 22 августа 1917 года в Кларксдейле, Миссисипи (США) — американский блюзовый певец, гитарист. 
<br>
В детстве пел в церкви. В 15 лет отправился в Мемфис, где работал билетером в музыкальном театре, пел на улицах. Вернулся в Миссисипи на короткое время, затем вновь отправился странствовать с группой ЧЕТВЕРО ИЗ ФЭРФИЛДА (FAIRFIELD FOUR), затем с группой THE BIG SIX, которая исполняла религиозную музыку.
<br>
В 1943 в Детройте работал дворником на автомобильном заводе, выступая по вечерам в небольших клубах черных кварталов и в пивных для рабочих. Первые попытки записать пластинку были неудачными до встречи с Бернардом Бесменом (Bernard Besman), пианистом и бизнесменом. Бесмен оценил необычность стиля Джона Ли Хукера и постарался в записи подчеркнуть ярко индивидуальные особенности его исполнения. 
<br>
Выпущенный в 1948 сингл с блюзом Boogie Chillen возглавил ритм-энд-блюзовый чарт и разошелся тиражом около миллиона экземпляров. Сегодня эта песня считается блюзовой классикой. Следующими хитами стали Crawlin Kingsnake (1949) и Im In The Mood (1951).
<br>
Со второй половины 50-х годов Джон Ли Хукер записывается в Чикаго на фирмах Chess или Vee-Jay. Две его песни этого периода — Dimples (1956) и Boom Boom (1962) — имели значительный успех в Великобритании, оказав огромное влияние на формирование британского блюза. 
<br>
К концу 70-х годов на фоне общего падения интереса публики к блюзу Джон Ли Хукер значительно теряет популярность, но продолжает выступать, не изменяя своему стилю, работая в основном в маленьких кафе и записываясь в полудомашних студиях энтузиастов блюза. Записи этого периода ныне относятся к числу наиболее значительных в истории блюза (альбомы Alone, Cream и др.) 
<br>
В 1980 его имя включено в Зал славы основателей блюза (Blues Faundations Hall Of Fame). Альбом 1986 впервые за сорок лет его карьеры удостоен премии Grammy. Казалось, это итог, на самом деле Джон Ли Хукер стоял на пороге нового блестящего успеха. 
<br>
В 1989 выходит альбом Hiller, записанный при поддержке старых друзей: Карлос Сантана (Santana), Бонни Рэйт (Bonnie Raitt), Роберт Крей (Robert Cray), Джордж Сорогуд (Geprge Thorogood) и других. Альбом был настоящим триумфом — высокие места в хит-парадах многих стран, Grammy, мультиплатиновый статус и видео в Hot Rotation на MTV. 
<br>
В 1990 его имя занесено в Зал славы рок-н-ролла (RocknRoll Hall of Fame). Концерт в его честь в Madison Square Garden обернулся чествованием блюза, на котором его и других блюзовых ветеранов приветствовали эстрадные и рок-звезды. К его 70-летию звезда с его именем установлена на голливудском бульваре звезд.
<br>
50-летие профессиональной музыкальной карьеры Джон Ли Хукер отметил альбомом, записанным при участии певца-пианиста Чарлза Брауна, его старого друга еще со времен первых записей в Детройте.
<br>
День смерти: 21/06/2001
