<div align=justify>
<img src="rockets.jpg" alt="<? echo $nam; ?>" width=200 height=150 hspace=5 vspace=5 border=0 align=left>
История "Rockets" началась в 1972 году в Париже, где они под вывеской "Crystal" играли в клубах. 
На сцене появлялись в обычном человеческом обличье - в нормальной одежде и с длинными волосами. 
В 1974 году "Crystal" изменили название на "Rocket men", и вот пять пришельцев с зелеными глазами (контактные линзы), серебряной кожей и в "космической" одежде впервые появились перед европейской публикой, записав сингл "Rocket Man". 
<br>В 1975-м название группы поменялось на "Rockets". В 1976 году во Франции вышла первая пластинка под названием "Rockets". 
<br>Следующая пластинка, "On the Road Again", вышла в 1978-м. Заглавная песня - одноименный хит конца 60-х годов.
Альбом "Plasteroid" помог "Rockets" закрепить свой успех в Испании и Германии. В Италии он получил статус "золотого" еще до того, как поступил в продажу - только по заказам магазинов. 
<br>По мнению критиков, следующий альбом, "Galaxy", был лучшим в их дискографии.
Начиная с альбома "Pi 3.14" группа изменила свой имидж - не только пришельцы, но и колдуны, ковбои и т.д. 
Впервые были представлены женский вокал ("Ideomatic") и электронные барабаны. 
<br>Весной 1982-го появился новый сингл "Radio Station"/ "Star Vision". Эти песни в несколько измененном виде были включены в следующий альбом, вышедший через несколько месяцев. Это был последний диск, записанный "Rockets" в оригинальном составе. Хотя альбом сохранял традиционный стиль, пресса и публика отнеслись к нему прохладно. 
В 1985 году группа записала альбом "One Way" и поменяла название на "Roketz". 
С изменением названия окончательно изменилась и их музыка и внешность. 
<br>После семилетнего молчания, в 1992 году, вышел альбом "Another Future". 
<br>В октябре 2000-го в Италии вышел официальный сборник лучших песен "The Definitive Collection" на двух дисках, включающий несколько ранее не издававшихся песен, причем лучшие классические вещи "Rockets" были записаны с винила. 
