<br>В 1958 году Джеггер стал участником блюзовой группы "Little Boy Blue & The Blue Boys". 
В 1962 году в "Jazz News" впервые промелькнуло название "Rolling Stones". А 12 июля состоялось первое официальное выступление группы в лондонском клубе "Marquee". 
28 апреля Эндрю Луг Олдхэм, бывший PR-менеджер "Beatles", и Эрик Истон посетили концерт "Rolling Stones" и на следующий же день подписали с группой контракт. 
Именно Олдхэму принадлежала идея создать для "Стоунз" имидж плохих мальчиков, бунтовщиков и секс-символов, который контрастировал бы с "домашним" образом "Битлз". 
<br>В апреле 1964-го вышел первый LP группы, "The Rolling Stones". Бурные овации сопровождали выступление группы на награждении победителей голосования "New Musical Express" на стадионе "Уэмбли". 8 июля "It's All Over Now" стала №1 в Британии. В 1965-м вышел второй альбом "Rolling Stones".
<br>29 августа вышел сингл "Satisfaction"/"Spider And The Fly". Песня "Satisfaction" тут же стала всемирным рок-гимном. 
В конце 1964 года по результатам читательского голосования в "Record Mirror" "Stones" оказались впереди "Beatles". 
В 1966-м вышел "Aftermath", ставший первым "стоунзовским" LP №1. 
В 1971-м на прилавках появился "Sticky Fingers", впервые изданный на собственном лейбле группы, "Rolling Stones Records". 
В 1978 году вышел альбом "Some Girls", на котором чувствовалось явное влияние диско и панка.
<br>Лишь в марте 1989-го все пятеро, наконец, собрались вместе и договорились о новой записи, начавшейся в мае в Лондоне. В августе вышел долгожданный альбом "Steel Wheels", принятый на ура.
<br>В 1991-м на церемонии "Ivor Novello Awards" "Rolling stones" получили награду за выдающийся вклад в британскую музыку. 
