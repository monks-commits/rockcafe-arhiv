Эта канадская хард-рок-группа c 1969 по 1972 год давала концерты в клубах и барах Торонто, исполняя музыку в стиле "Cream".
Качество живых выступлений группы постепенно улучшалось и "Rush" играли сначала на разогреве "New York dolls" во время канадского тура, а впоследствии отправились по Штатам вместе с "ZZ Top". 
Музыка "Rush" отличалась неординарно высоким голосом Ли, чрезвычайно мощным звуком гитары, особенно в ранние годы, 
и научно-фантастическими и фэнтэзийными текстами Нейла Перта. 
Позже он также использовал идеи авторов типа Джона Барта, Габриэля Гарсии Маркеса и Джона Доса Пассоса. 
Этот подход достиг своего зенита в концептуальном альбоме 1976 года "2112".
В 1979 "Rush" стали широко известны во всем мире, и канадское правительство присвоило им звание "официальных послов музыки". 
С 1982 года трио склонилось к исполнению более "песенного", доступного широкой публике рока, с искоренением сольных инструментальных вставок и увеличению роли электроники вплоть до появления электронной перкуссии. 

