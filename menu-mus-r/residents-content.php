Пожалуй, самая загадочная группа, "Residents", на протяжении всей своей карьеры так ни разу и не открыла имена и лица своих участников. 
На публике музыканты всегда появлялись в смокингах, высоких шляпах и масках в форме большого глазного яблока. 
Интервью "резиденты" также старались особо не давать. 
По официальной версии команда была основана в Сан-Франциско, где в начале 70-х годов собрались четверо ее участников. 
Свое название группа получила, когда ей вернули первую демоленту, посланную на "Warner Bros", с пометкой "for the attention of residents". 
<br>В 1972 году вышла первая пластинка "Santa Dog", изданная тиражом в 300 экземпляров. Их разослали разным известным личностям, начиная от Фрэнка Заппы и кончая президентом Никсоном. В 1973-м вышел первый лонгплей "Meet the Residents", продажи которого были сразу же приостановлены из-за конверта, на котором была изображены "битлы" с вампирскими зубами. 
<br>Следующий релиз 1974 года "Not Available" представлял собой экскурс в неоклассицизм. В 1976 году вышел студийник "Third Reich 'N' Roll", представлявший собой коллекцию золотых хитов поп-музыки, прошедших оригинальную переработку.
<br>Став культовой андеграундной группой, коллектив в конце 70-х выпустил три альбома, "Duck Stab/Buster & Glen", сборник арктических мелодий "Eskimo" и 40-минутную компиляцию рекламных роликов "Commercial Album".
<br>Начало 80-х ознаменовалось серией прог-роковых пластинок, объединенных темой сражения между вымышленными племенами Молов и Чабов. 
<br>Также в 1984 году стартовала серия "Американский композитор", в которую вошли обработки композиций Гершвина, Джеймса Брауна, Элвиса Пресли, Хэнка Вильямса и других. 
<br>Свое двадцатипятилетие группа отметила выходом ретроспективной компиляции "Our Tired, Our Poor, Our Huddled Masses". 
<br>В 2000 году вышел DVD "Icky Flix", включавший подборку видеоматериалов, а также ранние и новые треки. 
