Американский певец, композитор, гитарист Рой Келтон Орбисон родился 23 апреля 1936 года в Техасе, США.
<br>
Свою первую группу Рой Орбисон организовал в 1949 году, и когда The Wink Westerners появились на техасском радиоконкурсе талантов, их номер единогласно был признан лучшим. Став студентом университета штата Техас, Рой Орбисон собрал новую группу The Teen Kings. Благодаря поддержке и личному участию Пэта Буна, с которым Рой Орбисон учился на одном факультете, музыка группы стала более "рок"-ориентированной. Другой товарищ певца, Джонни Кэш, услышав любительские записи The Teen Kings, настоял, чтобы Рой Орбисон послал фонограмму Сэму Филипсу, главе фирмы грамзаписи Sun Records. Первая же композиция, которая попала в руки Филипсу - а это была знаменитая сегодня "Ooby Dooby", - потрясла великого открывателя талантов: техника гитарной игры "туэнгинг" и практически оперное бельканто новичка коренным образом отличались от той музыки, которую "раскручивал" Филипс. В 1956 году "Ooby Dooby" стала первым хитом Роя Орбисона, заняв в национальном хит-параде 59-е место. И хотя Рой Орбисон сейчас считается классиком рокабилл<br>
и, все же его голос "лучше приспособлен" для исполнения баллад, и потому материал, записанный для Sun Records не выдержал сравнения с более поздними работами Роя Орбисона. 
<br>
Перебравшись в Нэшвилл, Рой Орбисон подписал контракт с фирмой Acuff-Rose Publication. На новом месте он записывает композицию "Claudette" (она была посвящена его жене), которая становится хитом в исполнении Everly Brothers. И начинается целая серия хит-синглов: "Only The Lonely" (1960 год, США - 2-е место, Англия - 1-е), "Blue Angel" (1960 год США - 9-е место, Англия - 11-е), "I'm Hurtin'" (1961 год, США - 27-е место), "Candy Man" (1961 год, США - 25-е место), "Running Scared" (1961 год, США - 1-е место, Англия - 9-е), "Crying" (1961 год, США - 2-е место, Англия - 25-е) "The Crowd" (1962 год, США - 26-е место), "Leash" (1962 год, США - 25-е место), "Dream Baby" (1963 год, США - 4-е место, Англия - 2-е), "In Dreams" (1963 год, США - 7-е место, Англия - 6-е), "Falling" (1965 год, США -22-е место, Англия - 9-е), "Mean Woman Blues/Blue Bayou" (1963 год, США - 5-е место, Англия - З-е) и многие-многие другие. 
<br>
Феноменальный успех Роя Орбисона в Великобритании помимо всего прочего объясняется и триумфальными гастролями певца по этой стране вместе с The Beatles (1963 год). В то время в состав группы Роя Орбисона входили: Бобби Голдсборо, гитара, и Дьюи Мартин, ударные (последний позже перешел в Buffalo Springfield). 
<br>
Для лучших работ Роя Орбисона того периода - а специалисты считают, что 1960 - 1964 годы были пиком его успеха, - характерны романтические темы, к тому же певец наконец-то нашел способ наиболее эффектной подачи своего замечательного голоса. Концерты Роя Орбисона проходили с неизменным успехом. 
<br>
Успех, как известно, чередуется с неудачами - у Роя Орбисона это были трагедии: в 1966 году в автокатастрофе гибнет его жена Клодетт, а через год двое его сыновей заживо сгорели во время пожара в доме. С этого момента карьера Роя Орбисона неуклонно катится под гору (однако концертное турне по Великобритании в 1969 года оказалось все же удачным), но музыкант нашел в себе силы сразиться с судьбой и выиграл эту схватку. Несмотря на относительное затишье в 70-х и проблемы со здоровьем (в 1979 году Рою Орбисону была сделана операция на сердце), в начале 80-х он гастролирует с Eagles, а его официальное возвращение на сцену (1981 год) сопровождалось восторженными рецензиями даже в самых скептически настроенных музыкальных изданиях мира. Его бессмертная "Oh Pretty Woman" обрела второе рождение в исполнении бесчисленных групп и солистов, а группа Van Halen сделала ее своим "прорывным" хитом (1982 год). 
<br>
В 1988 году Рой Орбисон входит в состав Travelling Wilburys, группы, которую организовали его друзья Джордж Харрисон, Боб Дилан, Джефф Лини и Том Петти, но после выхода дебютного альбома этого коллектива в 1988 году Рой Орбисон умер (7 декабря 1988). Певец и композитор оставил огромное аудио- и видеонаследство. 



