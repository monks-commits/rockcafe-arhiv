<br>Одной из самых первых песен, сочиненных квартетом, оказалась "I Don't Wanna Walk Around with You", затем "I Don't Wanna Be Learned, I Don't Wanna Be Tamed" (не вошедшая в одноименный дебютный альбом группы, но оказавшаяся на его переиздании в серии "All that Stuff & and More" ("Warner"/"Sire", 1991), после нее "I Don't Wanna Go Down to The Basement" и "Now I Wanna Sniff Some Glue". 
<br>Первый альбом "Ramones", одноименный названию коллектива, вышел 23 апреля 1976 года. 
<br>7 января 1978 года - день, когда "Ramones" дали свой первый большой концерт в Нью-Йорке, сыграв аншлаговое представление с "Runaways".
