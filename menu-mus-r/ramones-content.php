<br>"Ramones" дали свой первый концерт 30-го мая 1974 года в "Perfomance Studio", New York в составе трио: Joey Ramone (Jeff Hyman, 19.05.51-15.04.2001) - барабаны, Johnny Ramone (John Cummings, 08.10.1951) - гитара, Dee Dee Ramone (Douglas Colvin, 18.09.1952-05.06.2002) - бас, вокал. Однако уже к следующему выступлению (16.08.74, CBGB, New York) группа приняла привычный фэнам всей планеты формат квартета благодаря примкнувшему к составу Tommy Ramone (Tommy Erdelyi, 09.01.1952) - барабаны (Joey стал вокалистом).
