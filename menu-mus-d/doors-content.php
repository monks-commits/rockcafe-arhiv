"Doors" были основаны в Лос-Анджелесе в 1965 году студентами Джимом Моррисоном и Рэем Манзареком. 
Манзарек как-то услышал, как Моррисон исполнял свою песню "Moonlight Drive". 
Песня Рэю очень понравилась, и он уговорил Джима присоединиться к его группе "Rick And The Ravensask". 
Завербовав еще к себе ударника Джона Денсмора, они вскоре сделали запись шести песен Моррисона. 
К тому времени группа была переименована Моррисоном в "Doors".
В 1967 были выпущены два первых диска. 
Грандиозный дебютный альбом представлял собой высококачественный сплав рока, блюза, классики, джаза и поэзии. Композиция "Light my fire" с него стала визитной карточкой группы. Сингл с этой песней тотчас взлетел на вершину хит-парадов. 
За короткое время "Doors" стали культовой группой для многих миллионов людей. 
В 1970-м группа выпустила "Morrison Hotel", альбом по силе равный их первому релизу. В апреле 1971-го последовал еще один мощный альбом "L.A. Woman", имевший более блюзовый саунд. 
<br>По официальной версии Моррисон умер 3 июля 1971 года от сердечного приступа, хотя было очевидно, что это произошло из-за передозировки наркотика.
Оставшиеся члены "Doors" продолжили свою музыкальную деятельность в составе трио (вокалистом был Манзарек). Они выпускали еще два неплохих альбома, но без Моррисона это были уже не те "Doors". В 1973 году команда была расформирована.
Спустя 5 лет, в 1978-м Манзарек, Кригер и Денсмор воссоединились и записали музыку к стихам которые надекламировал Моррисон на пленку в течение сессий "L.A. Woman". 
Альбом, названный "An American Prayer", имел шумный успех. Это привело к выпуску концертника "Alive She Cried", сделанного из архивного материала и вышедшего в 1983 году. В 1985-м фотография Моррисона появилась на обложке журнала "Rolling stone". Подпись к ней гласила: "He's Young, He's Hot, He's Sexy and He's Dead". 
