<img src="def-leppard.jpg" alt="<? echo $nam; ?>" width=200 height=133 hspace=5 vspace=5 border=0 align=left>
Эта вечно популярная английская хард-роковая группа была сформирована в 1977 году в Шеффилде (Йоркшир) под названием "Atomic Mass".
Затем команда сменила вывеску на "Def Leppard". 
В июле 1978 года "Def Leppard" дебютировали в Вестфилдской школе перед аудиторией из 150 детей. 
В 1979 году "Def Leppard" записали дебютный EP на "Bludgeon Riffola Records".
Позднее в том же году группа играла на "разогреве" Сэмми Хагара и <a class=white href="../menu-mus-a/ac-dc.php"><u>"AC/DC"</u></a> во время гастролей этих музыкантов по Англии. 
Их дебютный альбом "On Through The Night", вышел в 1980 году и сразу же вскарабкался на 15 позицию в британских чартах. 
"Pyromania" (1983) стала наиболее успешным альбомом "Def Leppard", поднявшись на вторую строчку в американских хит-парадах.
<br>Накануне 1984 года ударник Рик Аллен попал в автокатастрофу, в результате которой он потерял левую руку. Но группа не бросила в беде своего барабанщика и не возобновляла работу, пока Аллен не придумал специально разработанный комплект ударных, на большинстве которых можно было играть с помощью ног. 
Из-за этих событий была задержана запись "Hysteria", которая была наконец выпущена в 1987 году и разошлась 15-миллионным тиражом по всему миру. Этот диск возглавил хит-парады по обе стороны Атлантики. 
Альбом "Adrenalize" был выпущен в марте 1992 и сразу возглавил английские и американские хит-парады.
<br>"Def Leppard" выступил на концерте памяти Фредди Меркури на стадионе Уэмбли. 

