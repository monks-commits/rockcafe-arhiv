Марк Нопфлер родом из Глазго, Шотландия.
Он изучал английскую литературу в университете Лидса, работал младшим репортером в "Yorkshire Evening Post" и местной газете. 
После окончания университета он был учителем, а по вечерам играл в пабе в группе "Brewer's Droop".
Для музыкантов, исполняющих спокойную музыку, тогда был "не климат", поскольку в те времена господствовал панк. 
Ни один из британских лейблов не хотел брать под крыло "Dire straits". 
К концу 1977 года группа записала свой дебютный альбом с одноименным названием. 
В Штатах продажами диска "Dire straits" занялась известная "Warner Brothers Records" и тот попал на 2-ю строчку в "Billboard". 
Диск "Communique" разошелся тиражом в три миллиона по всему миру.
"Живой" двойник "Alchemy" заполнил промежуток перед следующим выпуском студийного альбома группы - "Brothers in arms". 
Этот альбом стал одним из самых продаваемых альбомов десятилетия. Композиция "Money For Nothing" стала номером 1 в США. Три других песни: "Walk Of Life", "So Far Away" и "Brothers in arms" стали лидерами хит-парадов по обе стороны Атлантики.  
<br>В марте 1995 года вышел альбом группы, названный "Live at the BBC"; он был записан еще до того, как группа стала знаменита - в 1978 году. 

