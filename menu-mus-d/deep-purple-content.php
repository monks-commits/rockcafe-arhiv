    <td colspan=2 valign=top class=menu1><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="deep-purple.jpg" alt="<? echo $nam; ?> photo" width=200 height=227 hspace=10 vspace=3 border=0 align=left></a> 
      <strong>Deep Purple</strong> (<strong>Дип Пёпл</strong>, Глубокий пурпур) 
      — британская рок-группа, играющая в жанре хард-рок. Была образована в 1968 
      году под названием Roundabout. В апреле того же года название было изменено 
      на Deep Purple. 
      <p>Группа считается одной из самых заметных и влиятельных в рок-музыке 
          1970-х, одними из основателей жанра хэви-метал (хотя причисление самих 
          Deep Purple к металу остается спорным) и повлиявшими на всю последующую 
          «тяжелую» музыку. Музыканты Deep Purple, в особенности гитарист Ричи 
          Блэкмор, считаются образцом виртуозной игры.</p>
        
      <p><strong>Предыстория</strong></p>
        <p>Инициатором создания группы и автором изначальной концепции стал барабанщик 
          Крис Кертис (известный по участию в The Searchers), который в 1967 году 
          прославился хитом Let’s Go To San Francisco, выпущенным под псевдонимом 
          The Flowerpot Men. Именно Кертис решил, что ядром нового коллектива 
          должны стать: он сам, его брат, а также клавишник Джон Лорд (Jon Douglas 
          Lord, род. 9 июня 1941 года в Лестере) и юный гитарист Ричи Блэкмор. 
          Лорд (к тому времени получивший классическое музыкальное образование, 
          увлекавшийся джазом и в качестве сешшнмэна принявший участие в записи 
          You Really Got Me The Kinks) с энтузиазмом принял это предложение.</p>

      <H1><font color=#50024F><? echo $nam; ?></font></H1>
<? require("../inc/banner_wide.php"); ?>
<br clear="all"><? require("../inc/banner_link_hor.php"); ?>

        <p>Группу назвали Roundabout: в ее состав вошли Крис Кертис (вокал), его 
          брат Дэйв (вокал), Блэкмор (гитара), Лорд (клавишные), Ник Симпер (бас) 
          и Бобби Вудман (ударные), который в 1950-х годах под псевдонимом Бобби 
          Кларк играл в группе Винса Тейлора (англ.: Vince Taylor). Симпер успел 
          поиграть в нескольких группах, самой известной из которых была Johnny 
          Kidd &amp; The New Pirates.</p>
        <p>Едва только Кертис увлек всех своей идеей «психоделической рок-карусели» 
          с меняющимися участниками, как сам же к ней охладел. Хотел было уйти 
          и Блэкмор (живший тогда в Гамбурге), но Лорд и Симпер уговорили его 
          остаться — по крайней мере на время репетиций в Дании. Почти сразу же 
          Ушел Вудман, но появились вокалист Род Эванс и барабанщик Иэн Пэйс: 
          эти двое играли в The MI5 (группе, которая позже под названием The Maze 
          выпустила два не имевших успеха сингла в 1967 году).</p>

        <p>В числе вокалистов, прошедших прослушивание, был и Род Стюарт, который, 
          по воспоминаниям Симпера, «был ужасен». Группа даже пыталась переманить 
          Майка Харрисона из Spooky Tooth, но тот, как вспоминает Блэкмор, «не 
          хотел об этом и слушать». С 22-летним Эвансом у микрофона группа провела 
          короткое турне по Дании и тут же изменила название — на Deep Purple.</p>
        
      <p><strong>1968—1973</strong></p>
        
      <p><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="deep_purple_03.jpg" alt="<? echo $nam; ?> photo" width=300 height=404 hspace=10 vspace=3 border=0 align=left></a>Весь 
        материал дебютного альбома <strong>Shades Of Deep Purple</strong> был 
        создан за два дня, в течение почти непрерывной 48-часовой студийной сессии 
        в древнем особняке Хайли (Балькомб, Англия).</p>
        <p>Первым синглом была выпущена Hush американского канти-сингера Джо Саута. 
          Однако, за основу группа взяла версию Билли Джо Ройала, с которой только 
          и была в тот момент знакома. Сингл вышел в июле 1968 года на Parlophone, 
          не имел успеха в Британии, но, к изумлению участников группы, попал 
          в первую пятерку «Биллборда».</p>
        <p>Второй альбом The Book Of Taliesyn (октябрь 1968 года) группа сконструировала 
          по той же формуле, основные надежды связав с кавер-версиями. Kentucky 
          Woman и River Deep — Mountain High имели умеренный успех, но его оказалось 
          достаточно, чтобы протолкнуть пластинку в американскую «двадцатку».</p>
        
      <p><strong>Deep Purple</strong> (1969) ознаменовал переход <strong>Deep 
        Purple</strong> к более тяжелой музыке, примером чему может служить Lalena. 
        Синглом, впрочем, вновь был выпущен кавер — Hallelujah (I Am The Preacher), 
        в альбом не вошедший. Этот сингл является первой записью Гиллана и Гловера 
        в группе. Основное развитие группы в те дни просиходило на концертной 
        сцене: музыка ужесточалась день ото дня. Свой последний концерт американского 
        турне DP дали в первом отделении Cream. После них хэдлайнеров зрители 
        свистом согнали со сцены! Центральной фигурой ансамбля постепенно стал 
        Блэкмор, разработавший своеобразный метод игры со «случайным шумом» (путем 
        манипуляций с усилителем). Именно он призвал коллег последовать путем 
        <a href="../menu-mus-l/led-zeppelin.php">Led Zeppelin</a> и <a href="../menu-mus-b/black-sabbath.php">Black 
        Sabbath</a>. Новый поворот потребовал радикальных перемен: группу покинули 
        Эванс и Симпер, на их место пришли Иэн Гиллан и Роджер Гловер, причем 
        в связке (точно так же в свое время Эванс привел Пэйса).</p>
        <p>Сначала Гловер, получив предложение, от него отказался, но уже на следующий 
          день изменил решение. «Первое время я чувствовал себя крайне неловко, 
          давая автографы на обложках, где был изображен Ник», вспоминал он. Очень 
          скоро, однако, стало ясно, что его сочный, мощный бас становится «якорем» 
          нового звука. И что драматический, экстравагантный вокал Гиллана идеально 
          соответствует новому радикальному пути развития, по которому направился 
          Блэкмор.</p>
        
      <p><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="deep-purple1.jpg" alt="<? echo $nam; ?> in rock photo" width=200 height=196 hspace=10 vspace=3 border=0 align=left></a>Первый 
        альбом нового состава Concerto For Group And Orchestra (в исполнении <strong>Deep 
        Purple</strong> and the Royal Philharmonic Orchestra), не содержал ни 
        намека на истинный потенциал квинтета. Записанный на концерте в Ройал 
        Алберт-холле 24 сентября 1969 года, он был выпущен (в США) три месяца 
        спустя и остался в истории группы не более чем своеобразным «псевдоклассическим» 
        курьезом. Классический purple-саунд реализовался в <strong>Deep Purple 
        In Rock</strong> (сентябрь 1970). Альбом имел огромный успех по обе стороны 
        океана и в первой альбомной «тридцатке» Британии продержался более года. 
        На тех же студийных сессиях был записан трек Black Night, выпущенный синглом 
        и ставший всемирным хитом.</p>
        
      <p>В июле 1971 года вышел <strong>Fireball</strong> и повторил успех предшественника. 
        К этому времени <strong>Блэкмор</strong>, давший волю собственной эксцентричности, 
        стал в Deep Purple «государством в государстве». «Если Ричи захочет сыграть 
        соло на 150 тактов, он сыграет его и никто не сможет остановить его», 
        — говорил Гиллан в интервью Melody Maker в сентябре 1971 года. Впрочем, 
        той зимой о разногласиях все забыли: участники группы сплотились вновь 
        перед лицом трудностей, с которыми столкнулись в ходе работы над следующей 
        пластинкой.(Cм. Smoke On The Water) Альбом Machine Head вышел в марте 
        1972 года, поднялся до 1-го места в Британии и разошелся 3-миллионным 
        тиражом в США, где сингл Smoke On The Water вошел в первую пятерку «Биллборда».</p>
        
      <p><a href="http://www.rock-cafe.dp.ua/menu2.php" target="_blank"><img src="deep-purple2.jpg" alt="<? echo $nam; ?> album photo" width=200 height=197 hspace=10 vspace=3 border=0 align=left></a>Повторить 
        этот успех было практически невозможно: неудивительно, что Who Do We Think 
        We Are (декабрь 1972 года) разочаровал всех, в том числе самих музыкантов. 
        Записанный на вилле неподалеку от Рима (где опять-таки местные жители 
        не уставали спрашивать: «Что они вообще о себе думают?» — отсюда и заголовок) 
        в разгар внутренних неурядиц, альбом оказался нервным и рваным по настроению, 
        хоть и содержал сильные вещи — «стадионную» Woman From Tokyo и оригинальную 
        Mary Long (пример сатирической публицистики, характерный, скорее, для 
        панк- чем для хард-рока).</p>
        <p>Словно бы предчувствуя скорый конец, ансамбль решил записать свое последнее 
          совместное выступление в Японии, которое оказалось лучшим за всю их 
          историю. «Идея живого альбома — добиться как можно более естественного 
          звучания всех инструментом при энергетической подпитке от публики, которая 
          способна вытянуть из группы такое, что она никогда не смогла бы создать 
          в студии», — говорил Блэкмор. Альбом Made In Japan по праву считается 
          одним из трех лучших концертников в истории наряду с «Live At Leeds» 
          The Who и «Get Yer Ya-Ya’s Out» (The Rolling Stones)</p>
        <p>Иэн Гиллан и Роджер Гловер объявили о своем уходе из Deep Purple одновременно. 
          Первый на время покинул музыкальную сцену, занявшись мотоциклетным бизнесом, 
          и вернулся три года спустя с Ian Gillan Band. Второй сконцентрировался 
          на продюсерской деятельности.</p>
        <p>В июне 1973 года трое оставшихся участников Deep Purple пригласили 
          вокалиста Дэвида Ковердэйла (который к тому времени работал в фэшшн-бутике) 
          и поющего басиста Гленна Хьюза (экс-Trapeze). В феврале 1974 года был 
          выпущен Burn, альбом выдающийся, но к прежним Deep Purple имевший лишь 
          косвенное отношение.</p>
        
