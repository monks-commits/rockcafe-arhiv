<div align=justify>

Родившийся 4 марта 1951 г., Миддлсбро (Великобритания), наполовину ирландец, наполовину итальянец Крис Ри увлекся гитарой под впечатлением от работ Рая Кудера и Джо Уолша.
Первый инструмент он приобрел, когда ему исполнилось 19 лет. 
Вокалистом группы MAGDALENE, в которой играл Крис, был некто Дэвид Кавердэйл, в 1973 г. ушедший в <a class=white href="../menu-mus-d/deep-purple.php"><u>DEEP PURPLE</u></a>. 
В мае 1974 г. MAGDALENE выпустили сингл "So Much Love", через год сменили название на THE BEAUTIFUL LOSERS и были названы журналом "Melody Maker" "лучшими новичками года". Однако в августе 1977-го Крис Ри решил начать сольную карьеру.
Первым вкладом музыканта в дело развития мирового рока стало его участие в проекте Хэнка Марвина "The Hank Marvin Guitar Syndicate" в ноябре 1977 г.
Дебютный альбом "Whatever Happened To Benni Santini?" (US-49), записанный с помощью Рода Арджента и Пита Уингфилда, был выдержан в спокойной, приятной AOR-овой стилистике и напоминал отчасти Брюса Спрингстина, отчасти (вокалом) Барри Уайта. 
В последующие пять лет Крис записал в той же манере четыре альбома, самый успешный из которых поднялся до 44-й позиции в британских чартах.
В середине 80-х Крис Ри понял, что его основная аудитория находится в континентальной Европе, и резко увеличил количество гастрольных поездок, в результате чего в Ирландии и ФРГ музыкант приобрел статус суперзвезды.
Успех на родине пришел в 1986 г. - альбом "On The Beach" занял 11-е место, одноименная композиция - 57-е. 
В 1989 и 1991 гг. гитарист выпустил два прекрасных альбома - "The Road To Hell" и "Auberge", которые возглавили британский хит-парад.
Музыкант еще раз подтвердил свою репутацию альбомом 1997 г. "The Blue Cafe".
  

