<b>Чак Берри</b> (Chuck Berry) родился 18 октября 1926г. в Чикаго.
<br>Это был человек, который все сочинял сам — и слова, и музыку. Он писал песни, настолько чутко отражавшие жизнь молодежи 50-х, ее проблемы, ее любовь, ее подспудные желания, что его часто называют первым поэтом рока. 
