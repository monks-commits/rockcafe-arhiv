<br>Наряду с Элвисом, Чак Берри был наиболее влиятельным артистом рок-н-ролла. Более того, он один из самых влиятельных артистов рока вообще. Всякий раз, когда рок заходит в тупик, кто-нибудь возвращается к корням, вновь открывает энергию, остроумие, оригинальность Берри и черпает в нем вдохновение. 
