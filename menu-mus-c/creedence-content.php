Участники группы начали выступать вместе еще в школьные годы в далеком 1959-м. 
Первоначально известный как "Blue Velvets", затем "Tommy Fogerty And The Blue Velvets", квартет был довольно популярным в окрестностях родной Калифорнии.
В период с 1965-го по 1967-й год группа записала семь синглов. К концу 1967-го квартет полностью перешел на профессиональную работу и окончательно поменял название на "Creedence Clearwater Revival".
"Криденс" принесли с собой в мир рок-музыки совершенно новый музыкальный саунд: в основе их стиля лежала сильно утяжеленная смесь традиционного рок-н-ролла и ритм-энд-блюза. Такие вещи с их дебютного альбома как "Suzie-Q" и "I Put A Spell On You" буквально ворвались в штатовские чарты. Выпущенный спустя несколько месяцев диск "Bayou country", закрепил славу Фогерти, как перспективного, талантливого композитора, а группа стала одной из ведущих в Америке конца 60-х. Композиция "Proud Mary" вошла в десятку лучших как в Новом так и в Старом Свете. Она представляла смесь южных креольских напевов, ритм-энд-блюза и рокабилли. 
Отныне и вплоть до 1972 года каждое новое произведение "Криденс" становилось бестселлером.
Оригинальный стиль калифорнийцев получил тогда свое название – "свамп-рок", хотя главное было конечно не в стиле, а в признании его уникальности.
<br>Пик творчества "Creedence" пришелся на выпуск диска "Cosmo's factory".
Этот диск по достоинству стал бестселлером 1970 года. 
В 1993-м "Криденс" был внесен в Зал Славы Рок-н-ролла.


