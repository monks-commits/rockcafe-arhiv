Хотя "Cream" просуществовали немногим более двух лет, их влияние на развитие рока очень большое, как в конце 60-х, так и после распада группы. "Cream" стали первой супергруппой, заложившей фундамент блюз-рока и хард-рока конца 60-х - начала 70-х. Проект был основан в середине 60-х годов. 
Ошеломляющие соло гитары Эрика Клэптона значительно выделяли звучание группы, но надо отдать должное и Джеку Брюсу, ответственного за формирование саунда "Cream", и исполнявшего большинство материала своим богатым голосом. Джек также написал лучшие песни группы, часть из которых была создана в сотрудничестве с поэтом Питом Броуном. 
В начале своей деятельности "Cream" сконцентрировались на наэлектризованном традиционалистском блюзе, который доминировал в их первом альбоме, "Fresh cream", попавшем в начале 1967-го в британский Топ 10. 
Но команда не зацикливается на блюзе, что нашло подтверждение на следующей пластинке - "Disraeli gears". 
На ней "Cream" исполняли бодрящий психоделический поп, состоявший из множества незабываемых мелодий и эффективной гармонии с "хрустящими" риффами. 
<img src="cream3.jpg" alt="<? echo $nam; ?>" width=200 height=150 hspace=5 vspace=5 border=0 align=right>
Самыми классными вещами на диске были "Strange brew", "Dance the night away", "Tales of brave Ulysses" и "S.W.L.A.B.R", благодаря которым альбом ворвался в пятерку лучших в США. 
Тогда же появился на свет хитовый сингл группы "Sunshine of your love", основанный на наиболее популярных хард-роковых риффах 60-ых. 
С двойным альбомом "Wheels of fire" "Cream" возглавили американские чарты в 1968 году, поставив себя на уровень рядом с <a class=white href="../menu-mus-b/beatles.php"><u>"Beatles"</u></a> и <b>Хендриксом</b>.
<br>Их решение о саморасформировании, объявленное в конце 1968 года, когда они были на пике славы, повергло в шок рок-аудиторию. 
Тем не менее, выпущенный в 1969-м "посмертный" альбом "Goodbye" попал на второе место в штатовских хит-парадах. На диске был представлен как студийный, так и концертный материал. Композиция "Badge" с этого альбома, написанная Эриком Клэптоном и <b>Джорджем Харрисоном</b> остается одной из наиболее любимых публикой вещей "Cream". 



