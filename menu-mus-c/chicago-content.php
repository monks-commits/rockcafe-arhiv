Основанная в 1966 году в Чикаго, США, группа оставалась хитмейкером в течение 70-х и 80-х. Поначалу команда называлась "Missing Links", затем "Big Thing", а позднее по предложению менеджера Джима Гурсио "Chicago Transit Authority". Первоначальный состав включал в себя Терри Ката (р. 31 января 1946, Чикаго, США, у. 23 января 1978; гитара, вокал), Питера Сетеру (р. 13 сентября 1944, Чикаго, США; бас, вокал), Роберта Лэмма (р. 13 октября 1944, Нью-Йорк, США; клавишные, вокал), Уолтера Паразайдера (Уолт Перри) (р. 14 марта 1945, Чикаго, США; саксофон), Дэнни Серафина (р. 28 августа 1948, Чикаго, США; барабаны), Джеймса Панкоу (р. 20 августа 1947, Чикаго, США; тромбон) и Ли Логнэйна (р. 21 октября 1941, Чикаго, США; труба). 
<br>"Chicago" отличалась рожковой секцией от других рок-групп середины 60-х, хотя "Chicago Transit Authority" был близок по саунду таким коллективам, как "Blood, Sweat And Tears" и "Electric flag". В 1967-68 годах благодаря Гурсио группа добилась относительной популярности, особенно в окрестностях Лос-Анджелеса, где они играли в клубах вещи типа "Whisky A-Go-Go". 
<br>Благодаря своей репутации, как продюсера "Blood, Sweat and Tears" и "Buckinghams" Гурсио подписал "Chicago" на "Columbia records" в январе 1969 года. В этом же году вышел первый альбом группы, на котором чувствовалось влияние джаза. Хотя он ни разу не попал в Топ 10, тем не менее, оставался в американских чартах в течение 171 недели. Также пользовались популярностью синглы "Does Anybody Really Know What Time It Is?" и "Beginnings". В 1970 группа сократила свое название до "Chicago". Все еще работая в стиле джаз-рок, они выпустили "Chicago II". В дальнейшем, за редкими исключениями каждый из альбомов группы получал свой порядковый номер. Обложка каждого диска представляла собой различную интересную обработку эмблемы группы, и многие из них поимели награды за свой дизайн. 
<br>В начале 70-х "Чикаго" начали отходить от своего джазового саунда к мэйнстриму, в результате чего получались такие знаменитые вещи, как "Colour My World", трансатлантический хит 1976 года "If You Leave Me Now" и № 1 в 1982 году "Hard To Say I'm Sorry". Пять последовательных альбомов "Chicago" возглавили чарты в период между 1972 и 1975 годом; однако, в конце 70-х группа испытала резкий коммерческий спад и выкарабкалась из ямы только в начале 80-х. 
<br>В 1974-м Лэмм записал довольно неудачный сольник. В этом же году состав группы пополнился бразильским ударником Лаудиром Де Оливейра. В следующем году группа гастролировала вместе с "Beach boys", чьим менеджером был тоже Гурсио. В 1977-м, после того, как "Chicago X" получил награду "Grammy", как лучший альбом, пути Гурсио и группы разошлись. 23 января 1978 один из основателей коллектива Кат был убит, случайно выстрелив в себя. На смену ему пришел гитарист Донни Дакус (но на следующий год он покинул состав и был заменен Крисом Пинником; Пинник ушел в 1981-м, когда к группе присоединился клавишник Билл Чамплин). 
<br>В 1981 году "Chicago" перешли с "Columbia" на лейбл "Full Moon Records", принадлежащий "Warner Brothers". В это же время Сетера записал сольник, имевший умеренный успех. После ухода из группы в 1985-м (его заменой стал Джейсон Шефф, сын басиста Элвиса Пресли Джерри Шеффа), он выпустил еще парочку сольных альбомов. 
<br>Подписание контракта с более влиятельным лейблом "Reprise Records" в 1988 году означало, что "Chicago" все еще рассматриваются, как группа, способная приносить коммерческий успех, несмотря на то, что она давно отошла от своих джаз-роковых корней. 
